//! `cpanfile` emission.
//!
//! `cpanfile` is the modern Perl dependency manifest read by `cpanm
//! --installdeps` (and `Carton`). It is content-light — every entry is fixed
//! at codegen time and the bindings consist of a single pure-Perl `Azul.pm`
//! module. There is no XS / C code to compile, so we don't need a
//! `Makefile.PL` (the experimental v1 generator used to emit one; v2 drops
//! it because `FFI::Platypus` does runtime loading and there's nothing to
//! `make`).
//!
//! References:
//!   https://metacpan.org/pod/cpanfile
//!   https://metacpan.org/dist/FFI-Platypus/view/lib/FFI/Platypus.pm

/// Build the `cpanfile` content.
///
/// The signature stays IR-agnostic so the call site doesn't need to change
/// when we eventually want to surface a real version number from the IR.
pub fn generate_cpanfile() -> String {
    let mut s = String::new();
    s.push_str("# WARNING: autogenerated by azul-doc codegen v2 -- DO NOT EDIT MANUALLY.\n");
    s.push_str("#\n");
    s.push_str("# Install with `cpanm --installdeps .` (or `carton install`).\n");
    s.push_str("# The bindings are pure-Perl; FFI::Platypus loads libazul at runtime.\n");
    s.push_str("\n");
    s.push_str("requires 'perl', '5.014';\n");
    s.push_str("requires 'FFI::Platypus', '>= 2.00';\n");
    s.push_str("requires 'FFI::CheckLib', '>= 0.28';\n");
    s.push_str("\n");
    s.push_str("on 'test' => sub {\n");
    s.push_str("    requires 'Test::More', '>= 0.98';\n");
    s.push_str("};\n");
    s
}
