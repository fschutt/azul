//! Emits the `pom.xml` Maven manifest that wraps the generated Java
//! sources and pulls in the JNA dependency.
//!
//! Produces a single deterministic file with no configurable knobs —
//! every Azul Java user starts from the same shape. The bundled native
//! libraries should be placed under `src/main/resources/<jna-rid>/`:
//!
//! - `linux-x86-64/libazul.so`
//! - `win32-x86-64/azul.dll`
//! - `darwin/libazul.dylib`
//!
//! At runtime JNA extracts the right one from the classpath and
//! resolves `Native.load("azul", ...)` to it.

/// Single public function returning the full `pom.xml` body.
pub fn generate_pom_xml() -> String {
    let s = r#"<?xml version="1.0" encoding="UTF-8"?>
<project xmlns="http://maven.apache.org/POM/4.0.0"
         xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
         xsi:schemaLocation="http://maven.apache.org/POM/4.0.0
                             http://maven.apache.org/xsd/maven-4.0.0.xsd">

    <!-- =====================================================================
         Auto-generated by azul-doc codegen v2 (lang_java/pom).
         DO NOT EDIT MANUALLY — re-run the generator instead.
         ===================================================================== -->

    <modelVersion>4.0.0</modelVersion>
    <groupId>com.azul</groupId>
    <artifactId>azul</artifactId>
    <version>1.0.0</version>
    <packaging>jar</packaging>

    <name>Azul Java Bindings</name>
    <description>Java (JNA) bindings for the Azul GUI framework.</description>

    <properties>
        <maven.compiler.source>11</maven.compiler.source>
        <maven.compiler.target>11</maven.compiler.target>
        <project.build.sourceEncoding>UTF-8</project.build.sourceEncoding>
    </properties>

    <dependencies>
        <!-- Java Native Access — pure-Java libffi binding, no JNI glue. -->
        <dependency>
            <groupId>net.java.dev.jna</groupId>
            <artifactId>jna</artifactId>
            <version>5.14.0</version>
        </dependency>
    </dependencies>

    <build>
        <plugins>
            <plugin>
                <groupId>org.apache.maven.plugins</groupId>
                <artifactId>maven-compiler-plugin</artifactId>
                <version>3.11.0</version>
            </plugin>
        </plugins>

        <!-- Native libraries packaged into the JAR under JNA's
             platform-specific resource layout. -->
        <resources>
            <resource>
                <directory>src/main/resources</directory>
                <includes>
                    <include>**/libazul.so</include>
                    <include>**/azul.dll</include>
                    <include>**/libazul.dylib</include>
                </includes>
            </resource>
        </resources>
    </build>
</project>
"#;
    s.to_string()
}
