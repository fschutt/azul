/// Library name that JNA passes to `Native.load(...)`. Resolves to
/// `azul.dll` on Windows, `libazul.so` on Linux, `libazul.dylib` on
/// macOS.
pub const LIBRARY_NAME: &str = "azul";

/// Java package the generated sources land in. Mirrors
/// `com.azul.<ClassName>` on disk.
pub const PACKAGE: &str = "com.azul";

/// Marker emitted before every Java source file in the multi-file
/// output. The orchestrator splits on this marker.
///
///     // ==FILE: AzulNative.java ==
///     package com.azul;
///     ...
///     // ==END==
///
/// Must remain stable; downstream tooling treats it as a contract.
pub const FILE_MARKER: &str = "// ==FILE: ";

/// Closing marker for a single Java source file.
pub const END_MARKER: &str = " ==";

/// Closing END marker line (used after each file body). Kept symmetric
/// with [`FILE_MARKER`] for ease of parsing.
pub const END_LINE: &str = "// ==END==";

/// Public entry point. Returns a single `String` containing every
/// `.java` source file the binding consists of, separated by
/// [`FILE_MARKER`] / [`END_LINE`] markers. The orchestrator splits on
/// these markers and writes each file separately.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    let mut out = String::new();

    // 1. Native FFI interface (one file: AzulNative.java)
    out.push_str(&emit_file(
        "AzulNative.java",
        |builder| functions::generate_native_interface(builder, ir, config),
        config,
    )?);

    // 2. POD/struct types â€” one file per type. Tagged-union enums are
    //    flattened into `<Type>_Tag.java` + `<Type>.java`.
    types::emit_all_type_files(&mut out, ir, config)?;

    // 3. Idiomatic AutoCloseable wrappers â€” one file per wrapped type.
    wrappers::emit_all_wrapper_files(&mut out, ir, config)?;

    // 4. Managed-FFI runtime helpers (host-invoker pattern). Two extra
    //    Java source files: AzulNativeManaged.java + AzulHostInvoker.java.
    managed::emit_files(&mut out, ir, config)?;

    Ok(out)
}

/// Helper: build a single Java source file using the supplied closure
/// and return it wrapped in `FILE_MARKER` / `END_LINE` markers.
pub(crate) fn emit_file<F>(filename: &str, body: F, config: &CodegenConfig) -> Result<String>
where
    F: FnOnce(&mut CodeBuilder) -> Result<()>,
{
    let mut builder = CodeBuilder::new(&config.indent);
    builder.line(&format!("{}{}{}", FILE_MARKER, filename, END_MARKER));
    builder.line(&format!("// Auto-generated by azul-doc codegen v2 (lang_java)."));
    builder.line("// DO NOT EDIT MANUALLY â€” re-run the generator instead.");
    builder.blank();
    builder.line(&format!("package {};", PACKAGE));
    builder.blank();
    emit_imports(&mut builder);
    body(&mut builder)?;
    builder.blank();
    builder.line(END_LINE);
    builder.blank();
    Ok(builder.finish())
}

/// Standard imports every generated Java source file needs.
fn emit_imports(builder: &mut CodeBuilder) {
    builder.line("import com.sun.jna.Library;");
    builder.line("import com.sun.jna.Native;");
    builder.line("import com.sun.jna.Pointer;");
    builder.line("import com.sun.jna.Structure;");
    builder.line("import com.sun.jna.Union;");
    builder.line("import java.util.Arrays;");
    builder.line("import java.util.List;");
    builder.blank();
}

