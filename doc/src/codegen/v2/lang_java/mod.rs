//! Java binding generator (JNA â€” Java Native Access).
//!
//! Generates idiomatic Java bindings from the IR. Unlike most other
//! generators in `v2/`, the Java target *cannot* live in a single
//! source file: the JCP convention is one public top-level class per
//! `.java` file. We therefore emit a multi-file string whose chunks
//! are demarcated with [`FILE_MARKER`] / [`END_MARKER`]. The
//! orchestrator (or the test harness) splits the string and writes each
//! chunk to disk under `src/main/java/com/azul/<ClassName>.java`.
//!
//! The high-level layout of the output is:
//!
//! 1. `AzulNative.java`   â€” `interface AzulNative extends Library` with
//!    every C-ABI symbol declared verbatim. Uses
//!    [`net.java.dev.jna.Native.load`] to bind to the prebuilt
//!    `azul.{dll,so,dylib}`.
//! 2. `<Type>.java` per FFI struct â€” a JNA `Structure` subclass with
//!    public fields, a `getFieldOrder()` override, and `ByValue` /
//!    `ByReference` inner classes for pass-by-value vs pointer
//!    parameters.
//! 3. `<Type>_Tag.java` per tagged-union enum â€” a Java `enum` for the
//!    discriminator and `<Type>.java` for the outer payload `Union`
//!    helper structure.
//! 4. `<Type>.java` wrapper class for every type that has a matching
//!    `_delete` C function. Implements `AutoCloseable`.
//!
//! All of the JNA boilerplate (Structure subclass, ByValue, ByReference,
//! Union, Pointer) is dropped in by [`types`] and [`wrappers`]; this
//! module owns only orchestration and the file-marker plumbing.
//!
//! ## Wiring
//!
//! Not yet referenced from `v2/mod.rs`. The orchestrator is responsible
//! for adding `pub mod lang_java;` and a `generate_java(...)` helper
//! that splits the multi-file string and writes each chunk.
//!
//! ## Shared with `lang_kotlin`
//!
//! Kotlin and Java target the same JNA runtime. The primitive-type
//! mapping ([`map_jvm_type`]) is therefore shared with Kotlin via
//! `pub` exports; the Kotlin generator imports `super::lang_java`
//! type helpers and surfaces them with Kotlin sugar
//! (`Pointer?`, `Long`, `Int`).

