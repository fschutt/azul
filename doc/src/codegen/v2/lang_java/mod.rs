/// Convert a snake_case name to lowerCamelCase for Java method names.
pub fn snake_to_lower_camel(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    let mut upper = false;
    let mut first = true;
    for c in s.chars() {
        if c == '_' {
            upper = true;
        } else if upper {
            out.extend(c.to_uppercase());
            upper = false;
        } else if first {
            out.extend(c.to_lowercase());
            first = false;
        } else {
            out.push(c);
        }
    }
    out
}
