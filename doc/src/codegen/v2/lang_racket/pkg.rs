//! `info.rkt` package-metadata emission for the Racket generator.
//!
//! Racket's package system (`raco pkg`) reads an `info.rkt` for the
//! package name, version, and dependencies. The native shared library
//! (`libazul.{so,dylib,dll}`) is **not** shipped through the package
//! catalogue — `ffi-lib` loads it at runtime from the dynamic loader's
//! search path (or from `AZ_LIB_DIR`).
//!
//! The version is threaded from `ir.api_version` (never hard-coded), the
//! same rule the rockspec / gemspec / cabal / asd emitters follow.

/// Generate the contents of `info.rkt` as a `String`.
pub fn generate_info_rkt(version: &str) -> String {
    format!(
        r#";;;; info.rkt
;;;; Auto-generated by azul-doc codegen v2 (lang_racket).
;;;;
;;;; Racket package metadata for the Azul GUI framework bindings. The
;;;; bindings themselves live in azul.rkt and depend only on `ffi/unsafe`
;;;; (shipped with Racket). The native library libazul.{{so,dylib,dll}} must
;;;; be reachable via the dynamic loader (or AZ_LIB_DIR) at run time.
#lang info

(define collection "azul")
(define version "{version}")
(define pkg-desc "ffi/unsafe bindings for the Azul GUI framework")
(define pkg-authors '("Azul contributors"))
(define license '(MPL-2.0 OR MIT OR Apache-2.0))
(define deps '("base"))
"#,
    )
}
