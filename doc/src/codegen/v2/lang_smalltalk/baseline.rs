//! Emits the `BaselineOfAzul.st` Metacello baseline.
//!
//! Pharo packages are loaded via Metacello, the Smalltalk equivalent
//! of Cargo's manifest. A baseline is a class extending
//! `BaselineOf` with one method (`baseline:`) declaring the package
//! layout and dependencies. Users load the binding by:
//!
//! ```smalltalk
//! Metacello new
//!     baseline: 'Azul';
//!     repository: 'github://azul-gui/azul-smalltalk:main/src';
//!     load.
//! ```
//!
//! For now we declare three packages (`Azul-Native`, `Azul-Types`,
//! `Azul-Core`) corresponding to the three categories used in
//! `lang_smalltalk/{functions,types,wrappers}.rs`. This is a single
//! deterministic Tonel string with no configurable knobs.

/// Single public function returning the full `BaselineOfAzul.st`
/// body.
pub fn generate_baseline() -> String {
    let s = r#""============================================================================
 BaselineOfAzul — Metacello baseline for the Azul GUI bindings.
 Auto-generated by azul-doc codegen v2 (lang_smalltalk/baseline).
 DO NOT EDIT MANUALLY — re-run the generator instead.

 Load with:
   Metacello new
       baseline: 'Azul';
       repository: 'github://azul-gui/azul-smalltalk:main/src';
       load.

 The native shared library (libazul.so / libazul.dylib / azul.dll)
 must be reachable through the OS loader path; UnifiedFFI does not
 try to download it.
============================================================================"

Class {
    #name : 'BaselineOfAzul',
    #superclass : 'BaselineOf',
    #instVars : [ ],
    #classVars : [ ],
    #category : 'BaselineOfAzul'
}

{ #category : 'baselines' }
BaselineOfAzul >> baseline: spec [
    "Declare the three packages produced by the codegen and their
     internal load order."

    <baseline>
    spec for: #common do: [
        spec
            package: 'Azul-Native';
            package: 'Azul-Types' with: [ spec requires: #('Azul-Native') ];
            package: 'Azul-Core'  with: [ spec requires: #('Azul-Types' 'Azul-Native') ].
        spec
            group: 'Core'    with: #('Azul-Core' 'Azul-Types' 'Azul-Native');
            group: 'default' with: #('Core').
    ]
]
"#;
    s.to_string()
}
