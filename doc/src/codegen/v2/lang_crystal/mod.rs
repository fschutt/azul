//! Crystal binding generator.
//!
//! Emits a single `azul.cr` source file exposing the C-ABI of `libazul`
//! to Crystal programs. Crystal is archetype-A (C-ABI-direct): a `lib`
//! block declares C structs by value, C enums, C function-pointer proc
//! types, and every exported symbol as a `fun` binding. Modeled directly
//! on the Odin backend (same archetype).
//!
//! Unlike Zig — which parses `azul.h` via `@cImport` — Crystal has no C
//! header importer, so we translate the FFI surface explicitly (like the
//! Odin / Pascal / Fortran backends): every struct becomes a Crystal
//! `struct` (inside `lib`), every enum a Crystal `enum` with an explicit
//! backing integer, every tagged union a `union` of per-variant structs
//! (mirroring the tested `lang_c` layout), and every C-ABI function a
//! `fun <crystal_name> = <CName>(...) : Ret` binding inside `lib LibAzul`.
//!
//! # Callbacks are C-direct (the key simplification)
//!
//! A Crystal `Proc` written as `->(a : T) { ... }` and passed to a C
//! `fun` whose parameter is a `(T) -> R` proc-type compiles to a real C
//! function pointer (Crystal boxes the closure only when it captures; the
//! callbacks here capture nothing, so they are bare fn pointers). So the
//! counter's `on_click` / `layout` callbacks are plain Crystal procs
//! passed straight to `AzButton_setOnClick` / `AzWindowCreateOptions_create`.
//! There is NO host-invoker table and NO handle registration — exactly the
//! model used by `examples/zig/hello-world.zig` and the Odin backend. For
//! each callback typedef we emit a Crystal proc-type alias
//! (`alias AzButtonOnClickCallbackType = (AzRefAny, AzCallbackInfo) -> AzUpdate`),
//! and functions that take a callback-wrapper arg bind the RAW C symbol
//! taking that bare proc typedef (matching the raw `_setOnClick` variant
//! the DLL exports — the same one Zig sees through `azul.h`).
//!
//! # Order-independence
//!
//! Crystal resolves `lib`-scope declarations regardless of source order,
//! so — unlike C / Pascal — no forward declarations or topological sort
//! are needed. Types skipped for codegen (recursive / generic template)
//! are emitted as opaque pointer aliases (`alias AzXmlNode = Void*`) so
//! any by-pointer reference still resolves, mirroring the Odin backend.
//!
//! # Build / link requirements
//!
//! `@[Link("azul")]` on the `lib` emits `-lazul` to the linker; the user
//! drops `libazul.{so,dylib}` / `azul.dll` on the library path
//! (`--link-flags "-L."`). The generated `azul.cr` is `require`d by the
//! driver.

pub mod functions;
pub mod types;

use std::collections::BTreeSet;

use anyhow::Result;

use super::config::CodegenConfig;
use super::generator::CodeBuilder;
use super::ir::{
    ArgRefKind, CodegenIR, EnumDef, FieldRefKind, FunctionDef, StructDef, TypeCategory,
};

/// The C library name used in `@[Link("azul")]`.
/// Resolves to `libazul.so` / `libazul.dylib` / `azul.dll` per platform.
pub const LIB_NAME: &str = "azul";

/// The Crystal `lib` module name wrapping the C-ABI surface.
pub const LIB_MODULE: &str = "LibAzul";

/// Generate the full `azul.cr` source file.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    // Crystal idiom is 2-space indentation.
    let mut b = CodeBuilder::new("  ");

    emit_header(&mut b);
    b.line(&format!("@[Link(\"{}\")]", LIB_NAME));
    b.line(&format!("lib {}", LIB_MODULE));
    b.blank();

    // Track every emitted lib-level name so a duplicate (a struct + a
    // type-alias sharing a name, a case-collision, …) can never produce a
    // Crystal redefinition error.
    let mut emitted: BTreeSet<String> = BTreeSet::new();

    types::generate_types(&mut b, ir, config, &mut emitted);
    functions::generate_funs(&mut b, ir, config);

    b.line("end");
    b.blank();

    // Idiomatic top-level type aliases dropping the `Az` prefix
    // (`alias Dom = LibAzul::AzDom`). The raw `LibAzul::Az*` names remain
    // available for both types and `fun` bindings.
    functions::generate_type_aliases(&mut b, ir, config);

    Ok(b.finish())
}

fn emit_header(b: &mut CodeBuilder) {
    b.line("# ============================================================================");
    b.line("# azul.cr — Crystal bindings for the Azul GUI framework");
    b.line("# Auto-generated by azul-doc codegen v2 (lang_crystal). DO NOT EDIT MANUALLY.");
    b.line("# ============================================================================");
    b.line("#");
    b.line("# The C-ABI surface is translated explicitly: structs -> struct, enums ->");
    b.line("# enum, tagged unions -> union, and every exported symbol is declared as a");
    b.line("# `fun` binding inside the `@[Link(\"azul\")] lib LibAzul` block below. The C");
    b.line("# symbol names are preserved verbatim on the right of each `fun` (AzApp_create,");
    b.line("# ...); the Crystal-side name lowercases the first letter (azApp_create) so it");
    b.line("# is a legal method name (a capitalized call would parse as a constant).");
    b.line("#");
    b.line("# Callbacks are C-direct: pass a plain non-capturing `->(...) { ... }` proc");
    b.line("# straight to AzButton_setOnClick / AzWindowCreateOptions_create — no");
    b.line("# host-invoker table, exactly like the Zig / Odin / C bindings.");
    b.line("#");
    b.line("# Idiomatic type aliases without the `Az` prefix (Dom = LibAzul::AzDom) are");
    b.line("# emitted at the bottom; the raw LibAzul::Az* names remain available.");
    b.line("#");
    b.line("# Build: place libazul.{so,dylib}/azul.dll on the link path and build with");
    b.line("#   crystal build hello-world.cr --link-flags \"-L.\"");
    b.line("# ============================================================================");
    b.blank();
}

// ============================================================================
// Shared name / type helpers (used by submodules)
// ============================================================================

/// The `Az`-prefixed FFI type name for an IR type (e.g. `Dom` -> `AzDom`).
pub fn ffi_type_name(name: &str) -> String {
    format!("Az{}", name)
}

/// The Crystal-side method name for a C `fun` binding. Crystal method
/// names must begin with a lowercase letter (a capitalized identifier is
/// parsed as a constant / type). All exported symbols start with `Az`, so
/// lowercasing only the first character yields a legal, still-unique name
/// (`AzApp_create` -> `azApp_create`).
pub fn crystal_fun_name(c_name: &str) -> String {
    let mut chars = c_name.chars();
    match chars.next() {
        Some(first) => {
            let mut out = String::new();
            for c in first.to_lowercase() {
                out.push(c);
            }
            out.push_str(chars.as_str());
            out
        }
        None => c_name.to_string(),
    }
}

/// Is `name` a type declared somewhere in the IR (so it resolves to an
/// `Az`-prefixed Crystal declaration)?
pub fn is_ir_type(name: &str, ir: &CodegenIR) -> bool {
    let n = name.trim();
    ir.find_struct(n).is_some()
        || ir.find_enum(n).is_some()
        || ir.find_type_alias(n).is_some()
        || ir.callback_typedefs.iter().any(|c| c.name == n)
}

/// Map a Rust/IR value type to its Crystal equivalent (by-value position).
///
/// Primitives map to Crystal's native fixed-width types; `usize`/`isize`
/// map to `LibC::SizeT`/`LibC::SSizeT` (== C `size_t`/`ssize_t`).
/// Pointers/references are delegated to [`ptr_to_crystal`]. Anything the
/// IR knows becomes `Az<Name>`; anything else degrades to `Void*`.
pub fn map_type_to_crystal(rust_type: &str, ir: &CodegenIR) -> String {
    let t = rust_type.trim();

    if let Some(rest) = t.strip_prefix("*const ") {
        return ptr_to_crystal(rest, ir);
    }
    if let Some(rest) = t.strip_prefix("*mut ") {
        return ptr_to_crystal(rest, ir);
    }
    if let Some(rest) = t.strip_prefix("&mut ") {
        return ptr_to_crystal(rest, ir);
    }
    if let Some(rest) = t.strip_prefix('&') {
        return ptr_to_crystal(rest, ir);
    }

    // Arrays: `[T; N]` -> `<CrystalT>[N]` (Crystal StaticArray shorthand).
    if t.starts_with('[') && t.ends_with(']') {
        let inner = &t[1..t.len() - 1];
        if let Some(semi) = inner.rfind(';') {
            let elem = inner[..semi].trim();
            if let Ok(n) = inner[semi + 1..].trim().parse::<usize>() {
                return format!("{}[{}]", map_type_to_crystal(elem, ir), n);
            }
        }
    }

    match t {
        // Void in value position never really occurs (void returns are
        // handled by omitting the `: ...`); degrade to an opaque ptr.
        "void" | "c_void" | "()" => "Void*".to_string(),

        "bool" | "GLboolean" => "Bool".to_string(),

        "u8" | "c_uchar" => "UInt8".to_string(),
        "i8" | "c_char" => "Int8".to_string(),
        "u16" | "c_ushort" => "UInt16".to_string(),
        "i16" | "c_short" => "Int16".to_string(),
        "u32" | "c_uint" | "GLuint" | "GLenum" | "GLbitfield" => "UInt32".to_string(),
        "i32" | "c_int" | "GLint" | "GLsizei" => "Int32".to_string(),
        // C `long`/`unsigned long` are 8 bytes on the LP64 targets azul
        // ships (Linux/macOS) and 4 on Windows LLP64; azul's public
        // surface does not use them, so mapping to 64-bit is safe here.
        "u64" | "c_ulong" | "c_ulonglong" | "GLuint64" => "UInt64".to_string(),
        "i64" | "c_long" | "c_longlong" | "GLint64" => "Int64".to_string(),
        "f32" | "c_float" | "GLfloat" | "GLclampf" => "Float32".to_string(),
        "f64" | "c_double" | "GLdouble" | "GLclampd" => "Float64".to_string(),
        // Pointer-sized integers: Crystal LibC::SizeT/SSizeT == size_t/ssize_t.
        "usize" | "size_t" | "uintptr_t" => "LibC::SizeT".to_string(),
        "isize" | "ssize_t" | "intptr_t" | "GLsizeiptr" | "GLintptr" => "LibC::SSizeT".to_string(),

        _ => {
            if is_ir_type(t, ir) {
                ffi_type_name(t)
            } else {
                "Void*".to_string()
            }
        }
    }
}

/// Map the pointee of a pointer/reference to a Crystal pointer expression.
///
/// - `void`/`c_void` -> `Void*`
/// - a known IR type -> `Az<Name>*`
/// - a primitive -> `<CrystalT>*`
/// - anything else -> `Void*`
pub fn ptr_to_crystal(inner: &str, ir: &CodegenIR) -> String {
    let inner = inner.trim();
    match inner {
        "c_void" | "void" | "()" => "Void*".to_string(),
        _ => {
            if is_ir_type(inner, ir) {
                format!("{}*", ffi_type_name(inner))
            } else {
                let mapped = map_type_to_crystal(inner, ir);
                if mapped == "Void*" {
                    "Void*".to_string()
                } else {
                    format!("{}*", mapped)
                }
            }
        }
    }
}

/// Map a `(type_name, FieldRefKind)` pair to the Crystal field/param type.
pub fn field_type_for_ref_kind(
    type_name: &str,
    ref_kind: &FieldRefKind,
    ir: &CodegenIR,
) -> String {
    match ref_kind {
        FieldRefKind::Owned => map_type_to_crystal(type_name, ir),
        FieldRefKind::Ref
        | FieldRefKind::RefMut
        | FieldRefKind::Ptr
        | FieldRefKind::PtrMut
        | FieldRefKind::Boxed
        | FieldRefKind::OptionBoxed => ptr_to_crystal(type_name, ir),
    }
}

/// Map a `(type_name, ArgRefKind)` pair to the Crystal argument type.
pub fn arg_type_for_ref_kind(type_name: &str, ref_kind: &ArgRefKind, ir: &CodegenIR) -> String {
    match ref_kind {
        ArgRefKind::Owned => map_type_to_crystal(type_name, ir),
        ArgRefKind::Ref | ArgRefKind::RefMut | ArgRefKind::Ptr | ArgRefKind::PtrMut => {
            ptr_to_crystal(type_name, ir)
        }
    }
}

/// Sanitize a name for use as a Crystal identifier (struct field / `fun`
/// parameter). Crystal reserved words get a trailing underscore.
pub fn sanitize_identifier(name: &str) -> String {
    if is_crystal_keyword(name) {
        format!("{}_", name)
    } else {
        name.to_string()
    }
}

/// Capitalize the first letter — Crystal enum members are constants and
/// must begin with an uppercase letter.
pub fn enum_member_name(name: &str) -> String {
    let mut chars = name.chars();
    match chars.next() {
        Some(first) if first.is_ascii_lowercase() => {
            let mut out = String::new();
            for c in first.to_uppercase() {
                out.push(c);
            }
            out.push_str(chars.as_str());
            out
        }
        _ => name.to_string(),
    }
}

