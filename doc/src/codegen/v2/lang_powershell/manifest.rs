//! Emits the `Azul.psd1` PowerShell module manifest that pairs with the
//! generated `Azul.psm1`.
//!
//! The manifest is the canonical "package" descriptor for a PowerShell
//! module. `Import-Module Azul.psd1` (or `Import-Module Azul`) reads this
//! file to learn which `.psm1` to load, what version it is, and which
//! commands it exports.
//!
//! We keep the manifest minimal and deterministic — no external
//! dependencies, broad PowerShell version compatibility (5.1 desktop and
//! 7+ core), wildcard exports so every `function New-Azul*`,
//! `Invoke-Azul*`, etc. that lands in `Azul.psm1` becomes discoverable.

/// Single public function returning the full `Azul.psd1` body.
///
/// This is intentionally not parameterised: every Azul PowerShell user
/// starts from the same shape, and any per-deployment customisation
/// belongs in a sibling profile script, not the autogenerated manifest.
pub fn generate_psd1() -> String {
    // PowerShell manifests are PowerShell hashtable literals, not JSON.
    // The keys we set:
    //   RootModule          — the .psm1 to dot-source on import
    //   ModuleVersion       — semver-ish; bumped per release of Azul
    //   GUID                — fixed per-module GUID (regenerate only on
    //                         major rebrand; PowerShell uses it to
    //                         distinguish modules with identical names)
    //   Author / Description — human-readable metadata
    //   PowerShellVersion   — minimum supported PS host
    //   FunctionsToExport   — wildcard verbs we know we emit
    //   CmdletsToExport     — empty (we ship script functions, not
    //                         compiled cmdlets)
    //   VariablesToExport   — empty
    //   AliasesToExport     — empty
    let s = r#"@{
    # =====================================================================
    # Auto-generated by azul-doc codegen v2 (lang_powershell/manifest).
    # DO NOT EDIT MANUALLY — re-run the generator instead.
    # =====================================================================

    RootModule        = 'Azul.psm1'
    ModuleVersion     = '1.0.0'
    GUID              = 'a8f2d1c4-3b7e-4a91-9c2d-5e6f7a8b9c0d'
    Author            = 'Azul Contributors'
    CompanyName       = 'Azul'
    Copyright         = '(c) Azul Contributors. All rights reserved.'
    Description       = 'PowerShell bindings for the Azul GUI framework. Wraps the C# P/Invoke layer via Add-Type.'
    PowerShellVersion = '5.1'

    # The .psm1 itself uses Add-Type to JIT-compile the embedded C#.
    # No external assemblies or modules are required at import time.
    RequiredAssemblies = @()
    RequiredModules    = @()

    # We export every function whose name follows the Verb-Azul* pattern.
    # The .psm1 also calls Export-ModuleMember explicitly; the manifest
    # filter is the second gate.
    FunctionsToExport = @(
        'New-Azul*',
        'Invoke-Azul*',
        'Get-Azul*',
        'Set-Azul*',
        'Remove-Azul*',
        'Add-Azul*',
        'Clear-Azul*',
        'Update-Azul*',
        'Start-Azul*',
        'Stop-Azul*'
    )
    CmdletsToExport   = @()
    VariablesToExport = @()
    AliasesToExport   = @()

    PrivateData = @{
        PSData = @{
            Tags         = @('GUI', 'Azul', 'FFI', 'PInvoke', 'CrossPlatform')
            ProjectUri   = 'https://azul.rs'
            ReleaseNotes = 'Auto-generated bindings. See the upstream Azul changelog.'
        }
    }
}
"#;
    s.to_string()
}
