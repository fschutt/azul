//! PowerShell binding generator.
//!
//! Strategy — the killer move: PowerShell's `Add-Type -TypeDefinition` JIT
//! compiles arbitrary C# at module-import time. That means the entire C#
//! P/Invoke layer the C# generator already produces (Azul.cs) can be
//! embedded *verbatim* inside `Azul.psm1` as a here-string, and we get
//! P/Invoke + `[StructLayout]` + `IDisposable` wrappers + delegate types
//! for free. We then layer idiomatic Verb-Noun PowerShell function shims
//! on top so users can write `New-AzulApp` instead of `[Azul.App]::Create()`.
//!
//! Output structure (`Azul.psm1`):
//!
//! ```text
//! # Azul.psm1 — PowerShell bindings for the Azul GUI framework.
//! $csharpSource = @'
//! <full Azul.cs from lang_csharp::generate>
//! '@
//! Add-Type -TypeDefinition $csharpSource -Language CSharp `
//!     -ReferencedAssemblies @('System.dll')
//!
//! function New-AzulApp { ... }       # wraps [Azul.App]::Create(...)
//! function Invoke-AzulApp { ... }    # wraps $app.Run(...)
//! ...
//! Export-ModuleMember -Function New-Azul*, Invoke-Azul*, Get-Azul*, Set-Azul*
//! ```
//!
//! ### Here-string delimiter strategy
//!
//! PowerShell offers two here-strings: `@"…"@` (interpolating) and
//! `@'…'@` (literal). The C# embed must use the **single-quoted literal**
//! form so PowerShell does not try to expand `$variable` references that
//! appear inside C# identifiers (e.g. autogenerated names that happen to
//! start with `$` are uncommon but possible). The literal here-string
//! terminator is the two-character sequence `'@` at the start of a line.
//! Before emitting we scan the C# source for that sequence and abort if
//! present (we never emit a generator that silently produces broken
//! PowerShell).
//!
//! ### What's exposed
//!
//! - Every `[StructLayout]` struct, `enum : uint`, and `[DllImport]` the
//!   C# layer produced (compiled at import time).
//! - Every `IDisposable` wrapper class (`Azul.App`, `Azul.Dom`, …).
//! - Verb-Noun shims (`New-AzulApp`, `Invoke-AzulAppRun`, …) that
//!   delegate to the wrapper classes — see `cmdlets.rs`.

pub mod cmdlets;
pub mod manifest;

use anyhow::{anyhow, Result};

use super::config::CodegenConfig;
use super::generator::CodeBuilder;
use super::ir::CodegenIR;

/// Public entry point. Returns the full `Azul.psm1` source as a String.
///
/// The caller is expected to write the result to disk (typically
/// `target/codegen/v2/Azul.psm1`) and to separately emit the
/// accompanying `Azul.psd1` via [`manifest::generate_psd1`].
///
/// This generator internally calls [`super::lang_csharp::generate`] to
/// obtain the embedded C# source. Any breakage in the C# generator
/// surfaces here.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    // 1. Generate the embedded C# source by reusing the production C#
    //    generator. Any change to the C# layout (e.g. namespace rename)
    //    flows through to PowerShell automatically.
    let csharp_source = super::lang_csharp::generate(ir, config)?;

    // 2. Validate the embedded C# does not contain the literal
    //    here-string terminator `'@` at the start of a line, which would
    //    truncate the embed inside the .psm1. We reject rather than
    //    silently emit broken PowerShell.
    validate_no_literal_terminator(&csharp_source)?;

    // 3. Build the .psm1 source.
    let mut builder = CodeBuilder::new("    ");
    generate_header(&mut builder);
    generate_csharp_embed(&mut builder, &csharp_source);
    generate_addtype_block(&mut builder);
    generate_helper_functions(&mut builder);
    cmdlets::generate_cmdlets(&mut builder, ir, config)?;
    generate_footer(&mut builder);

    Ok(builder.finish())
}

// ============================================================================
// Section emitters
// ============================================================================

fn generate_header(builder: &mut CodeBuilder) {
    builder.line(
        "# ============================================================================",
    );
    builder.line("# Azul.psm1 — PowerShell bindings for the Azul GUI framework.");
    builder.line("# Auto-generated by azul-doc codegen v2 (lang_powershell).");
    builder.line("# DO NOT EDIT MANUALLY — re-run the generator instead.");
    builder.line("#");
    builder.line("# This module embeds a full C# P/Invoke layer (compiled at import time");
    builder.line("# via Add-Type) and exposes idiomatic Verb-Noun shims around the");
    builder.line("# resulting .NET classes.");
    builder.line("#");
    builder.line("# Disposable types: PowerShell does not have a `using` statement in all");
    builder.line("# hosts. Wrap manual lifetime management in try / finally:");
    builder.line("#");
    builder.line("#     $app = New-AzulApp -Data $data -Config $cfg");
    builder.line("#     try   { Invoke-AzulAppRun -Instance $app -Options $opts }");
    builder.line("#     finally { $app.Dispose() }");
    builder.line("#");
    builder.line("# The wrapper classes also have finalizers so failure to call");
    builder.line("# Dispose() leaks until the next GC pass — safe but not prompt.");
    builder.line(
        "# ============================================================================",
    );
    builder.blank();

    // Strict mode keeps user errors loud rather than silent.
    builder.line("Set-StrictMode -Version Latest");
    builder.line("$ErrorActionPreference = 'Stop'");
    builder.blank();
}

fn generate_csharp_embed(builder: &mut CodeBuilder, csharp: &str) {
    builder.line("# --------------------------------------------------------------------------");
    builder.line("# Embedded C# P/Invoke definitions. Compiled at import time via Add-Type.");
    builder.line("# Single-quoted here-string (@'...'@) — PowerShell does NOT interpolate");
    builder.line("# variables inside, so any $-prefixed identifiers in the C# survive.");
    builder.line("# --------------------------------------------------------------------------");
    builder.line("$AzulCSharpSource = @'");
    // The here-string terminator must sit at column 0. CodeBuilder
    // applies indentation only via `line`; raw() emits without
    // indentation, which is what we need for the embed.
    builder.raw(csharp);
    if !csharp.ends_with('\n') {
        builder.raw("\n");
    }
    builder.line("'@");
    builder.blank();
}

fn generate_addtype_block(builder: &mut CodeBuilder) {
    builder.line("# Compile the embedded C#. The first import pays the JIT cost (~1s on");
    builder.line("# Windows PowerShell 5.1, faster on PS Core 7+); subsequent imports in");
    builder.line("# the same session are no-ops because the [Azul.App] type is already");
    builder.line("# loaded.");
    builder.line("if (-not ('Azul.App' -as [type])) {");
    builder.indent();
    builder.line("try {");
    builder.indent();
    builder.line("Add-Type -TypeDefinition $AzulCSharpSource -Language CSharp `");
    builder.indent();
    builder.line("-ReferencedAssemblies @('System.dll') -ErrorAction Stop");
    builder.dedent();
    builder.dedent();
    builder.line("} catch {");
    builder.indent();
    builder.line("Write-Error \"Failed to load Azul native bindings: $($_.Exception.Message)\"");
    builder.line("Write-Error 'Ensure azul.dll / libazul.so / libazul.dylib is on the search path,'");
    builder.line("Write-Error 'or call Set-AzulLibraryPath to add the directory containing it.'");
    builder.line("throw");
    builder.dedent();
    builder.line("}");
    builder.dedent();
    builder.line("}");
    builder.blank();
}

fn generate_helper_functions(builder: &mut CodeBuilder) {
    builder.line("# --------------------------------------------------------------------------");
    builder.line("# Helper: prepend a directory to the native-library search path.");
    builder.line("# Useful when azul.dll / libazul.so lives next to the .psm1 rather than");
    builder.line("# in a system directory. Call BEFORE invoking any New-Azul* function.");
    builder.line("# --------------------------------------------------------------------------");
    builder.line("function Set-AzulLibraryPath {");
    builder.indent();
    builder.line("[CmdletBinding()]");
    builder.line("param(");
    builder.indent();
    builder.line("[Parameter(Mandatory=$false, Position=0)]");
    builder.line("[string]$Path = $PSScriptRoot");
    builder.dedent();
    builder.line(")");
    builder.line("if ($IsWindows -or $env:OS -eq 'Windows_NT') {");
    builder.indent();
    builder.line("$env:PATH = \"$Path;$env:PATH\"");
    builder.dedent();
    builder.line("} else {");
    builder.indent();
    builder.line("$env:LD_LIBRARY_PATH = \"$Path:$env:LD_LIBRARY_PATH\"");
    builder.line("$env:DYLD_LIBRARY_PATH = \"$Path:$env:DYLD_LIBRARY_PATH\"");
    builder.dedent();
    builder.line("}");
    builder.dedent();
    builder.line("}");
    builder.blank();
}

fn generate_footer(builder: &mut CodeBuilder) {
    builder.line("# --------------------------------------------------------------------------");
    builder.line("# End of Azul.psm1.");
    builder.line("# --------------------------------------------------------------------------");
}

// ============================================================================
// Validation
// ============================================================================

/// Reject a C# source that would terminate the literal here-string early.
///
/// PowerShell closes a `@'...'@` literal here-string when a line begins
/// with the two-character sequence `'@`. If that sequence appears in the
/// embedded C# we cannot safely emit a here-string: switching to the
/// double-quoted form is not a fix because then we would have to escape
/// every `$` and backtick. Instead we abort and ask the C# generator to
/// avoid the sequence.
fn validate_no_literal_terminator(csharp: &str) -> Result<()> {
    for (idx, line) in csharp.lines().enumerate() {
        // Strip leading whitespace; PowerShell allows the terminator to
        // start at column 0 only, but emitting indented C# whose
        // *trimmed* start is `'@` would still be brittle when a
        // formatter normalises the file. Be strict.
        if line.trim_start().starts_with("'@") {
            return Err(anyhow!(
                "Embedded C# contains the PowerShell literal here-string terminator `'@` at line {} — \
                 the .psm1 cannot embed this source as-is. \
                 Adjust the C# generator to avoid emitting `'@` at the start of a line, \
                 or extend lang_powershell::mod.rs to use a different delimiter strategy.",
                idx + 1
            ));
        }
    }
    Ok(())
}
