//! Common Lisp (CFFI) binding generator.
//!
//! Generates a single `azul.lisp` source file that exposes the C-ABI of
//! `libazul` to Common Lisp implementations (SBCL, CCL, ECL, ABCL,
//! Allegro CL, ...) via the portable **CFFI** library.
//!
//! ## File structure
//!
//! 1. File banner (Lisp `;;;` line comments).
//! 2. Two `defpackage` forms:
//!    - `:azul-internal` — holds the raw `%az-*` FFI symbols (the leading
//!      `%` follows the convention of "internal/raw" in many Lisp
//!      libraries: `cl-fad`, `bordeaux-threads`, etc.).
//!    - `:azul` — the user-facing package; exports CLOS classes,
//!      `with-*` macros, idiomatic `make-*` constructors and named
//!      methods. Uses `:cl` and `:cffi`.
//! 3. `cffi:define-foreign-library` + `cffi:use-foreign-library` that
//!    locate `libazul.{so,dylib}` / `azul.dll` automatically.
//! 4. Type definitions inside `:azul-internal`:
//!    - `cffi:defcenum` for unit-only enums.
//!    - `cffi:defcstruct` for POD structs.
//!    - For tagged unions: a `cffi:defcenum` for the tag plus a
//!      `cffi:defcstruct` per variant payload plus a wrapping
//!      `cffi:defcunion`. (See [`types`].)
//! 5. `cffi:defcfun` declarations — one per IR function, mapping the
//!    C-ABI symbol to a `%az-*` Lisp function.
//! 6. CLOS wrappers in `:azul`:
//!    - `(defclass app () ((ptr ...)))` per heap-owning type.
//!    - `(defmethod close-app ...)` calling the matching `_delete`.
//!    - `(defmacro with-app ...)` doing the `unwind-protect` for users.
//!    - Idiomatic `make-app`, `app-run`, ... wrappers that call the
//!      `%az-*` raw FFI symbols.
//!
//! ## Why CFFI
//!
//! CFFI is the de facto portable Lisp FFI; it works on every major
//! implementation. Its `defcfun` / `defcstruct` / `defcunion` /
//! `defcenum` macros are structurally close to LuaJIT's `ffi.cdef`:
//! declare types, declare functions, then load the library — which
//! makes the Lua generator a useful structural reference, even though
//! the actual S-expression syntax differs.
//!
//! ## Wiring
//!
//! This module is intentionally *not* wired from `v2/mod.rs` yet. The
//! orchestrator is responsible for adding `pub mod lang_lisp;` and the
//! corresponding `generate_all` step that writes `azul.lisp` and
//! `azul.asd`.

pub mod asd;
pub mod functions;
pub mod types;
pub mod wrappers;

use anyhow::Result;

use super::config::CodegenConfig;
use super::generator::CodeBuilder;
use super::ir::CodegenIR;

/// Library name used in `cffi:define-foreign-library`. Matches the
/// prebuilt artifact name without extension. CFFI resolves
/// `libazul.so` / `libazul.dylib` / `azul.dll` automatically.
pub const LIB_NAME: &str = "azul";

/// Public entry point. Produces the complete `azul.lisp` source as a
/// `String`. The caller writes it to disk (e.g.
/// `target/codegen/v2/azul.lisp`) and emits the accompanying
/// `azul.asd` via [`asd::generate_asd`].
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    let mut builder = CodeBuilder::new("  ");

    // 1. Banner
    emit_header(&mut builder);

    // 2. Package declarations
    emit_packages(&mut builder);

    // 3. Library load
    builder.line("(in-package :azul-internal)");
    builder.blank();
    emit_library_load(&mut builder);

    // 4. Type declarations (enums, structs, tagged unions, callbacks)
    types::generate_types(&mut builder, ir, config)?;

    // 5. defcfun bindings (one per IR function)
    functions::generate_defcfuns(&mut builder, ir, config)?;

    // 6. CLOS wrappers in :azul
    builder.blank();
    builder.line("(in-package :azul)");
    builder.blank();
    wrappers::generate_wrappers(&mut builder, ir, config)?;

    Ok(builder.finish())
}

fn emit_header(builder: &mut CodeBuilder) {
    builder.line(";;;; ============================================================================");
    builder.line(";;;; Auto-generated Common Lisp (CFFI) bindings for the Azul GUI framework.");
    builder.line(";;;; Generated by azul-doc codegen v2 (lang_lisp).");
    builder.line(";;;; DO NOT EDIT MANUALLY -- re-run the generator instead.");
    builder.line(";;;;");
    builder.line(";;;; Compatible with any Common Lisp implementation supported by CFFI:");
    builder.line(";;;;   SBCL, CCL, ECL, ABCL, Allegro CL, LispWorks, CMUCL, CLISP, ...");
    builder.line(";;;;");
    builder.line(";;;; Two packages:");
    builder.line(";;;;   :azul-internal -- raw FFI symbols (`%az-*`).");
    builder.line(";;;;   :azul          -- idiomatic CLOS wrappers + with-* macros.");
    builder.line(";;;; ============================================================================");
    builder.blank();
}

fn emit_packages(builder: &mut CodeBuilder) {
    // Internal package: holds raw %az-* FFI symbols.
    builder.line("(cl:in-package :cl-user)");
    builder.blank();
    builder.line("(cl:defpackage :azul-internal");
    builder.line("  (:use :cl :cffi))");
    builder.blank();

    // Public package: re-exports CLOS classes + idiomatic helpers.
    builder.line("(cl:defpackage :azul");
    builder.line("  (:use :cl)");
    builder.line("  ;; Public symbols are exported via (export ...) calls below to keep");
    builder.line("  ;; the package definition lean and additive.");
    builder.line("  )");
    builder.blank();
}

fn emit_library_load(builder: &mut CodeBuilder) {
    builder.line(";; -----------------------------------------------------------------------------");
    builder.line(";; Foreign library");
    builder.line(";; -----------------------------------------------------------------------------");
    builder.line("(define-foreign-library libazul");
    builder.line(&format!(
        "  (:darwin (:or \"lib{}.dylib\" \"{}.dylib\"))",
        LIB_NAME, LIB_NAME
    ));
    builder.line(&format!(
        "  (:unix    (:or \"lib{}.so\" \"{}.so\"))",
        LIB_NAME, LIB_NAME
    ));
    builder.line(&format!(
        "  (:windows (:or \"{}.dll\" \"lib{}.dll\"))",
        LIB_NAME, LIB_NAME
    ));
    builder.line(&format!("  (t (:default \"lib{}\")))", LIB_NAME));
    builder.blank();
    builder.line("(use-foreign-library libazul)");
    builder.blank();
}

