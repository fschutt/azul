//! Emits the `Azul.csproj` project file that wraps the generated
//! `Azul.cs` and bundles prebuilt native libraries under
//! `runtimes/<rid>/native/`.
//!
//! .NET 6+ resolves a P/Invoke `[DllImport("azul")]` to the correct
//! native binary on each platform automatically when the natives live
//! at:
//!
//! - `runtimes/win-x64/native/azul.dll`
//! - `runtimes/linux-x64/native/libazul.so`
//! - `runtimes/osx-x64/native/libazul.dylib`
//! - `runtimes/osx-arm64/native/libazul.dylib`
//!
//! This produces a single deterministic .csproj string with no
//! configurable knobs — every Azul C# user starts from the same shape.
//! Pure stdlib, no external NuGet dependencies.

/// Single public function returning the full `Azul.csproj` body.
pub fn generate_csproj() -> String {
    // Note: tabs/spaces inside the literal are intentional and match the
    // canonical MSBuild output. Indentation is two spaces.
    let s = r#"<Project Sdk="Microsoft.NET.Sdk">

  <!-- =====================================================================
       Auto-generated by azul-doc codegen v2 (lang_csharp/csproj).
       DO NOT EDIT MANUALLY — re-run the generator instead.
       ===================================================================== -->

  <PropertyGroup>
    <!-- Keep in sync with examples/csharp/Hello.csproj and
         doc/guide/en/hello-world/csharp.md (single TFM story). -->
    <TargetFramework>net10.0</TargetFramework>
    <RuntimeIdentifiers>win-x64;linux-x64;osx-x64;osx-arm64</RuntimeIdentifiers>
    <LangVersion>10.0</LangVersion>
    <Nullable>disable</Nullable>
    <AllowUnsafeBlocks>true</AllowUnsafeBlocks>
    <!-- The .NET SDK globs `**/*.cs` into <Compile> by default; the ItemGroup
         below lists `Azul.cs` explicitly, so disable the default glob to avoid
         the duplicate `Compile` item (NETSDK1022). -->
    <EnableDefaultCompileItems>false</EnableDefaultCompileItems>
    <AssemblyName>Azul</AssemblyName>
    <RootNamespace>Azul</RootNamespace>
    <Version>1.0.0</Version>
    <Description>C# bindings for the Azul GUI framework.</Description>
    <PackageId>Azul.Net</PackageId>
    <Authors>Azul Contributors</Authors>
    <GenerateDocumentationFile>true</GenerateDocumentationFile>
    <NoWarn>$(NoWarn);CS1591</NoWarn>
  </PropertyGroup>

  <!-- The generated bindings file. -->
  <ItemGroup>
    <Compile Include="Azul.cs" />
  </ItemGroup>

  <!-- Native runtime payloads. .NET 6+ picks the correct one
       automatically based on the host RID at runtime. -->
  <ItemGroup>
    <None Include="runtimes/win-x64/native/azul.dll" Condition="Exists('runtimes/win-x64/native/azul.dll')">
      <Pack>true</Pack>
      <PackagePath>runtimes/win-x64/native/</PackagePath>
      <CopyToOutputDirectory>PreserveNewest</CopyToOutputDirectory>
    </None>
    <None Include="runtimes/linux-x64/native/libazul.so" Condition="Exists('runtimes/linux-x64/native/libazul.so')">
      <Pack>true</Pack>
      <PackagePath>runtimes/linux-x64/native/</PackagePath>
      <CopyToOutputDirectory>PreserveNewest</CopyToOutputDirectory>
    </None>
    <None Include="runtimes/osx-x64/native/libazul.dylib" Condition="Exists('runtimes/osx-x64/native/libazul.dylib')">
      <Pack>true</Pack>
      <PackagePath>runtimes/osx-x64/native/</PackagePath>
      <CopyToOutputDirectory>PreserveNewest</CopyToOutputDirectory>
    </None>
    <None Include="runtimes/osx-arm64/native/libazul.dylib" Condition="Exists('runtimes/osx-arm64/native/libazul.dylib')">
      <Pack>true</Pack>
      <PackagePath>runtimes/osx-arm64/native/</PackagePath>
      <CopyToOutputDirectory>PreserveNewest</CopyToOutputDirectory>
    </None>
  </ItemGroup>

</Project>
"#;
    s.to_string()
}
