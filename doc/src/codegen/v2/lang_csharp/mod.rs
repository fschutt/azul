//! C# binding generator
//!
//! Generates C# (P/Invoke) bindings from the IR. The generator emits a
//! single `Azul.cs` source file that contains:
//!
//! 1. POD/blittable structs as `[StructLayout(LayoutKind.Sequential)]`
//! 2. Tagged-union FFI structs as `[StructLayout(LayoutKind.Explicit)]`
//! 3. Plain C-style enums (`enum : uint`) for unit-only enums
//! 4. Cdecl callback delegates for every callback typedef
//! 5. P/Invoke `[DllImport]` extern declarations in a private `NativeMethods`
//! 6. `IDisposable` wrapper classes around opaque/disposable types with
//!    idiomatic, namespaced static method names (no `Az` prefix)
//! 7. A discriminator class hierarchy for tagged-union enums
//!
//! The output is a single `String` for the user to write to disk; this
//! mirrors `lang_python.rs` rather than implementing the
//! `LanguageGenerator` trait.
//!
//! Wiring: this module is intentionally not yet referenced from
//! `v2/mod.rs`. The orchestrator is responsible for adding the
//! `pub mod lang_csharp;` line and a `pub fn generate_csharp(...)`
//! entry point that mirrors `generate_python`.

pub mod csproj;
pub mod functions;
pub mod managed;
pub mod types;
pub mod wrappers;

use anyhow::Result;

use super::config::CodegenConfig;
use super::generator::CodeBuilder;
use super::ir::CodegenIR;

/// DLL name used in `[DllImport]`. Matches the prebuilt artifact's name
/// without extension; .NET resolves `azul` to `azul.dll` / `libazul.so`
/// / `libazul.dylib` automatically when natives are placed under
/// `runtimes/<rid>/native/`.
pub const DLL_NAME: &str = "azul";

/// Public entry point. Generates the complete `Azul.cs` file as a `String`.
///
/// The caller is expected to write the result to disk
/// (e.g. `target/codegen/v2/Azul.cs`) and to separately emit the
/// accompanying `Azul.csproj` via [`csproj::generate_csproj`].
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    let mut builder = CodeBuilder::new(&config.indent);

    // File banner
    generate_header(&mut builder);

    // using directives
    generate_using_directives(&mut builder);

    // Open `namespace Azul { ... }`
    builder.line("namespace Azul");
    builder.line("{");
    builder.indent();

    // 1. Type declarations: enums (unit), structs (POD), tagged-union FFI structs
    types::generate_types(&mut builder, ir, config)?;

    // 2. Callback delegate declarations (Cdecl, UnmanagedFunctionPointer)
    types::generate_callback_delegates(&mut builder, ir, config)?;

    // 3. `internal static class NativeMethods` containing every DllImport
    functions::generate_native_methods(&mut builder, ir, config)?;

    // 3b. Sibling `NativeMethodsManaged` class with the host-invoker imports.
    managed::emit_native_method_imports(&mut builder, ir);

    // 4. Idiomatic public wrapper classes (IDisposable + namespaced statics)
    wrappers::generate_wrappers(&mut builder, ir, config)?;

    // 5. Tagged-union public class hierarchy (sealed subclasses + Match())
    wrappers::generate_union_hierarchies(&mut builder, ir, config)?;

    // 6. Public `HostInvoker` class — RegisterCallback per kind +
    //    RefanyCreate / RefanyGet for user data.
    managed::emit_host_invoker_class(&mut builder, ir);

    // Close namespace
    builder.dedent();
    builder.line("}");

    Ok(builder.finish())
}

fn generate_header(builder: &mut CodeBuilder) {
    builder.line("// ============================================================================");
    builder.line("// Auto-generated C# bindings for the Azul GUI framework.");
    builder.line("// Generated by azul-doc codegen v2 (lang_csharp).");
    builder.line("// DO NOT EDIT MANUALLY.");
    builder.line("// ============================================================================");
    builder.blank();
}

fn generate_using_directives(builder: &mut CodeBuilder) {
    builder.line("using System;");
    builder.line("using System.Runtime.InteropServices;");
    builder.line("using System.Text;");
    builder.blank();
}

// ============================================================================
// Shared type-name helpers (used by submodules)
// ============================================================================

/// The Az-prefixed FFI type name for a given IR type
/// (e.g. `Dom` -> `AzDom`).
pub fn ffi_type_name(name: &str) -> String {
    format!("Az{}", name)
}

/// Map a Rust/IR type name to its C# equivalent.
///
/// `is_field` true means we are emitting a field of a `[StructLayout]`
/// type, where strings cannot be `string` (must be `IntPtr` or a
/// nested struct). For function parameters/returns we leave that
/// nuance to the caller.
pub fn map_type_to_csharp(rust_type: &str, ir: &CodegenIR) -> String {
    let trimmed = rust_type.trim();

    // Pointers
    if let Some(rest) = trimmed.strip_prefix("*const ") {
        // `*const c_char` is a C string. Surface it as IntPtr; the
        // wrapper layer can choose to marshal as string via
        // [MarshalAs(UnmanagedType.LPStr)] when desired.
        let _ = rest;
        return "IntPtr".to_string();
    }
    if let Some(rest) = trimmed.strip_prefix("*mut ") {
        let _ = rest;
        return "IntPtr".to_string();
    }
    if let Some(rest) = trimmed.strip_prefix("&mut ") {
        // We are P/Invoke-ing; references are emitted as IntPtr at the
        // FFI level. Wrappers can use ref/out for blittable structs
        // when desired.
        let _ = rest;
        return "IntPtr".to_string();
    }
    if let Some(rest) = trimmed.strip_prefix('&') {
        let _ = rest;
        return "IntPtr".to_string();
    }

    match trimmed {
        // Primitives
        "bool" => "bool".to_string(),
        "u8" | "c_uchar" | "char" => "byte".to_string(),
        "i8" | "c_char" => "sbyte".to_string(),
        "u16" => "ushort".to_string(),
        "i16" => "short".to_string(),
        "u32" | "c_uint" => "uint".to_string(),
        "i32" | "c_int" => "int".to_string(),
        "u64" => "ulong".to_string(),
        "i64" => "long".to_string(),
        "f32" => "float".to_string(),
        "f64" => "double".to_string(),
        "usize" => "UIntPtr".to_string(),
        "isize" => "IntPtr".to_string(),
        "c_void" | "()" | "void" => "void".to_string(),

        // Anything else: assume it's a known type and apply Az prefix
        _ => {
            // Resolve simple type aliases (e.g. `GLuint = u32`) to their
            // target type recursively. Aliases with `monomorphized_def`
            // are emitted as concrete types and keep their `Az<Name>`.
            if let Some(ta) = ir.find_type_alias(trimmed) {
                if ta.monomorphized_def.is_none() {
                    return map_type_to_csharp(&ta.target, ir);
                }
                return ffi_type_name(trimmed);
            }
            // Recursive struct/enum types in field positions can't be
            // expanded inline (would loop infinitely in marshaling).
            // Collapse to IntPtr — same strategy the C header uses.
            if let Some(s) = ir.find_struct(trimmed) {
                if matches!(s.category, super::super::ir::TypeCategory::Recursive) {
                    return "IntPtr".to_string();
                }
                return ffi_type_name(trimmed);
            }
            if let Some(e) = ir.find_enum(trimmed) {
                if matches!(e.category, super::super::ir::TypeCategory::Recursive) {
                    return "IntPtr".to_string();
                }
                return ffi_type_name(trimmed);
            }
            if ir.callback_typedefs.iter().any(|c| c.name == trimmed) {
                ffi_type_name(trimmed)
            } else {
                "IntPtr".to_string()
            }
        }
    }
}

/// Sanitize a name for use as a C# identifier. C# reserved keywords get
/// an `@` prefix (a verbatim identifier).
pub fn sanitize_identifier(name: &str) -> String {
    match name {
        // C# keyword set (subset: those likely to collide with field/arg names)
        "abstract" | "as" | "base" | "bool" | "break" | "byte" | "case" | "catch" | "char"
        | "checked" | "class" | "const" | "continue" | "decimal" | "default" | "delegate"
        | "do" | "double" | "else" | "enum" | "event" | "explicit" | "extern" | "false"
        | "finally" | "fixed" | "float" | "for" | "foreach" | "goto" | "if" | "implicit"
        | "in" | "int" | "interface" | "internal" | "is" | "lock" | "long" | "namespace"
        | "new" | "null" | "object" | "operator" | "out" | "override" | "params" | "private"
        | "protected" | "public" | "readonly" | "ref" | "return" | "sbyte" | "sealed"
        | "short" | "sizeof" | "stackalloc" | "static" | "string" | "struct" | "switch"
        | "this" | "throw" | "true" | "try" | "typeof" | "uint" | "ulong" | "unchecked"
        | "unsafe" | "ushort" | "using" | "virtual" | "void" | "volatile" | "while" => {
            format!("@{}", name)
        }
        _ => name.to_string(),
    }
}

/// Convert a snake_case method name to PascalCase for idiomatic C#.
pub fn snake_to_pascal(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    let mut upper = true;
    for c in s.chars() {
        if c == '_' {
            upper = true;
        } else if upper {
            out.extend(c.to_uppercase());
            upper = false;
        } else {
            out.push(c);
        }
    }
    out
}
