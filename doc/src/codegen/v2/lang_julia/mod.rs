/// Generate the full `azul.jl` source file.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    // Julia idiom is 4-space indentation.
    let mut b = CodeBuilder::new("    ");

    emit_header(&mut b);
    b.line("module Azul");
    b.blank();
    emit_prelude(&mut b);

    types::generate_types(&mut b, ir, config);
    functions::generate_functions(&mut b, ir, config);
    functions::generate_aliases(&mut b, ir, config);

    b.blank();
    b.line("export az_string, setfields");
    b.line("end # module Azul");

    Ok(b.finish())
}

fn emit_header(b: &mut CodeBuilder) {
    b.line("# ============================================================================");
    b.line("# azul.jl — Julia bindings for the Azul GUI framework");
    b.line("# Auto-generated by azul-doc codegen v2 (lang_julia). DO NOT EDIT MANUALLY.");
    b.line("# ============================================================================");
    b.line("#");
    b.line("# The C-ABI surface is translated explicitly: structs -> isbits `struct`,");
    b.line("# enums -> `@enum`, tagged unions -> isbits byte-blob `struct`, and every");
    b.line("# exported symbol -> a thin `ccall` wrapper. Symbol names match the C");
    b.line("# bindings verbatim (AzApp_create, ...).");
    b.line("#");
    b.line("# Callbacks are C-direct: `@cfunction(f, Ret, (Args...,))` mints a real C");
    b.line("# function pointer passed straight to AzButton_setOnClick /");
    b.line("# AzWindowCreateOptions_create — no host-invoker, like the Odin / C bindings.");
    b.line("#");
    b.line("# Load: `include(\"azul/azul.jl\"); using .Azul` with libazul on the loader");
    b.line("# path (or point AZUL_LIB at its absolute path).");
    b.line("# ============================================================================");
    b.blank();
}

/// Module prelude: the shared C-library handle and the runtime helpers the
/// generated types / examples rely on.
fn emit_prelude(b: &mut CodeBuilder) {
    b.line("# The libazul shared library. `ccall` dlopen's it at call time; set the");
    b.line("# AZUL_LIB env var to an absolute path, or keep the library on the loader");
    b.line("# path (LD_LIBRARY_PATH / DYLD_LIBRARY_PATH / PATH).");
    b.line("const LIBAZUL = get(ENV, \"AZUL_LIB\", Sys.iswindows() ? \"azul\" : \"libazul\")");
    b.blank();
    b.line("# --- tagged-union blob helpers (used by @eval'd union structs) --------------");
    b.line("# Element type giving a Julia value the requested C alignment.");
    b.line("_az_blob_eltype(a::Integer) = a >= 8 ? UInt64 : a == 4 ? UInt32 : a == 2 ? UInt16 : UInt8");
    b.line("# Alignment of an isbits type (matches the C union's alignment).");
    b.line("_az_alignof(@nospecialize(T)) = Base.datatype_alignment(T)");
    b.blank();
    b.line("# --- convenience helpers ----------------------------------------------------");
    b.line("\"\"\"");
    b.line("    az_string(s::AbstractString) -> AzString");
    b.line("");
    b.line("Copy a Julia string into a refcounted AzString (AzString_fromUtf8 copies the");
    b.line("bytes, so passing a temporary buffer is safe).");
    b.line("\"\"\"");
    b.line("function az_string(s::AbstractString)");
    b.line("    bytes = Vector{UInt8}(codeunits(String(s)))");
    b.line("    return GC.@preserve bytes AzString_fromUtf8(pointer(bytes), length(bytes))");
    b.line("end");
    b.blank();
    b.line("\"\"\"");
    b.line("    setfields(x::T; field = value, ...) -> T");
    b.line("");
    b.line("Functional update for an immutable isbits struct: returns a copy of `x` with");
    b.line("the named fields replaced. Used instead of field assignment (isbits structs");
    b.line("are immutable) to customize e.g. `window.window_state.title`.");
    b.line("\"\"\"");
    b.line("function setfields(x::T; kwargs...) where {T}");
    b.line("    names = fieldnames(T)");
    b.line("    vals = Any[getfield(x, i) for i in 1:fieldcount(T)]");
    b.line("    for (k, v) in kwargs");
    b.line("        idx = findfirst(==(k), names)");
    b.line("        idx === nothing && throw(ArgumentError(\"$(T) has no field $(k)\"))");
    b.line("        vals[idx] = v");
    b.line("    end");
    b.line("    return T(vals...)");
    b.line("end");
    b.blank();
}

