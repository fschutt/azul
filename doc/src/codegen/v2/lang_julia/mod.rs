/// The `Az`-prefixed FFI type name for an IR type (e.g. `Dom` -> `AzDom`).
pub fn ffi_type_name(name: &str) -> String {
    format!("Az{}", name)
}

/// Is `name` a type declared somewhere in the IR (so it resolves to an
/// `Az`-prefixed Julia declaration)?
pub fn is_ir_type(name: &str, ir: &CodegenIR) -> bool {
    let n = name.trim();
    ir.find_struct(n).is_some()
        || ir.find_enum(n).is_some()
        || ir.find_type_alias(n).is_some()
        || ir.callback_typedefs.iter().any(|c| c.name == n)
}

/// Map a Rust/IR value type to its Julia equivalent (by-value position).
///
/// Primitives map to Julia's native fixed-width types; `usize`/`isize`
/// map to `Csize_t`/`Cssize_t` (== C `size_t`/`ssize_t`).
/// Pointers/references are delegated to [`ptr_to_julia`] (always
/// `Ptr{Cvoid}`). Anything the IR knows becomes `Az<Name>`; anything else
/// degrades to `Ptr{Cvoid}`.
pub fn map_type_to_julia(rust_type: &str, ir: &CodegenIR) -> String {
    let t = rust_type.trim();

    if let Some(rest) = t.strip_prefix("*const ") {
        return ptr_to_julia(rest, ir);
    }
    if let Some(rest) = t.strip_prefix("*mut ") {
        return ptr_to_julia(rest, ir);
    }
    if let Some(rest) = t.strip_prefix("&mut ") {
        return ptr_to_julia(rest, ir);
    }
    if let Some(rest) = t.strip_prefix('&') {
        return ptr_to_julia(rest, ir);
    }

    // Arrays: `[T; N]` -> `NTuple{N, <JuliaT>}` (C array layout, isbits).
    if t.starts_with('[') && t.ends_with(']') {
        let inner = &t[1..t.len() - 1];
        if let Some(semi) = inner.rfind(';') {
            let elem = inner[..semi].trim();
            if let Ok(n) = inner[semi + 1..].trim().parse::<usize>() {
                return format!("NTuple{{{}, {}}}", n, map_type_to_julia(elem, ir));
            }
        }
    }

    match t {
        // Void in value position never really occurs (void returns emit
        // `Cvoid`); degrade to an opaque ptr.
        "void" | "c_void" | "()" => "Ptr{Cvoid}".to_string(),

        "bool" | "GLboolean" => "Bool".to_string(),

        "u8" | "c_uchar" => "UInt8".to_string(),
        "i8" | "c_char" => "Int8".to_string(),
        "u16" | "c_ushort" => "UInt16".to_string(),
        "i16" | "c_short" => "Int16".to_string(),
        "u32" | "c_uint" | "GLuint" | "GLenum" | "GLbitfield" => "UInt32".to_string(),
        "i32" | "c_int" | "GLint" | "GLsizei" => "Int32".to_string(),
        // C `long`/`unsigned long` are 8 bytes on the LP64 targets azul
        // ships; azul's public surface does not use them, so 64-bit is safe.
        "u64" | "c_ulong" | "c_ulonglong" | "GLuint64" => "UInt64".to_string(),
        "i64" | "c_long" | "c_longlong" | "GLint64" => "Int64".to_string(),
        "f32" | "c_float" | "GLfloat" | "GLclampf" => "Float32".to_string(),
        "f64" | "c_double" | "GLdouble" | "GLclampd" => "Float64".to_string(),
        // Pointer-sized integers: Csize_t/Cssize_t == C size_t/ssize_t.
        "usize" | "size_t" | "uintptr_t" => "Csize_t".to_string(),
        "isize" | "ssize_t" | "intptr_t" | "GLsizeiptr" | "GLintptr" => "Cssize_t".to_string(),

        _ => {
            if is_ir_type(t, ir) {
                ffi_type_name(t)
            } else {
                "Ptr{Cvoid}".to_string()
            }
        }
    }
}

/// Map the pointee of a pointer/reference to a Julia pointer expression.
///
/// Every pointer becomes an opaque `Ptr{Cvoid}` — ABI-identical to any
/// typed pointer, and (unlike a typed `Ptr{AzX}`) it imposes no ordering
/// constraint on the referenced type, which is what lets Julia's
/// forward-declaration-free type section resolve `Vec.ptr` / recursive
/// `Box` fields.
pub fn ptr_to_julia(_inner: &str, _ir: &CodegenIR) -> String {
    "Ptr{Cvoid}".to_string()
}

/// Map a `(type_name, FieldRefKind)` pair to the Julia field type.
pub fn field_type_for_ref_kind(
    type_name: &str,
    ref_kind: &FieldRefKind,
    ir: &CodegenIR,
) -> String {
    match ref_kind {
        FieldRefKind::Owned => map_type_to_julia(type_name, ir),
        FieldRefKind::Ref
        | FieldRefKind::RefMut
        | FieldRefKind::Ptr
        | FieldRefKind::PtrMut
        | FieldRefKind::Boxed
        | FieldRefKind::OptionBoxed => ptr_to_julia(type_name, ir),
    }
}

/// Map a `(type_name, ArgRefKind)` pair to the Julia argument type.
pub fn arg_type_for_ref_kind(type_name: &str, ref_kind: &ArgRefKind, ir: &CodegenIR) -> String {
    match ref_kind {
        ArgRefKind::Owned => map_type_to_julia(type_name, ir),
        ArgRefKind::Ref | ArgRefKind::RefMut | ArgRefKind::Ptr | ArgRefKind::PtrMut => {
            ptr_to_julia(type_name, ir)
        }
    }
}

/// Backing integer type for a Julia `@enum`. Rust `#[repr(C, u8)]` -> `UInt8`,
/// otherwise a `#[repr(C)]` enum is C-`int` sized -> `Int32`.
pub fn enum_backing(repr: Option<&str>) -> &'static str {
    match repr {
        Some(r) if r.contains("u8") => "UInt8",
        Some(r) if r.contains("u16") => "UInt16",
        Some(r) if r.contains("u32") => "UInt32",
        Some(r) if r.contains("i64") || r.contains("u64") => "Int64",
        _ => "Int32",
    }
}

/// Discriminant field width for a tagged-union variant struct.
pub fn tag_type(repr: Option<&str>) -> &'static str {
    match repr {
        Some(r) if r.contains("u8") => "UInt8",
        Some(r) if r.contains("u16") => "UInt16",
        Some(r) if r.contains("u32") => "UInt32",
        _ => "Int32",
    }
}

/// Sanitize a name for use as a Julia identifier. Julia reserved words get
/// a trailing underscore (Julia is case-sensitive, so only exact keyword
/// matches collide).
pub fn sanitize_identifier(name: &str) -> String {
    if is_julia_keyword(name) {
        format!("{}_", name)
    } else {
        name.to_string()
    }
}

fn is_julia_keyword(s: &str) -> bool {
    matches!(
        s,
        "baremodule"
            | "begin"
            | "break"
            | "catch"
            | "const"
            | "continue"
            | "do"
            | "else"
            | "elseif"
            | "end"
            | "export"
            | "false"
            | "finally"
            | "for"
            | "function"
            | "global"
            | "if"
            | "import"
            | "in"
            | "isa"
            | "let"
            | "local"
            | "macro"
            | "module"
            | "quote"
            | "return"
            | "struct"
            | "true"
            | "try"
            | "using"
            | "where"
            | "while"
            // context-sensitive words that still break as bare identifiers
            | "abstract"
            | "mutable"
            | "primitive"
            | "type"
            | "outer"
    )
}

/// Strip characters that would break a Julia `#` line comment.
pub fn sanitize_comment(s: &str) -> String {
    s.replace('\n', " ").replace('\r', " ")
}

