//! Odin binding generator.
//!
//! Emits a single `azul.odin` source file (`package azul`) that exposes
//! the C-ABI of `libazul` to Odin programs. Unlike the Zig binding —
//! which parses `azul.h` directly via `@cImport` — Odin has no C-header
//! importer, so we translate the FFI surface explicitly (like the
//! Pascal / Fortran backends): every struct becomes an Odin `struct`,
//! every enum an Odin `enum`, every tagged union a `struct #raw_union`
//! of per-variant structs (mirroring the tested `lang_c` layout), and
//! every C-ABI function a declaration inside a
//! `@(default_calling_convention="c") foreign azul { ... }` block.
//!
//! # Callbacks are C-direct (the key simplification)
//!
//! Odin — like Zig, Go and C — can produce a real C function pointer
//! with `proc "c" (...) -> ...`. So the counter's `on_click` / `layout`
//! callbacks are plain Odin `proc "c"` values passed straight to
//! `AzButton_setOnClick` / `AzWindowCreateOptions_create`. There is NO
//! host-invoker table, NO handle registration — exactly the model used
//! by `examples/zig/hello-world.zig`. For each callback typedef we emit
//! an Odin proc-type alias (`AzButtonOnClickCallbackType :: #type proc
//! "c" (AzRefAny, AzCallbackInfo) -> AzUpdate`), and functions that take
//! a callback-wrapper arg bind the RAW C symbol taking that bare
//! fn-pointer typedef (matching the raw `_setOnClick` variant the DLL
//! exports — the same one Zig sees through `azul.h`).
//!
//! # Order-independence
//!
//! Odin resolves package-scope declarations regardless of source order,
//! so — unlike C / Pascal — no forward declarations or topological sort
//! are needed. Types skipped for codegen (recursive / generic template)
//! are emitted as opaque pointer aliases (`AzXmlNode :: rawptr`) so any
//! by-pointer reference still resolves, mirroring the Pascal backend.
//!
//! # Build / link requirements
//!
//! `foreign import azul "system:azul"` emits `-lazul` to the linker; the
//! user drops `libazul.{so,dylib}` / `azul.dll` on the library path
//! (`-extra-linker-flags:"-L."`). The generated `azul.odin` is meant to
//! live in an `azul/` subdirectory imported via `import azul "azul"`.

pub mod functions;
pub mod types;

use std::collections::BTreeSet;

use anyhow::Result;

use super::config::CodegenConfig;
use super::generator::CodeBuilder;
use super::ir::{
    ArgRefKind, CodegenIR, EnumDef, FieldRefKind, FunctionDef, StructDef, TypeCategory,
};

/// The C library name used in `foreign import azul "system:azul"`.
/// Resolves to `libazul.so` / `libazul.dylib` / `azul.dll` per platform.
pub const LIB_NAME: &str = "azul";

/// Generate the full `azul.odin` source file.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    // Odin idiom is tab indentation.
    let mut b = CodeBuilder::new("\t");

    emit_header(&mut b);
    b.line("package azul");
    b.blank();
    b.line(&format!("foreign import azul \"system:{}\"", LIB_NAME));
    b.blank();

    // Track every emitted top-level name so a duplicate (a struct + a
    // type-alias sharing a name, a case-collision, …) can never produce
    // an Odin "redeclaration" error.
    let mut emitted: BTreeSet<String> = BTreeSet::new();

    types::generate_types(&mut b, ir, config, &mut emitted);
    functions::generate_foreign_block(&mut b, ir, config);
    functions::generate_aliases(&mut b, ir, config);

    Ok(b.finish())
}

fn emit_header(b: &mut CodeBuilder) {
    b.line("// ============================================================================");
    b.line("// azul.odin — Odin bindings for the Azul GUI framework");
    b.line("// Auto-generated by azul-doc codegen v2 (lang_odin). DO NOT EDIT MANUALLY.");
    b.line("// ============================================================================");
    b.line("//");
    b.line("// The C-ABI surface is translated explicitly: structs -> struct, enums ->");
    b.line("// enum, tagged unions -> struct #raw_union, and every exported symbol is");
    b.line("// declared inside the `@(default_calling_convention=\"c\") foreign azul` block");
    b.line("// below. Symbol names match the C bindings verbatim (AzApp_create, ...).");
    b.line("//");
    b.line("// Callbacks are C-direct: pass a plain `proc \"c\" (...) -> ...` value");
    b.line("// straight to AzButton_setOnClick / AzWindowCreateOptions_create — no");
    b.line("// host-invoker table, exactly like the Zig / C bindings.");
    b.line("//");
    b.line("// Idiomatic aliases without the `Az` prefix (App_create :: AzApp_create) are");
    b.line("// emitted at the bottom; the raw Az* symbols remain available.");
    b.line("//");
    b.line("// Build: place libazul.{so,dylib}/azul.dll on the link path and build with");
    b.line("//   odin build . -extra-linker-flags:\"-L.\"");
    b.line("// ============================================================================");
    b.blank();
}

// ============================================================================
// Shared name / type helpers (used by submodules)
// ============================================================================

/// The `Az`-prefixed FFI type name for an IR type (e.g. `Dom` -> `AzDom`).
pub fn ffi_type_name(name: &str) -> String {
    format!("Az{}", name)
}

/// Is `name` a type declared somewhere in the IR (so it resolves to an
/// `Az`-prefixed Odin declaration)?
pub fn is_ir_type(name: &str, ir: &CodegenIR) -> bool {
    let n = name.trim();
    ir.find_struct(n).is_some()
        || ir.find_enum(n).is_some()
        || ir.find_type_alias(n).is_some()
        || ir.callback_typedefs.iter().any(|c| c.name == n)
}

/// Map a Rust/IR value type to its Odin equivalent (by-value position).
///
/// Primitives map to Odin's native fixed-width types; `usize`/`isize`
/// map to Odin's pointer-sized `uint`/`int` (== C `size_t`/`ssize_t`).
/// Pointers/references are delegated to [`ptr_to_odin`]. Anything the IR
/// knows becomes `Az<Name>`; anything else degrades to `rawptr`.
pub fn map_type_to_odin(rust_type: &str, ir: &CodegenIR) -> String {
    let t = rust_type.trim();

    if let Some(rest) = t.strip_prefix("*const ") {
        return ptr_to_odin(rest, ir);
    }
    if let Some(rest) = t.strip_prefix("*mut ") {
        return ptr_to_odin(rest, ir);
    }
    if let Some(rest) = t.strip_prefix("&mut ") {
        return ptr_to_odin(rest, ir);
    }
    if let Some(rest) = t.strip_prefix('&') {
        return ptr_to_odin(rest, ir);
    }

    // Arrays: `[T; N]` -> `[N]<OdinT>`
    if t.starts_with('[') && t.ends_with(']') {
        let inner = &t[1..t.len() - 1];
        if let Some(semi) = inner.rfind(';') {
            let elem = inner[..semi].trim();
            if let Ok(n) = inner[semi + 1..].trim().parse::<usize>() {
                return format!("[{}]{}", n, map_type_to_odin(elem, ir));
            }
        }
    }

    match t {
        // Void in value position never really occurs (void returns are
        // handled by omitting the `-> ...`); degrade to an opaque ptr.
        "void" | "c_void" | "()" => "rawptr".to_string(),

        "bool" | "GLboolean" => "bool".to_string(),

        "u8" | "c_uchar" => "u8".to_string(),
        "i8" | "c_char" => "i8".to_string(),
        "u16" | "c_ushort" => "u16".to_string(),
        "i16" | "c_short" => "i16".to_string(),
        "u32" | "c_uint" | "GLuint" | "GLenum" | "GLbitfield" => "u32".to_string(),
        "i32" | "c_int" | "GLint" | "GLsizei" => "i32".to_string(),
        // C `long`/`unsigned long` are 8 bytes on the LP64 targets azul
        // ships (Linux/macOS) and 4 on Windows LLP64; azul's public
        // surface does not use them, so mapping to 64-bit is safe here.
        "u64" | "c_ulong" | "c_ulonglong" | "GLuint64" => "u64".to_string(),
        "i64" | "c_long" | "c_longlong" | "GLint64" => "i64".to_string(),
        "f32" | "c_float" | "GLfloat" | "GLclampf" => "f32".to_string(),
        "f64" | "c_double" | "GLdouble" | "GLclampd" => "f64".to_string(),
        // Pointer-sized integers: Odin `uint`/`int` == C size_t/ssize_t.
        "usize" | "size_t" | "uintptr_t" => "uint".to_string(),
        "isize" | "ssize_t" | "intptr_t" | "GLsizeiptr" | "GLintptr" => "int".to_string(),

        _ => {
            if is_ir_type(t, ir) {
                ffi_type_name(t)
            } else {
                "rawptr".to_string()
            }
        }
    }
}

/// Map the pointee of a pointer/reference to an Odin pointer expression.
///
/// - `void`/`c_void` -> `rawptr`
/// - a known IR type -> `^Az<Name>` (single pointer)
/// - a primitive -> `[^]<OdinT>` (multi-pointer; matches `raw_data(...)`)
/// - anything else -> `rawptr`
pub fn ptr_to_odin(inner: &str, ir: &CodegenIR) -> String {
    let inner = inner.trim();
    match inner {
        "c_void" | "void" | "()" => "rawptr".to_string(),
        _ => {
            if is_ir_type(inner, ir) {
                format!("^{}", ffi_type_name(inner))
            } else {
                let mapped = map_type_to_odin(inner, ir);
                if mapped == "rawptr" {
                    "rawptr".to_string()
                } else {
                    format!("[^]{}", mapped)
                }
            }
        }
    }
}

/// Map a `(type_name, FieldRefKind)` pair to the Odin field/param type.
pub fn field_type_for_ref_kind(
    type_name: &str,
    ref_kind: &FieldRefKind,
    ir: &CodegenIR,
) -> String {
    match ref_kind {
        FieldRefKind::Owned => map_type_to_odin(type_name, ir),
        FieldRefKind::Ref
        | FieldRefKind::RefMut
        | FieldRefKind::Ptr
        | FieldRefKind::PtrMut
        | FieldRefKind::Boxed
        | FieldRefKind::OptionBoxed => ptr_to_odin(type_name, ir),
    }
}

/// Map a `(type_name, ArgRefKind)` pair to the Odin argument type.
pub fn arg_type_for_ref_kind(type_name: &str, ref_kind: &ArgRefKind, ir: &CodegenIR) -> String {
    match ref_kind {
        ArgRefKind::Owned => map_type_to_odin(type_name, ir),
        ArgRefKind::Ref | ArgRefKind::RefMut | ArgRefKind::Ptr | ArgRefKind::PtrMut => {
            ptr_to_odin(type_name, ir)
        }
    }
}

/// Sanitize a name for use as an Odin identifier. Odin reserved words
/// get a trailing underscore (Odin is case-sensitive, so only exact
/// keyword matches collide).
pub fn sanitize_identifier(name: &str) -> String {
    if is_odin_keyword(name) {
        format!("{}_", name)
    } else {
        name.to_string()
    }
}

fn is_odin_keyword(s: &str) -> bool {
    matches!(
        s,
        "asm"
            | "auto_cast"
            | "bit_set"
            | "break"
            | "case"
            | "cast"
            | "context"
            | "continue"
            | "defer"
            | "distinct"
            | "do"
            | "dynamic"
            | "else"
            | "enum"
            | "fallthrough"
            | "for"
            | "foreign"
            | "if"
            | "import"
            | "in"
            | "map"
            | "matrix"
            | "not_in"
            | "or_else"
            | "or_return"
            | "package"
            | "proc"
            | "return"
            | "struct"
            | "switch"
            | "transmute"
            | "typeid"
            | "union"
            | "using"
            | "when"
            | "where"
    )
}

// ============================================================================
// Shared inclusion filters (mirror the Pascal backend's proven rules)
// ============================================================================

/// A struct gets a real Odin `struct`; skipped ones become opaque
/// pointer aliases. Only Recursive / GenericTemplate are skipped —
/// VecRef / DestructorOrClone are kept because Vec wrappers embed them
/// as fields and eliding them would corrupt surrounding field offsets.
pub fn include_struct(s: &StructDef, config: &CodegenConfig) -> bool {
    if !config.should_include_type(&s.name) {
        return false;
    }
    if !s.generic_params.is_empty() {
        return false;
    }
    !matches!(
        s.category,
        TypeCategory::Recursive | TypeCategory::GenericTemplate
    )
}

pub fn include_enum(e: &EnumDef, config: &CodegenConfig) -> bool {
    if !config.should_include_type(&e.name) {
        return false;
    }
    if !e.generic_params.is_empty() {
        return false;
    }
    !matches!(
        e.category,
        TypeCategory::Recursive | TypeCategory::GenericTemplate
    )
}

/// Whether a function should get a `foreign` declaration. Mirrors the
/// Pascal / Fortran filter: skip functions whose owning class is a
/// recursive / VecRef / destructor / generic-template type.
pub fn should_emit_function(func: &FunctionDef, ir: &CodegenIR, config: &CodegenConfig) -> bool {
    if !config.should_include_type(&func.class_name) {
        return false;
    }
    if let Some(s) = ir.find_struct(&func.class_name) {
        if matches!(
            s.category,
            TypeCategory::Recursive
                | TypeCategory::VecRef
                | TypeCategory::DestructorOrClone
                | TypeCategory::GenericTemplate
        ) {
            return false;
        }
        if !s.generic_params.is_empty() {
            return false;
        }
    }
    if let Some(e) = ir.find_enum(&func.class_name) {
        if matches!(
            e.category,
            TypeCategory::Recursive
                | TypeCategory::DestructorOrClone
                | TypeCategory::GenericTemplate
        ) {
            return false;
        }
        if !e.generic_params.is_empty() {
            return false;
        }
    }
    true
}
