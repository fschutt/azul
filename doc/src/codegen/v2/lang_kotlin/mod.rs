fn emit_unit_enum(builder: &mut CodeBuilder, enum_def: &EnumDef) {
    let name = ffi_type_name(&enum_def.name);

    if !enum_def.doc.is_empty() {
        // (KDoc switched to triple-slash to bypass parser issues with `*/` in inline code samples)
        for d in &enum_def.doc {
            builder.line(&format!("/// {}", wrappers::kdoc_escape(d)));
        }
        
    }

    builder.line(&format!("enum class {}(val value: Int) {{", name));
    builder.indent();
    let last = enum_def.variants.len().saturating_sub(1);
    for (idx, variant) in enum_def.variants.iter().enumerate() {
        let v = sanitize_kt_identifier(&variant.name);
        let sep = if idx == last { ";" } else { "," };
        builder.line(&format!("{}({}){}", v, idx, sep));
    }
    builder.line("companion object {");
    builder.indent();
    builder.line(&format!(
        "@JvmStatic fun fromInt(v: Int): {} = values().first {{ it.value == v }}",
        name
    ));
    builder.dedent();
    builder.line("}");
    builder.dedent();
    builder.line("}");
    builder.blank();
}

// ============================================================================
// Tagged union
// ============================================================================

fn emit_tagged_union(builder: &mut CodeBuilder, enum_def: &EnumDef, ir: &CodegenIR) {
    let name = ffi_type_name(&enum_def.name);

    // 1. Tag enum
    builder.line(&format!("enum class {}_Tag(val value: Int) {{", name));
    builder.indent();
    let last = enum_def.variants.len().saturating_sub(1);
    for (idx, v) in enum_def.variants.iter().enumerate() {
        let sep = if idx == last { "" } else { "," };
        builder.line(&format!(
            "{}({}){}",
            sanitize_kt_identifier(&v.name),
            idx,
            sep
        ));
    }
    builder.dedent();
    builder.line("}");
    builder.blank();

    // 2. Per-variant payload structures
    for v in &enum_def.variants {
        let variant_struct = format!("{}Variant_{}", name, v.name);
        builder.line(&format!("open class {} : Structure() {{", variant_struct));
        builder.indent();
        // C-ABI `#[repr(C, u8)]` tag is 1 byte. JNA's `Int` would
        // be 4 bytes and shift every payload offset on small-aligned
        // variants — same family of bug Java/C#/OCaml fixed last week.
        builder.line(&format!("@JvmField var tag: Byte = 0 // {}_Tag", name));

        let mut field_names: Vec<String> = vec!["\"tag\"".to_string()];
        match &v.kind {
            EnumVariantKind::Unit => {}
            EnumVariantKind::Tuple(types) => {
                if types.len() == 1 {
                    let (ty, ref_kind) = &types[0];
                    let (kt, default) = ref_kind_kt_field(ty, ref_kind, ir);
                    builder.line(&format!("@JvmField var payload: {} = {}", kt, default));
                    field_names.push("\"payload\"".to_string());
                } else {
                    for (i, (ty, ref_kind)) in types.iter().enumerate() {
                        let (kt, default) = ref_kind_kt_field(ty, ref_kind, ir);
                        builder.line(&format!(
                            "@JvmField var payload_{}: {} = {}",
                            i, kt, default
                        ));
                        field_names.push(format!("\"payload_{}\"", i));
                    }
                }
            }
            EnumVariantKind::Struct(fields) => {
                for f in fields {
                    let (kt, default) = ref_kind_kt_field(&f.type_name, &f.ref_kind, ir);
                    let fname = sanitize_kt_identifier(&f.name);
                    builder.line(&format!("@JvmField var {}: {} = {}", fname, kt, default));
                    field_names.push(format!("\"{}\"", f.name));
                }
            }
        }

        emit_field_order(builder, &field_names);
        emit_byvalue_byref(builder, &variant_struct);
        builder.dedent();
        builder.line("}");
        builder.blank();
    }

    // 3. Outer Union
    if !enum_def.doc.is_empty() {
        // (KDoc switched to triple-slash to bypass parser issues with `*/` in inline code samples)
        for d in &enum_def.doc {
            builder.line(&format!("/// {}", wrappers::kdoc_escape(d)));
        }
        
    }
    builder.line(&format!("open class {} : Union() {{", name));
    builder.indent();
    let mut field_names: Vec<String> = Vec::new();
    for v in &enum_def.variants {
        let variant_struct = format!("{}Variant_{}", name, v.name);
        let field = sanitize_kt_identifier(&v.name);
        builder.line(&format!(
            "@JvmField var {}: {} = {}()",
            field, variant_struct, variant_struct
        ));
        field_names.push(format!("\"{}\"", v.name));
    }
    emit_field_order(builder, &field_names);

    // AzOption<T>.toNullable() — Kotlin nullable mirror of the Java
    // accessor (lang_java/types.rs). Detected shape-only (variants
    // exactly [None, Some]) — the name doesn't have to start with
    // "Option" (e.g. `MaybeFoo` would still get the accessor if its
    // variants match).
    if enum_def.variants.len() == 2 {
        let none = enum_def.variants.iter().find(|v| v.name == "None");
        let some = enum_def.variants.iter().find(|v| v.name == "Some");
        if let (Some(_), Some(sv)) = (none, some) {
            let payload_tuple = match &sv.kind {
                EnumVariantKind::Tuple(types) if types.len() == 1 => {
                    Some((types[0].0.clone(), types[0].1.clone()))
                }
                _ => None,
            };
            if let Some((payload_ty, ref_kind)) = payload_tuple {
                let (kt, _default) = ref_kind_kt_field(&payload_ty, &ref_kind, ir);
                builder.line("/**");
                builder.line(" * Decode this Option as a Kotlin nullable.");
                builder.line(" * Returns null when the C-ABI tag is None, the Some");
                builder.line(" * payload otherwise.");
                builder.line(" */");
                builder.line(&format!("fun toNullable(): {}? {{", kt));
                builder.indent();
                builder.line("val p = pointer ?: return null");
                builder.line("if (p.getByte(0).toInt() == 0) return null");
                builder.line(&format!(
                    "val __s = Structure.newInstance({}Variant_Some::class.java, p)",
                    name
                ));
                builder.line("__s.read()");
                builder.line("return __s.payload");
                builder.dedent();
                builder.line("}");
            }
        }
    }

    // AzResult<T, E>.unwrap() — return Ok payload or throw on Err.
    // Detected shape-only (variants exactly [Ok, Err]) — types like
    // `IcuResult` that don't start with the literal "Result" prefix
    // still get the accessor.
    if enum_def.variants.len() == 2 {
        let ok = enum_def.variants.iter().find(|v| v.name == "Ok");
        let err = enum_def.variants.iter().find(|v| v.name == "Err");
        if let (Some(ov), Some(_)) = (ok, err) {
            let payload_tuple = match &ov.kind {
                EnumVariantKind::Tuple(types) if types.len() == 1 => {
                    Some((types[0].0.clone(), types[0].1.clone()))
                }
                _ => None,
            };
            if let Some((payload_ty, ref_kind)) = payload_tuple {
                let (kt, _default) = ref_kind_kt_field(&payload_ty, &ref_kind, ir);
                builder.line("/**");
                builder.line(" * Return the Ok payload, or throw a RuntimeException carrying");
                builder.line(" * the Err payload's toString() when the tag is Err.");
                builder.line(" */");
                builder.line(&format!("fun unwrap(): {} {{", kt));
                builder.indent();
                builder.line("val p = pointer ?: throw RuntimeException(\"unwrap on null pointer\")");
                builder.line("if (p.getByte(0).toInt() == 0) {");
                builder.indent();
                builder.line(&format!(
                    "val __ok = Structure.newInstance({}Variant_Ok::class.java, p)",
                    name
                ));
                builder.line("__ok.read()");
                builder.line("return __ok.payload");
                builder.dedent();
                builder.line("}");
                builder.line(&format!(
                    "val __err = Structure.newInstance({}Variant_Err::class.java, p)",
                    name
                ));
                builder.line("__err.read()");
                builder.line(&format!(
                    "throw RuntimeException(\"{} unwrap on Err: ${{__err.payload}}\")",
                    name
                ));
                builder.dedent();
                builder.line("}");

                builder.line("/** True when the tag is Ok. */");
                builder.line("fun isOk(): Boolean = (pointer?.getByte(0)?.toInt() ?: -1) == 0");
                builder.line("/** True when the tag is Err. */");
                builder.line("fun isErr(): Boolean = !isOk()");
            }
        }
    }

    builder.line(&format!(
        "class ByValue : {}(), Structure.ByValue",
        name
    ));
    builder.line(&format!(
        "class ByReference : {}(), Structure.ByReference",
        name
    ));
    builder.dedent();
    builder.line("}");
    builder.blank();
}

// ============================================================================
// Monomorphized type aliases (concrete instantiations of generic types)
// ============================================================================

fn emit_monomorphized_alias(
    builder: &mut CodeBuilder,
    ta: &TypeAliasDef,
    mono_def: &MonomorphizedTypeDef,
    ir: &CodegenIR,
) {
    let name = ffi_type_name(&ta.name);
    for d in &ta.doc {
        builder.line(&format!("/** {} */", d));
    }
    match &mono_def.kind {
        MonomorphizedKind::SimpleEnum { variants, .. } => {
            builder.line(&format!("enum class {}(val value: Int) {{", name));
            builder.indent();
            let last = variants.len().saturating_sub(1);
            for (idx, v) in variants.iter().enumerate() {
                let v_name = sanitize_kt_identifier(v);
                let sep = if idx == last { ";" } else { "," };
                builder.line(&format!("{}({}){}", v_name, idx, sep));
            }
            builder.line(&format!("companion object {{ fun fromInt(v: Int): {} = values().first {{ it.value == v }} }}", name));
            builder.dedent();
            builder.line("}");
            builder.blank();
        }
        MonomorphizedKind::Struct { fields } => {
            builder.line(&format!("open class {} : Structure() {{", name));
            builder.indent();
            let mut field_names: Vec<String> = Vec::new();
            if fields.is_empty() {
                builder.line("@JvmField var _dummy: Byte = 0");
                field_names.push("\"_dummy\"".to_string());
            } else {
                for f in fields {
                    emit_struct_field(builder, f, ir, &mut field_names);
                }
            }
            emit_kotlin_field_order_override(builder, &field_names);
            builder.line(&format!(
                "class ByValue : {}(), Structure.ByValue",
                name
            ));
            builder.line(&format!(
                "class ByReference : {}(), Structure.ByReference",
                name
            ));
            builder.dedent();
            builder.line("}");
            builder.blank();
        }
        MonomorphizedKind::TaggedUnion { variants, .. } => {
            // Tag enum
            builder.line(&format!("enum class {}_Tag(val value: Int) {{", name));
            builder.indent();
            let last = variants.len().saturating_sub(1);
            for (idx, v) in variants.iter().enumerate() {
                let v_name = sanitize_kt_identifier(&v.name);
                let sep = if idx == last { ";" } else { "," };
                builder.line(&format!("{}({}){}", v_name, idx, sep));
            }
            builder.dedent();
            builder.line("}");

            // Per-variant payload structs
            for v in variants {
                let variant_struct = format!("{}Variant_{}", name, v.name);
                builder.line(&format!(
                    "open class {} : Structure() {{",
                    variant_struct
                ));
                builder.indent();
                builder.line("@JvmField var tag: Byte = 0 // repr(C, u8)");
                let mut field_names = vec!["\"tag\"".to_string()];
                if let Some(ref payload_type) = v.payload_type {
                    let jt = ref_kind_field_type_kt(payload_type, &v.payload_ref_kind, ir);
                    // Enums can't be `Foo()`-instantiated; declare as
                    // late-init via lateinit isn't possible on primitives
                    // either. JNA reads the bytes when Union.read() runs,
                    // so just declare with a safe default for the type.
                    let init = default_for_kt_type_or_struct(&jt, ir);
                    builder.line(&format!("@JvmField var payload: {} = {}", jt, init));
                    field_names.push("\"payload\"".to_string());
                }
                emit_kotlin_field_order_override(builder, &field_names);
                builder.line(&format!(
                    "class ByValue : {}(), Structure.ByValue",
                    variant_struct
                ));
                builder.line(&format!(
                    "class ByReference : {}(), Structure.ByReference",
                    variant_struct
                ));
                builder.dedent();
                builder.line("}");
            }

            // Outer Union
            builder.line(&format!("open class {} : Union() {{", name));
            builder.indent();
            let mut field_names = Vec::new();
            for v in variants {
                let variant_struct = format!("{}Variant_{}", name, v.name);
                let f = sanitize_kt_identifier(&v.name);
                builder.line(&format!("@JvmField var {}: {} = {}()", f, variant_struct, variant_struct));
                field_names.push(format!("\"{}\"", v.name));
            }
            emit_kotlin_field_order_override(builder, &field_names);
            builder.line(&format!(
                "class ByValue : {}(), Structure.ByValue",
                name
            ));
            builder.line(&format!(
                "class ByReference : {}(), Structure.ByReference",
                name
            ));
            builder.dedent();
            builder.line("}");
            builder.blank();
        }
    }
}

fn default_for_kt_type(jt: &str) -> &'static str {
    match jt {
        "Boolean" => "false",
        "Byte" => "0",
        "Short" => "0",
        "Int" => "0",
        "Long" => "0L",
        "Float" => "0f",
        "Double" => "0.0",
        "Pointer?" => "null",
        _ => "/* default */ throw IllegalStateException()",
    }
}

/// Like `default_for_kt_type` but recognises Az-prefixed types and
/// produces the right initializer per kind:
/// - Plain Structure-flavoured (`AzFoo.ByValue` / `AzFoo`): `AzFoo()`
/// - Unit enum (`AzFoo`): `AzFoo.values().first()`
fn default_for_kt_type_or_struct(jt: &str, ir: &CodegenIR) -> String {
    let prim = default_for_kt_type(jt);
    if !prim.starts_with("/* default */") {
        return prim.to_string();
    }
    // Strip `.ByValue` suffix and `Az` prefix to look up in IR.
    let base = jt.trim_end_matches(".ByValue");
    let unprefixed = base.strip_prefix("Az").unwrap_or(base);
    if let Some(e) = ir.find_enum(unprefixed) {
        if !e.is_union {
            // Unit enum — use the first variant as default.
            if let Some(first) = e.variants.first() {
                return format!("{}.{}", base, sanitize_kt_identifier(&first.name));
            }
        }
    }
    // Structure / Union — default-construct.
    format!("{}()", jt)
}

fn emit_kotlin_field_order_override(builder: &mut CodeBuilder, field_names: &[String]) {
    builder.line("override fun getFieldOrder(): kotlin.collections.List<kotlin.String> =");
    builder.indent();
    builder.line(&format!("listOf({})", field_names.join(", ")));
    builder.dedent();
}

fn ref_kind_field_type_kt(type_name: &str, ref_kind: &FieldRefKind, ir: &CodegenIR) -> String {
    match ref_kind {
        FieldRefKind::Owned => map_kt_owned(type_name, ir),
        FieldRefKind::Ref
        | FieldRefKind::RefMut
        | FieldRefKind::Ptr
        | FieldRefKind::PtrMut
        | FieldRefKind::Boxed
        | FieldRefKind::OptionBoxed => "Pointer?".to_string(),
    }
}

