/// Library name JNA loads. Matches the Java side.
pub const LIBRARY_NAME: &str = "azul";

/// Public entry point. Returns the full `Azul.kt` source as a single
/// `String`. The orchestrator writes it to disk verbatim.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    let mut builder = CodeBuilder::new(&config.indent);

    emit_header(&mut builder);
    emit_imports(&mut builder);

    // 1. FFI interface
    emit_native_interface(&mut builder, ir, config);

    // 2. Type declarations (enums + structs + callbacks)
    for enum_def in &ir.enums {
        if !should_include_enum(enum_def, config) {
            continue;
        }
        if enum_def.is_union {
            emit_tagged_union(&mut builder, enum_def, ir);
        } else {
            emit_unit_enum(&mut builder, enum_def);
        }
    }
    for struct_def in &ir.structs {
        if !should_include_struct(struct_def, config) {
            continue;
        }
        emit_struct(&mut builder, struct_def, ir);
    }
    for cb in &ir.callback_typedefs {
        emit_callback_interface(&mut builder, cb, ir);
    }

    // 2b. Monomorphized type aliases (e.g. `PhysicalSize<u32>` →
    // `AzPhysicalSizeU32`) emitted as concrete Kotlin classes/enums,
    // parallel to C / Java / C# / Node. Without this the wrappers
    // and AzulNative interface reference types that aren't declared.
    for ta in &ir.type_aliases {
        let Some(ref mono_def) = ta.monomorphized_def else {
            continue;
        };
        if !config.should_include_type(&ta.name) {
            continue;
        }
        emit_monomorphized_alias(&mut builder, ta, mono_def, ir);
    }

    // 3. Idiomatic wrappers
    wrappers::emit_all(&mut builder, ir, config)?;

    // 4. Managed-FFI runtime helpers (host-invoker pattern).
    managed::emit(&mut builder, ir);

    Ok(builder.finish())
}

// ============================================================================
// Header / imports
// ============================================================================

fn emit_header(builder: &mut CodeBuilder) {
    builder.line("// ============================================================================");
    builder.line("// Auto-generated Kotlin bindings for the Azul GUI framework.");
    builder.line("// Generated by azul-doc codegen v2 (lang_kotlin).");
    builder.line("// DO NOT EDIT MANUALLY.");
    builder.line("// ============================================================================");
    builder.blank();
    builder.line("@file:Suppress(\"FunctionName\", \"PropertyName\", \"unused\", \"ClassName\")");
    builder.blank();
    builder.line("package com.azul");
    builder.blank();
}

fn emit_imports(builder: &mut CodeBuilder) {
    // Alias JNA's `Callback` interface to `JnaCallback` so it does not
    // collide with the Azul wrapper class `Callback` (which lives in
    // the same `com.azul` package). A naked `import com.sun.jna.Callback`
    // shadows the wrapper class at top-level scope, causing function
    // signatures like `fun create(...): Callback` to bind to the JNA
    // interface and the body's `return Callback(ptr)` (resolved in the
    // class scope) to fail with a return-type mismatch.
    builder.line("import com.sun.jna.Callback as JnaCallback");
    builder.line("import com.sun.jna.Library");
    builder.line("import com.sun.jna.Memory");
    builder.line("import com.sun.jna.Native");
    builder.line("import com.sun.jna.Pointer");
    builder.line("import com.sun.jna.Structure");
    builder.line("import com.sun.jna.Union");
    builder.blank();
}

// ============================================================================
// Type filters
// ============================================================================

fn should_include_struct(s: &StructDef, config: &CodegenConfig) -> bool {
    if !config.should_include_type(&s.name) {
        return false;
    }
    if !s.generic_params.is_empty() {
        return false;
    }
    // VecRef + DestructorOrClone are referenced as field types from
    // Vec wrappers; unfilter to match the C / Java / C# generators.
    !matches!(
        s.category,
        TypeCategory::Recursive | TypeCategory::GenericTemplate
    )
}

fn should_include_enum(e: &EnumDef, config: &CodegenConfig) -> bool {
    if !config.should_include_type(&e.name) {
        return false;
    }
    if !e.generic_params.is_empty() {
        return false;
    }
    !matches!(
        e.category,
        TypeCategory::Recursive | TypeCategory::GenericTemplate
    )
}

fn should_emit_function(func: &FunctionDef, ir: &CodegenIR, config: &CodegenConfig) -> bool {
    if !config.should_include_type(&func.class_name) {
        return false;
    }
    if let Some(s) = ir.find_struct(&func.class_name) {
        if matches!(
            s.category,
            TypeCategory::Recursive
                | TypeCategory::VecRef
                | TypeCategory::DestructorOrClone
                | TypeCategory::GenericTemplate
        ) || !s.generic_params.is_empty()
        {
            return false;
        }
    }
    if let Some(e) = ir.find_enum(&func.class_name) {
        if matches!(
            e.category,
            TypeCategory::Recursive
                | TypeCategory::DestructorOrClone
                | TypeCategory::GenericTemplate
        ) || !e.generic_params.is_empty()
        {
            return false;
        }
    }
    true
}

