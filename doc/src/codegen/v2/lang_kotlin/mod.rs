fn emit_callback_interface(
    builder: &mut CodeBuilder,
    cb: &CallbackTypedefDef,
    ir: &CodegenIR,
) {
    let name = ffi_type_name(&cb.name);

    if !cb.doc.is_empty() {
        // (KDoc switched to triple-slash to bypass parser issues with `*/` in inline code samples)
        for d in &cb.doc {
            builder.line(&format!("/// {}", wrappers::kdoc_escape(d)));
        }
        
    }

    // Kotlin SAM interface — ergonomic lambda construction site.
    // Use `JnaCallback` (aliased above) rather than naked `Callback`
    // to avoid collision with the Azul `Callback` wrapper class.
    builder.line(&format!("fun interface {} : JnaCallback {{", name));
    builder.indent();

    let return_type = cb
        .return_type
        .as_ref()
        .map(|r| map_kt_return(r, ir))
        .unwrap_or_else(|| "Unit".to_string());

    let args: Vec<String> = cb
        .args
        .iter()
        .enumerate()
        .map(|(i, arg)| {
            let kt = match arg.ref_kind {
                ArgRefKind::Owned => map_kt_owned(&arg.type_name, ir),
                ArgRefKind::Ref
                | ArgRefKind::RefMut
                | ArgRefKind::Ptr
                | ArgRefKind::PtrMut => "Pointer?".to_string(),
            };
            let raw_name = arg.name.trim();
            let name = if raw_name.is_empty() {
                format!("arg{}", i)
            } else {
                sanitize_kt_identifier(raw_name)
            };
            format!("{}: {}", name, kt)
        })
        .collect();

    builder.line(&format!(
        "fun callback({}): {}",
        args.join(", "),
        return_type
    ));

    builder.dedent();
    builder.line("}");
    builder.blank();
}

