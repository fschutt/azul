fn emit_struct(builder: &mut CodeBuilder, s: &StructDef, ir: &CodegenIR) {
    let name = ffi_type_name(&s.name);

    if !s.doc.is_empty() {
        // (KDoc switched to triple-slash to bypass parser issues with `*/` in inline code samples)
        for d in &s.doc {
            builder.line(&format!("/// {}", wrappers::kdoc_escape(d)));
        }
        
    }

    builder.line(&format!("open class {} : Structure() {{", name));
    builder.indent();

    let mut field_names: Vec<String> = Vec::new();

    if s.fields.is_empty() {
        builder.line("@JvmField var _dummy: Byte = 0");
        field_names.push("\"_dummy\"".to_string());
    } else {
        for f in &s.fields {
            emit_struct_field(builder, f, ir, &mut field_names);
        }
    }

    emit_field_order(builder, &field_names);

    // AzVec<T> iterable accessor — see the Java mirror in
    // lang_java/types.rs::emit_vec_to_list_java.
    if s.category == TypeCategory::Vec {
        emit_vec_to_list_kt(builder, s, ir);
    }

    emit_byvalue_byref(builder, &name);

    builder.dedent();
    builder.line("}");
    builder.blank();
}

fn emit_vec_to_list_kt(builder: &mut CodeBuilder, s: &StructDef, ir: &CodegenIR) {
    let Some(ptr_field) = s.fields.iter().find(|f| f.name == "ptr") else {
        return;
    };
    let elem_rust = ptr_field.type_name.trim();
    let elem_jvm = base_map_jvm_type(elem_rust, ir);
    let elem_kt = jvm_to_kotlin_primitive(&elem_jvm);
    // Skip exotic element types (void / Unit) — typically a typed
    // pointer field the JVM type-map collapsed to opaque. Users can
    // still reach the raw ptr/len fields.
    if elem_kt == "Unit" || elem_kt == "void" || elem_jvm == "void" {
        return;
    }

    let primitive = match elem_kt.as_str() {
        "Byte" => Some(("ByteArray", "toByteArray", "getByteArray")),
        "Short" => Some(("ShortArray", "toShortArray", "getShortArray")),
        "Int" => Some(("IntArray", "toIntArray", "getIntArray")),
        "Long" => Some(("LongArray", "toLongArray", "getLongArray")),
        "Float" => Some(("FloatArray", "toFloatArray", "getFloatArray")),
        "Double" => Some(("DoubleArray", "toDoubleArray", "getDoubleArray")),
        _ => None,
    };
    if let Some((ret, method, jna_method)) = primitive {
        builder.line("/**");
        builder.line(&format!(
            " * Decode the wrapped {} elements into a Kotlin array.",
            elem_rust
        ));
        builder.line(" */");
        builder.line(&format!("fun {}(): {} {{", method, ret));
        builder.indent();
        builder.line(&format!(
            "if (ptr == null || len == 0L) return {}(0)",
            ret
        ));
        // `ptr` is `@JvmField var` (mutable, nullable). Kotlin can't
        // smart-cast a mutable property across the null guard above,
        // so use `!!` to assert non-null at the call site.
        builder.line(&format!(
            "return ptr!!.{}(0, len.toInt())",
            jna_method
        ));
        builder.dedent();
        builder.line("}");
        return;
    }

    // Struct elements → List<T>.
    builder.line("/**");
    builder.line(&format!(
        " * Decode the wrapped {} elements into a Kotlin List.",
        elem_rust
    ));
    builder.line(" */");
    builder.line(&format!(
        "fun toList(): kotlin.collections.List<{}> {{",
        elem_kt
    ));
    builder.indent();
    builder.line(&format!(
        "if (ptr == null || len == 0L) return emptyList()"
    ));
    builder.line(&format!(
        "val __out = java.util.ArrayList<{}>(len.toInt())",
        elem_kt
    ));
    builder.line(&format!(
        "val __size = Structure.newInstance({}::class.java).size()",
        elem_kt
    ));
    builder.line("for (__i in 0 until len) {");
    builder.indent();
    // `ptr` is mutable; smart-cast across the null guard above doesn't
    // apply. Assert non-null with `!!`.
    builder.line("val __ep = ptr!!.share(__i * __size)");
    builder.line(&format!(
        "val __e = Structure.newInstance({}::class.java, __ep)",
        elem_kt
    ));
    builder.line("__e.read()");
    builder.line("__out.add(__e)");
    builder.dedent();
    builder.line("}");
    builder.line("return __out");
    builder.dedent();
    builder.line("}");
}

fn emit_struct_field(
    builder: &mut CodeBuilder,
    f: &FieldDef,
    ir: &CodegenIR,
    field_names: &mut Vec<String>,
) {
    if let Some(ref doc) = f.doc {
        builder.line(&format!("/** {} */", doc));
    }

    if let Some((elem_ty, count)) = parse_array_type(&f.type_name) {
        let kt_elem = base_map_jvm_type(&elem_ty, ir);
        // Map Java primitive name to Kotlin primitive name for arrays:
        let kt_elem_kt = jvm_to_kotlin_primitive(&kt_elem);
        let array_type = match kt_elem_kt.as_str() {
            "Byte" => "ByteArray".to_string(),
            "Short" => "ShortArray".to_string(),
            "Int" => "IntArray".to_string(),
            "Long" => "LongArray".to_string(),
            "Float" => "FloatArray".to_string(),
            "Double" => "DoubleArray".to_string(),
            "Boolean" => "BooleanArray".to_string(),
            other => format!("Array<{}>", other),
        };
        let init = match kt_elem_kt.as_str() {
            "Byte" => format!("ByteArray({})", count),
            "Short" => format!("ShortArray({})", count),
            "Int" => format!("IntArray({})", count),
            "Long" => format!("LongArray({})", count),
            "Float" => format!("FloatArray({})", count),
            "Double" => format!("DoubleArray({})", count),
            "Boolean" => format!("BooleanArray({})", count),
            _ => format!("arrayOfNulls({})", count),
        };
        builder.line(&format!(
            "@JvmField var {}: {} = {}",
            sanitize_kt_identifier(&f.name),
            array_type,
            init
        ));
        field_names.push(format!("\"{}\"", f.name));
        return;
    }

    let (kt, default) = ref_kind_kt_field(&f.type_name, &f.ref_kind, ir);
    builder.line(&format!(
        "@JvmField var {}: {} = {}",
        sanitize_kt_identifier(&f.name),
        kt,
        default
    ));
    field_names.push(format!("\"{}\"", f.name));
}

