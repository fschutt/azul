// ============================================================================
// Type mapping (IR / Rust → Algol 68 MODE)
// ============================================================================

/// Map an IR / Rust type expression to an Algol 68 MODE.
///
/// Pointer / reference forms collapse to `REF <MODE>` when the inner
/// type is a known IR struct / enum / typedef, or to the generic
/// `REF VOID` otherwise. (Algol 68's `VOID` is normally restricted to
/// PROC return types, but a68g accepts `REF VOID` as the C `void *`
/// equivalent.) Strings collapse to the built-in `STRING` MODE; a68g's
/// runtime marshals `STRING` to a null-terminated `char *` automatically.
pub fn map_type_to_algol(rust_type: &str, ir: &CodegenIR) -> String {
    let t = rust_type.trim();

    // Pointer / reference prefixes.
    if let Some(rest) = t.strip_prefix("*const ") {
        return ptr_type(rest, ir);
    }
    if let Some(rest) = t.strip_prefix("*mut ") {
        return ptr_type(rest, ir);
    }
    if let Some(rest) = t.strip_prefix("&mut ") {
        return ptr_type(rest, ir);
    }
    if let Some(rest) = t.strip_prefix('&') {
        return ptr_type(rest, ir);
    }

    match t {
        // Algol 68 has no `void` MODE for variables; map to `REF VOID`
        // (a68g accepts this as the void* equivalent).
        "void" | "c_void" | "()" => "REF VOID".to_string(),

        "bool" | "GLboolean" => "BOOL".to_string(),
        "char" | "c_char" | "i8" | "u8" | "c_uchar" => "CHAR".to_string(),

        "i16" | "u16" => "SHORT INT".to_string(),
        "i32" | "u32" | "c_int" | "c_uint" | "GLint" | "GLuint" | "GLenum" | "GLsizei"
        | "GLbitfield" => "INT".to_string(),
        "i64" | "u64" | "GLint64" | "GLuint64" | "usize" | "isize" | "size_t" => {
            "LONG INT".to_string()
        }

        "f32" | "GLfloat" | "GLclampf" => "REAL".to_string(),
        "f64" | "GLdouble" | "GLclampd" => "LONG REAL".to_string(),

        // Known IR types -> the AZ-prefixed uppercase MODE.
        _ => {
            if ir.find_struct(t).is_some()
                || ir.find_enum(t).is_some()
                || ir.find_type_alias(t).is_some()
                || ir.callback_typedefs.iter().any(|c| c.name == t)
            {
                algol_mode_name(t)
            } else {
                // Unknown -> opaque void pointer.
                "REF VOID".to_string()
            }
        }
    }
}

/// Helper: turn the inner type after a pointer / reference sigil into an
/// Algol 68 `REF <MODE>` expression.
pub fn ptr_type(inner: &str, ir: &CodegenIR) -> String {
    let inner = inner.trim();
    match inner {
        "c_char" | "char" => "STRING".to_string(),
        "c_void" | "void" | "()" => "REF VOID".to_string(),
        _ => {
            if ir.find_struct(inner).is_some()
                || ir.find_enum(inner).is_some()
                || ir.find_type_alias(inner).is_some()
            {
                format!("REF {}", algol_mode_name(inner))
            } else {
                "REF VOID".to_string()
            }
        }
    }
}

/// Sanitize a doc comment for inclusion in Algol 68's `# ... #`
/// comment delimiters: an embedded `#` would close the comment, so we
/// replace it with a `?` and collapse newlines to spaces.
pub fn sanitize_comment(s: &str) -> String {
    s.replace('#', "?").replace('\n', " ").replace('\r', " ")
}
