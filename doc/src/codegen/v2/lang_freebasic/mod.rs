/// Sanitize an identifier for use as a FreeBASIC field / argument name.
/// Reserved-word collisions get a trailing underscore.
pub fn sanitize_identifier(name: &str) -> String {
    if is_freebasic_reserved(name) {
        format!("{}_", name)
    } else {
        name.to_string()
    }
}

/// Convert a snake_case or camelCase name to PascalCase (idiomatic
/// for FreeBASIC method / wrapper names).
pub fn to_pascal_case(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    let mut upper_next = true;
    for c in s.chars() {
        if c == '_' {
            upper_next = true;
        } else if upper_next {
            out.extend(c.to_uppercase());
            upper_next = false;
        } else {
            out.push(c);
        }
    }
    out
}

/// Sanitize a doc comment so it can sit safely inside a FreeBASIC
/// `'` line comment. We collapse newlines into spaces — `'` only
/// terminates at end-of-line so apostrophes are fine inside.
pub fn sanitize_comment(s: &str) -> String {
    s.replace('\n', " ").replace('\r', " ")
}
