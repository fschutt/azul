//! FreeBASIC binding generator.
//!
//! Generates a single `azul.bi` include header that exposes the prebuilt
//! `azul.dll` / `libazul.so` / `libazul.dylib` to FreeBASIC programs.
//!
//! The output has four sections, top-to-bottom:
//!
//! 1. `#inclib "azul"` plus banner comment — instructs `fbc` to link
//!    against the shared library.
//! 2. POD `Type ... End Type` records, integer-backed `Enum ... End Enum`
//!    declarations, and tagged-union types (`Type` containing a `Union`).
//! 3. `Extern "C" Lib "azul" ... End Extern` block declaring every C-ABI
//!    function with `Alias "az_..."` so the case-insensitive FreeBASIC
//!    name resolution does not mangle the symbol the linker looks for.
//! 4. Idiomatic `Type ... End Type` wrappers inside `Namespace Azul ...
//!    End Namespace` whose `Constructor` invokes the matching `_create`
//!    / `_new` C function and whose `Destructor` calls `_delete`.
//!    User-facing names drop the `Az` prefix (e.g. `Azul.App`,
//!    `Azul.Dom`).
//!
//! # FreeBASIC quirks
//!
//! - `fbc` is case-INSENSITIVE for identifiers: `App` and `APP` and `app`
//!   are the same symbol. Only the link-name in `Alias "..."` is
//!   case-sensitive — that string is the one the dynamic loader matches.
//! - `Field = 1` on a `Type` block forces 1-byte packed C-compatible
//!   struct layout, matching Rust's `#[repr(C, packed)]`. We use plain
//!   `Type` (default natural alignment) because Rust's `extern "C"`
//!   structs use natural alignment, not packed.
//! - FreeBASIC's `Integer` is platform-width (32-bit on x86, 64-bit on
//!   x86_64). For C-ABI portability we use the explicit-size aliases
//!   `LongInt` (32-bit), `LongLong` (64-bit), `Single` (f32),
//!   `Double` (f64), `UByte` (u8), etc.
//! - String pointers from C use `ZString Ptr` (null-terminated UTF-8).
//! - `Any Ptr` is the FreeBASIC equivalent of `void*`.
//!
//! # Wiring
//!
//! Like the other Wave-2 generators (Pascal/Ada/etc.) this module is
//! NOT wired from `v2/mod.rs` — the orchestrator threads it through
//! after merging the per-language sidecar JSON. Entry point:
//! [`generate`].

use anyhow::Result;

use super::config::CodegenConfig;
use super::generator::CodeBuilder;
use super::ir::CodegenIR;

pub mod functions;
pub mod types;
pub mod wrappers;

/// Library name used in `Lib "azul"` and `#inclib "azul"`.
///
/// `fbc` resolves this to `azul.dll` on Windows, `libazul.so` on Linux,
/// and `libazul.dylib` on macOS.
pub const LIB_NAME: &str = "azul";

/// Public entry point. Produces the full `azul.bi` include source.
///
/// The caller writes the result to `target/codegen/v2/azul.bi` (or wherever
/// the orchestrator decides). FreeBASIC builds via `fbc hello-world.bas`
/// directly; no separate project file is emitted.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    let mut builder = CodeBuilder::new("    ");

    emit_header(&mut builder);

    // `#inclib "azul"` tells fbc to link against the shared library.
    // It is a directive, not a declaration, so it sits before everything.
    builder.line(&format!("#inclib \"{}\"", LIB_NAME));
    builder.blank();

    // 1. POD types, enums, tagged unions, callback procedural typedefs.
    types::generate_types(&mut builder, ir, config)?;

    // 2. Extern "C" Lib "azul" ... End Extern block.
    builder.line(&format!("Extern \"C\" Lib \"{}\"", LIB_NAME));
    builder.blank();
    functions::generate_externals(&mut builder, ir, config)?;
    builder.blank();
    builder.line("End Extern");
    builder.blank();

    // 3. Idiomatic Namespace Azul wrappers.
    wrappers::generate_wrappers(&mut builder, ir, config)?;

    Ok(builder.finish())
}

fn emit_header(builder: &mut CodeBuilder) {
    builder.line("' ============================================================================");
    builder.line("' Auto-generated FreeBASIC bindings for the Azul GUI framework.");
    builder.line("' Generated by azul-doc codegen v2 (lang_freebasic).");
    builder.line("' DO NOT EDIT MANUALLY.");
    builder.line("'");
    builder.line("' Compatible with FreeBASIC 1.10+.");
    builder.line("' Use:   #include \"azul.bi\"");
    builder.line("' Build: fbc your-program.bas");
    builder.line("' ============================================================================");
    builder.blank();
}

