// Shared type-name helpers (used by submodules)
// ============================================================================

/// The Az-prefixed FFI type name for a given IR type (e.g. `Dom` -> `AzDom`).
///
/// FreeBASIC does not require a leading `T`/`P` like Pascal — the same
/// identifier is used for the type and for declaring variables of that
/// type. Pointers are written `AzDom Ptr`.
pub fn ffi_type_name(name: &str) -> String {
    format!("Az{}", name)
}

/// Map an IR / Rust type name to the corresponding FreeBASIC type token.
///
/// - Pointers / references collapse to `<inner> Ptr` when the inner is a
///   known IR type, `ZString Ptr` for `c_char` / `char` strings, and
///   `Any Ptr` (void*) otherwise.
/// - Primitives map to the explicit-width FreeBASIC aliases so the
///   binding is correct on both 32- and 64-bit hosts.
pub fn map_type_to_fb(rust_type: &str, ir: &CodegenIR) -> String {
    let trimmed = rust_type.trim();

    // Pointers / references → typed `Ptr` when the inner is known.
    if let Some(rest) = trimmed.strip_prefix("*const ") {
        return ptr_type_for(rest, ir);
    }
    if let Some(rest) = trimmed.strip_prefix("*mut ") {
        return ptr_type_for(rest, ir);
    }
    if let Some(rest) = trimmed.strip_prefix("&mut ") {
        return ptr_type_for(rest, ir);
    }
    if let Some(rest) = trimmed.strip_prefix('&') {
        return ptr_type_for(rest, ir);
    }

    // Arrays: `[T; N]` → fixed-size FB array `<elem> * N`. FB array
    // syntax is positional in `Type` field declarations, so we emit it
    // as a parenthesised dimension that the field emitter can place
    // after the field name.
    if trimmed.starts_with('[') && trimmed.ends_with(']') {
        let inner = &trimmed[1..trimmed.len() - 1];
        if let Some(semi) = inner.rfind(';') {
            let elem = inner[..semi].trim();
            if let Ok(count) = inner[semi + 1..].trim().parse::<usize>() {
                let fb_elem = map_type_to_fb(elem, ir);
                // Encode as a marker the field emitter can split on.
                // FreeBASIC field syntax is `name(0 To N-1) As elem`.
                let upper = if count == 0 { 0 } else { count - 1 };
                return format!("__FB_ARRAY__{}__{}", upper, fb_elem);
            }
        }
    }

    match trimmed {
        // Void / unit
        "void" | "c_void" | "()" => "Any Ptr".to_string(),

        // Booleans — FB has a native `Boolean`, but the C ABI uses 1 byte.
        "bool" | "GLboolean" => "UByte".to_string(),

        // Signed / unsigned integers via explicit-width FB aliases.
        "i8" | "c_char" | "char" => "Byte".to_string(),
        "u8" | "c_uchar" => "UByte".to_string(),
        "i16" => "Short".to_string(),
        "u16" => "UShort".to_string(),
        "i32" | "c_int" | "GLint" | "GLsizei" => "LongInt".to_string(),
        "u32" | "c_uint" | "GLuint" | "GLenum" | "GLbitfield" => "ULongInt".to_string(),
        "i64" | "GLint64" => "LongLong".to_string(),
        "u64" | "GLuint64" => "ULongLong".to_string(),
        "f32" | "GLfloat" | "GLclampf" => "Single".to_string(),
        "f64" | "GLdouble" | "GLclampd" => "Double".to_string(),
        "usize" | "size_t" | "uintptr_t" => "ULongLong".to_string(),
        "isize" | "ssize_t" | "intptr_t" | "GLsizeiptr" | "GLintptr" => "LongLong".to_string(),

        _ => {
            if ir.find_struct(trimmed).is_some()
                || ir.find_enum(trimmed).is_some()
                || ir.find_type_alias(trimmed).is_some()
                || ir.callback_typedefs.iter().any(|c| c.name == trimmed)
            {
                ffi_type_name(trimmed)
            } else {
                // Unknown — opaque void-pointer.
                "Any Ptr".to_string()
            }
        }
    }
}

/// Pointer mapping helper: `<inner>` (after `*const`/`*mut`/`&`/`&mut`)
/// → FB type token.
fn ptr_type_for(inner: &str, ir: &CodegenIR) -> String {
    let inner = inner.trim();
    match inner {
        "c_char" | "char" => "ZString Ptr".to_string(),
        "i8" | "u8" => "UByte Ptr".to_string(),
        "c_void" | "void" | "()" => "Any Ptr".to_string(),
        _ => {
            if ir.find_struct(inner).is_some()
                || ir.find_enum(inner).is_some()
                || ir.find_type_alias(inner).is_some()
            {
                format!("{} Ptr", ffi_type_name(inner))
            } else {
                "Any Ptr".to_string()
            }
        }
    }
}

