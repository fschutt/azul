//! Makefile emission for Fortran examples.
//!
//! Fortran has no canonical package manifest the way C# has `.csproj`
//! or Ruby has `.gemspec`. The `fpm` (Fortran Package Manager) project
//! is gaining adoption but requires `fpm` to be installed; gfortran +
//! a plain Makefile is still the universal lowest common denominator.
//!
//! The emitted Makefile compiles `azul.f90` (the generated module) as
//! a `.mod` + `.o` pair, then compiles the example program against it.
//! The user is expected to drop `libazul.so` / `azul.dll` /
//! `libazul.dylib` next to the example before running `make`.

/// Generate a generic Makefile for the Fortran hello-world example.
///
/// The Makefile is parameter-free and assumes:
/// - `azul.f90` is the generated bindings module (sibling to the example).
/// - `hello_world.f90` is the example program.
/// - The shared library is named `libazul.{so,dylib,dll}` and lives in
///   the same directory.
///
/// The user can override `FC=` on the command line to swap compilers
/// (e.g. `make FC=ifort`).
pub fn generate_makefile() -> String {
    // Use a raw string and avoid `cat << EOF`-style indentation traps;
    // every line below is the literal Makefile text.
    let body = r#"# ============================================================================
# Auto-generated Makefile for the Fortran hello-world example.
# Generated by azul-doc codegen v2 (lang_fortran::makefile).
# ============================================================================
#
# Usage:
#   make            - build hello_world
#   make clean      - remove build artifacts
#   make run        - run the example
#
# Override FC=ifort or FC=ifx to use the Intel Fortran compiler.

# NOT `FC ?=`: GNU make PREDEFINES FC=f77 as a builtin default, so `?=`
# never fires and `make` tries the nonexistent f77. $(origin) tells the
# builtin default apart from a real user override (env / command line).
ifeq ($(origin FC),default)
FC      = gfortran
endif
# -ffree-line-length-none: the generated azul.f90 has declaration lines
# beyond the F2008 132-column limit (long widget/callback type names);
# without it -std=f2008 truncates them into hard errors. Intel ifort/ifx
# allows 7200-char lines by default, so the flag is gfortran-only anyway.
FFLAGS  ?= -O2 -std=f2008 -ffree-line-length-none -fimplicit-none
LDFLAGS ?= -L. -Wl,-rpath,'$$ORIGIN'
LIBS    ?= -lazul

EXE     := hello_world

# `azul.f90` produces both `azul.o` and an `azul.mod` (compiler-managed
# module file) that `hello_world.f90` USEs.

all: $(EXE)

azul.o azul.mod: azul.f90
	$(FC) $(FFLAGS) -c azul.f90

hello_world.o: hello_world.f90 azul.mod
	$(FC) $(FFLAGS) -c hello_world.f90

$(EXE): hello_world.o azul.o
	$(FC) $(FFLAGS) hello_world.o azul.o $(LDFLAGS) $(LIBS) -o $(EXE)

run: $(EXE)
	./$(EXE)

clean:
	rm -f *.o *.mod $(EXE)

.PHONY: all run clean
"#;
    body.to_string()
}
