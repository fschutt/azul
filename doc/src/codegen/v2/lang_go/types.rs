//! Go-side type emission for the Go (cgo) generator.
//!
//! Two distinct outputs:
//!
//! 1. **Unit-only enums** -> `type AzButtonType uint32` plus a `const`
//!    block listing every variant. Values are referenced through cgo
//!    (`C.AzButtonType_Primary`) so the Go side stays in sync with the
//!    C ABI even if the underlying numeric values change.
//!
//! 2. **Tagged-union enums** -> a sealed Go interface
//!    (`type AzCssProperty interface { isAzCssProperty() }`) plus a
//!    per-variant struct that implements it. This gives users
//!    Go-idiomatic exhaustiveness via type switches:
//!
//!    ```go
//!    switch v := prop.(type) {
//!    case AzCssPropertyFontSize:
//!        // v.Payload has the typed value
//!    case AzCssPropertyTextColor:
//!        // ...
//!    }
//!    ```
//!
//!    Conversion to/from the FFI struct (`fromCAzCssProperty`,
//!    `toCAzCssProperty`) lives next to the variants so callers don't
//!    have to handcraft tag dispatch.
//!
//! POD structs are intentionally NOT mirrored here: they're already
//! reachable as `C.AzWindowState`, `C.AzColorU`, etc. through cgo, and
//! redeclaring them on the Go side would create blittability headaches
//! (Go strings / slices / pointers don't share layout with C). When a
//! wrapper method needs to surface a POD struct to user code, it hands
//! back the cgo type directly. The IR's `Recursive`, `VecRef`, `Boxed`,
//! `GenericTemplate`, and `DestructorOrClone` categories are skipped
//! with a `// SKIPPED:` marker.

use anyhow::Result;

use super::super::config::CodegenConfig;
use super::super::generator::CodeBuilder;
use super::super::ir::{CodegenIR, EnumDef, EnumVariantKind, FieldRefKind, TypeCategory};
use super::{ffi_type_name, sanitize_identifier};

/// Generate the contents of `types.go`.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    let mut b = CodeBuilder::new(&config.indent);

    emit_header(&mut b);

    // Unit enums first - they may be referenced by tagged-union variant
    // payloads.
    for e in &ir.enums {
        if !should_include_enum(e, config) {
            continue;
        }
        if e.is_union {
            continue;
        }
        emit_unit_enum(&mut b, e);
    }

    // Tagged-union sealed-interface hierarchies.
    for e in &ir.enums {
        if !should_include_enum(e, config) {
            continue;
        }
        if !e.is_union {
            continue;
        }
        emit_tagged_union(&mut b, e, ir);
    }

    Ok(b.finish())
}

fn emit_header(b: &mut CodeBuilder) {
    b.line("// ============================================================================");
    b.line("// types.go - Go-side mirror types: enums, tagged-union sealed interfaces.");
    b.line("// Auto-generated by azul-doc codegen v2 (lang_go). DO NOT EDIT MANUALLY.");
    b.line("// ============================================================================");
    b.line("//");
    b.line("// POD structs are reachable directly as `C.Az<Name>` through cgo and are");
    b.line("// not redeclared here. Tagged unions get a Go-native sealed-interface");
    b.line("// hierarchy so users can pattern-match with `switch v := x.(type) { ... }`.");
    b.blank();
    b.line("package azul");
    b.blank();

    b.line("/*");
    b.line("#include \"azul.h\"");
    b.line("*/");
    b.line("import \"C\"");
    b.blank();
    // Keep the cgo import live even if all enums end up filtered out, so
    // the file compiles unconditionally regardless of api.json contents.
    b.line("var _ = C.size_t(0)");
    b.blank();
}

// ============================================================================
// Inclusion filter
// ============================================================================

fn should_include_enum(e: &EnumDef, config: &CodegenConfig) -> bool {
    if !config.should_include_type(&e.name) {
        return false;
    }
    if !e.generic_params.is_empty() {
        return false;
    }
    !matches!(
        e.category,
        TypeCategory::Recursive | TypeCategory::GenericTemplate | TypeCategory::DestructorOrClone
    )
}

// ============================================================================
// Unit-only enum
// ============================================================================

fn emit_unit_enum(b: &mut CodeBuilder, e: &EnumDef) {
    let go_name = ffi_type_name(&e.name);
    let underlying = enum_underlying_type(e);

    if !e.doc.is_empty() {
        for d in &e.doc {
            b.line(&format!("// {}", d));
        }
    }

    b.line(&format!("type {} {}", go_name, underlying));
    b.blank();

    b.line("const (");
    b.indent();
    for v in &e.variants {
        let safe = sanitize_identifier(&v.name);
        b.line(&format!(
            "{}_{} {} = {}(C.{}_{})",
            go_name, safe, go_name, go_name, go_name, v.name
        ));
    }
    b.dedent();
    b.line(")");
    b.blank();
}

fn enum_underlying_type(e: &EnumDef) -> &'static str {
    match e.repr.as_deref() {
        Some(r) if r.contains("u8") => "uint8",
        Some(r) if r.contains("i8") => "int8",
        Some(r) if r.contains("u16") => "uint16",
        Some(r) if r.contains("i16") => "int16",
        Some(r) if r.contains("i32") => "int32",
        Some(r) if r.contains("u64") => "uint64",
        Some(r) if r.contains("i64") => "int64",
        // Default to uint32 to match the C ABI's default `unsigned int` enum width.
        _ => "uint32",
    }
}

// ============================================================================
// Tagged union (sealed interface + per-variant struct)
// ============================================================================

fn emit_tagged_union(b: &mut CodeBuilder, e: &EnumDef, ir: &CodegenIR) {
    let iface_name = ffi_type_name(&e.name);
    let ffi_name = ffi_type_name(&e.name);
    let tag_name = format!("{}_Tag", ffi_name);
    let marker_method = format!("is{}", iface_name);

    if !e.doc.is_empty() {
        for d in &e.doc {
            b.line(&format!("// {}", d));
        }
    }

    // Sealed interface: the marker method is unexported, so only the
    // generated variant types in this package can satisfy the
    // interface. Users can write `switch v := x.(type)` on it.
    b.line(&format!("type {} interface {{", iface_name));
    b.indent();
    b.line(&format!("{}()", marker_method));
    b.dedent();
    b.line("}");
    b.blank();

    // Per-variant types.
    for v in &e.variants {
        let variant_struct = format!("{}{}", iface_name, v.name);

        if let Some(d) = &v.doc {
            b.line(&format!("// {}", d));
        }

        match &v.kind {
            EnumVariantKind::Unit => {
                b.line(&format!("type {} struct{{}}", variant_struct));
            }
            EnumVariantKind::Tuple(payloads) => {
                b.line(&format!("type {} struct {{", variant_struct));
                b.indent();
                if payloads.len() == 1 {
                    let (ty, ref_kind) = &payloads[0];
                    let go_ty = field_go_type(ty, *ref_kind, ir);
                    b.line(&format!("Payload {}", go_ty));
                } else {
                    for (i, (ty, ref_kind)) in payloads.iter().enumerate() {
                        let go_ty = field_go_type(ty, *ref_kind, ir);
                        b.line(&format!("Payload{} {}", i, go_ty));
                    }
                }
                b.dedent();
                b.line("}");
            }
            EnumVariantKind::Struct(fields) => {
                b.line(&format!("type {} struct {{", variant_struct));
                b.indent();
                for f in fields {
                    let go_ty = field_go_type(&f.type_name, f.ref_kind, ir);
                    let name = pascal_field(&f.name);
                    b.line(&format!("{} {}", name, go_ty));
                }
                b.dedent();
                b.line("}");
            }
        }
        // Marker-method implementation.
        b.line(&format!(
            "func ({}) {}() {{}}",
            variant_struct, marker_method
        ));
        b.blank();
    }

    // Tag-discriminator constant table. Mirrors the C ABI's
    // `Az<Enum>_Tag_<Variant>` constants under a Go-friendly name so
    // user code never has to touch the underlying cgo identifiers.
    //
    // Pluralised (`<Iface>Tags`) to avoid colliding with the
    // `<Iface>Tag` *type* the unit-enum emit produces for the
    // discriminator enum itself — Go forbids redeclaring an identifier
    // even when one is a type and the other a `var`.
    b.line(&format!(
        "// {}Tags exposes the C-ABI tag discriminators for {} (variant-name accessors).",
        iface_name, iface_name
    ));
    b.line(&format!("var {}Tags = struct {{", iface_name));
    b.indent();
    for v in &e.variants {
        let safe = sanitize_identifier(&v.name);
        b.line(&format!("{} C.{}", safe, tag_name));
    }
    b.dedent();
    b.line("}{");
    b.indent();
    for v in &e.variants {
        let safe = sanitize_identifier(&v.name);
        b.line(&format!("{}: C.{}_{},", safe, tag_name, v.name));
    }
    b.dedent();
    b.line("}");
    b.blank();
}

// ============================================================================
// Field-type mapping
// ============================================================================

/// Map an IR struct/variant field type + ref_kind to the user-facing
/// Go type. We keep this conservative: when the type is a known FFI
/// type we route through `C.Az<Name>`; primitives use Go-native names.
/// Pointer fields surface as `unsafe.Pointer` when we can't tell what
/// they point at.
fn field_go_type(type_name: &str, ref_kind: FieldRefKind, ir: &CodegenIR) -> String {
    let trimmed = type_name.trim();

    if let Some(rest) = trimmed.strip_prefix("*const ") {
        return format!("*{}", field_go_type(rest, FieldRefKind::Owned, ir));
    }
    if let Some(rest) = trimmed.strip_prefix("*mut ") {
        return format!("*{}", field_go_type(rest, FieldRefKind::Owned, ir));
    }

    // Array form `[T; N]` — Go has no `[T; N]` literal that's
    // interchangeable with a Go slice, but cgo emits an array type
    // anyway. Surface it through the C namespace.
    if trimmed.starts_with('[') && trimmed.ends_with(']') {
        // SKIPPED: array fields are accessed through the cgo type directly.
        return "unsafe.Pointer".to_string();
    }

    if let Some(go) = super::primitive_to_go(trimmed) {
        if go.is_empty() {
            return "unsafe.Pointer".to_string();
        }
        let base = go.to_string();
        return apply_ref_kind(base, ref_kind);
    }

    // Known FFI type — surface the cgo type.
    let base = if ir.find_struct(trimmed).is_some()
        || ir.find_enum(trimmed).is_some()
        || ir.find_type_alias(trimmed).is_some()
        || ir.callback_typedefs.iter().any(|c| c.name == trimmed)
    {
        format!("C.{}", ffi_type_name(trimmed))
    } else {
        // Unknown type — route as opaque pointer to keep the file compilable.
        "unsafe.Pointer".to_string()
    };

    apply_ref_kind(base, ref_kind)
}

fn apply_ref_kind(base: String, ref_kind: FieldRefKind) -> String {
    match ref_kind {
        FieldRefKind::Owned => base,
        FieldRefKind::Ref
        | FieldRefKind::RefMut
        | FieldRefKind::Ptr
        | FieldRefKind::PtrMut
        | FieldRefKind::Boxed
        | FieldRefKind::OptionBoxed => format!("*{}", base),
    }
}

/// Convert a snake_case field name to PascalCase so it's exported
/// from the package (Go visibility rule).
fn pascal_field(name: &str) -> String {
    super::snake_to_pascal(name)
}
