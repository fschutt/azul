//! Emits the `go.mod` manifest for the generated Go bindings.
//!
//! Go modules are minimal: a `module` declaration giving the import
//! path, plus a `go` directive declaring the language version. Azul's
//! Go bindings have no third-party dependencies (cgo + stdlib is enough),
//! so `require` is empty.
//!
//! The module path follows the convention used by other azul language
//! bindings published under `github.com/azul/`. Consumers who fork or
//! vendor the package can rewrite the `module` line to match their
//! import structure.

/// Generate the contents of a `go.mod` file as a String.
///
/// We target Go 1.21 because that's the first release that stabilises
/// the standard library's `cmp`, `slices`, and `maps` packages — useful
/// for downstream consumers — without requiring any newer-language
/// features cgo cares about. Older Go versions (>= 1.18) will compile
/// the generated bindings just fine; bumping the directive only blocks
/// people on truly old toolchains.
pub fn generate_go_mod() -> String {
    String::from(
        "// go.mod - auto-generated by azul-doc codegen v2 (lang_go).\n\
         // DO NOT EDIT MANUALLY - re-run the generator instead.\n\
         //\n\
         // Consumers who fork or vendor this package can rewrite the\n\
         // `module` line to match their own import path.\n\
         \n\
         module github.com/azul/azul-go\n\
         \n\
         go 1.21\n",
    )
}
