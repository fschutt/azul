//! Top-level Go function emission for the Go (cgo) generator.
//!
//! Most C functions are exposed as **methods** on wrapper types (see
//! `wrappers.rs`). This file holds the small remainder:
//!
//! 1. Free constants exported from api.json (e.g. `GL_TRUE`).
//! 2. Free functions that aren't attached to any class — currently the
//!    IR doesn't surface any (every C-ABI function belongs to some
//!    class), so we emit only a header section and an explicit
//!    `// SKIPPED:` marker per category that intentionally produces
//!    nothing.
//!
//! Keeping this file in the manifest (instead of inlining into
//! `wrappers.go`) preserves the conventional Go layout: one logical
//! unit per file. If a future api.json revision starts emitting
//! free functions, this is where they'd land.

use anyhow::Result;

use super::super::config::CodegenConfig;
use super::super::generator::CodeBuilder;
use super::super::ir::CodegenIR;

/// Generate the contents of `functions.go`.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    let mut b = CodeBuilder::new(&config.indent);

    emit_header(&mut b);

    emit_constants(&mut b, ir);

    Ok(b.finish())
}

fn emit_header(b: &mut CodeBuilder) {
    b.line("// ============================================================================");
    b.line("// functions.go - Top-level Go constants and (any) free functions.");
    b.line("// Auto-generated by azul-doc codegen v2 (lang_go). DO NOT EDIT MANUALLY.");
    b.line("// ============================================================================");
    b.line("//");
    b.line("// Most C-ABI functions are surfaced as methods on wrapper types in");
    b.line("// `wrappers.go` (App, Dom, Css, etc.). This file holds the small set");
    b.line("// of stand-alone declarations that don't fit any class.");
    b.blank();
    b.line("package azul");
    b.blank();
    b.line("/*");
    b.line("#include \"azul.h\"");
    b.line("*/");
    b.line("import \"C\"");
    b.blank();
    // Keep the cgo import live even when no constants reference it, so the
    // file compiles unconditionally regardless of api.json contents.
    b.line("var _ = C.size_t(0)");
    b.blank();
}

// ============================================================================
// Constants
// ============================================================================

fn emit_constants(b: &mut CodeBuilder, ir: &CodegenIR) {
    if ir.constants.is_empty() {
        b.line("// SKIPPED: api.json carries no top-level constants for this build.");
        b.blank();
        return;
    }

    b.line("// Public constants exported by the C ABI.");
    b.line("const (");
    b.indent();
    for c in &ir.constants {
        if !c.doc.is_empty() {
            for d in &c.doc {
                b.line(&format!("// {}", d));
            }
        }
        let go_ty = super::primitive_to_go(&c.type_name).unwrap_or("");
        if go_ty.is_empty() {
            b.line(&format!(
                "// SKIPPED: constant {} has non-primitive type {}.",
                c.name, c.type_name
            ));
            continue;
        }
        b.line(&format!("{} {} = {}", c.name, go_ty, c.value));
    }
    b.dedent();
    b.line(")");
    b.blank();
}
