//! Wrapper documentation for the COBOL binding.
//!
//! Standard COBOL has no class / destructor mechanism. OO-COBOL exists
//! (the `INVOKE` / `METHOD` syntax shipped with the COBOL 2002 standard
//! and accepted by GnuCOBOL >= 3.0) but is so rarely used in practice
//! that emitting OO scaffolding would obscure the primary copybook
//! content for the 99% audience that uses procedural COBOL.
//!
//! Instead we emit a documentation block describing the expected
//! manual-cleanup pattern: every type that owns native memory has a
//! companion `FN-AZ-<TYPE>-DELETE` constant; the user must call it
//! before the owning record goes out of scope. The block lists every
//! such type so a copybook user can audit their code at a glance.
//!
//! No paragraphs / executable PROCEDURE DIVISION code is generated by
//! this module. The entire output is COBOL line comments (`*` in the
//! indicator area, column 7) intended to live in `WORKING-STORAGE
//! SECTION`.

use std::collections::BTreeSet;

use anyhow::Result;

use super::super::config::CodegenConfig;
use super::super::generator::CodeBuilder;
use super::super::ir::{CodegenIR, FunctionKind, StructDef, TypeCategory};
use super::{cobol_identifier, to_cobol_case};

pub fn generate_wrapper_docs(
    builder: &mut CodeBuilder,
    ir: &CodegenIR,
    config: &CodegenConfig,
) -> Result<()> {
    let targets = collect_owning_targets(ir, config);

    builder.line("      ******************************************************************");
    builder.line("      * MANUAL CLEANUP PATTERN                                         *");
    builder.line("      *                                                                 *");
    builder.line("      * COBOL has no destructor. Every type below owns native memory   *");
    builder.line("      * and the caller MUST invoke its matching delete entry before    *");
    builder.line("      * the owning record goes out of scope, or the address is         *");
    builder.line("      * overwritten. Pattern:                                          *");
    builder.line("      *                                                                 *");
    builder.line("      *   01  WS-APP USAGE POINTER.                                    *");
    builder.line("      *   ...                                                          *");
    builder.line("      *   CALL FN-AZ-APP-CREATE USING BY VALUE WS-DATA                 *");
    builder.line("      *                                BY VALUE WS-CONFIG              *");
    builder.line("      *                       RETURNING WS-APP.                        *");
    builder.line("      *   ...                                                          *");
    builder.line("      *   CALL FN-AZ-APP-DELETE USING BY VALUE WS-APP.                 *");
    builder.line("      *                                                                 *");
    builder.line("      * Types with manual cleanup requirements                         *");
    builder.line("      * (one delete entry per type):                                   *");
    builder.line("      ******************************************************************");

    if targets.is_empty() {
        builder.line("      * (no types in this build require manual cleanup)");
        builder.blank();
        return Ok(());
    }

    for s in &targets {
        let t = cobol_identifier(&format!("AZ-{}", to_cobol_case(&s.name)));
        let dtor = cobol_identifier(&format!("FN-AZ-{}-DELETE", to_cobol_case(&s.name)));
        builder.line(&format!("      *   {:<32} -> {}", t, dtor));
    }
    builder.blank();

    builder.line("      ******************************************************************");
    builder.line("      * SUGGESTED HELPER PARAGRAPHS                                    *");
    builder.line("      *                                                                 *");
    builder.line("      * Copy these into your PROCEDURE DIVISION (do not put them in    *");
    builder.line("      * the copybook; they are documentation only). Each paragraph     *");
    builder.line("      * frees the named type unconditionally. Replace WS-* with your   *");
    builder.line("      * actual variable.                                               *");
    builder.line("      *                                                                 *");
    builder.line("      *   FREE-AZ-APP.                                                 *");
    builder.line("      *       IF WS-APP NOT = NULL                                     *");
    builder.line("      *           CALL FN-AZ-APP-DELETE USING BY VALUE WS-APP          *");
    builder.line("      *           SET WS-APP TO NULL                                   *");
    builder.line("      *       END-IF.                                                  *");
    builder.line("      ******************************************************************");
    builder.blank();

    Ok(())
}

/// Collect every struct that has a matching `_delete` C function and
/// passes the inclusion filter — these are the types the COBOL caller
/// must dispose of explicitly.
fn collect_owning_targets<'a>(ir: &'a CodegenIR, config: &CodegenConfig) -> Vec<&'a StructDef> {
    let delete_set: BTreeSet<&str> = ir
        .functions
        .iter()
        .filter(|f| f.kind == FunctionKind::Delete)
        .map(|f| f.class_name.as_str())
        .collect();

    ir.structs
        .iter()
        .filter(|s| should_emit(s, config) && delete_set.contains(s.name.as_str()))
        .collect()
}

fn should_emit(s: &StructDef, config: &CodegenConfig) -> bool {
    if !config.should_include_type(&s.name) {
        return false;
    }
    if !s.generic_params.is_empty() {
        return false;
    }
    !matches!(
        s.category,
        TypeCategory::Recursive
            | TypeCategory::VecRef
            | TypeCategory::DestructorOrClone
            | TypeCategory::GenericTemplate
    )
}
