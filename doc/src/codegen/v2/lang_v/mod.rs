//! V (vlang) binding generator.
//!
//! Emits a single `azul.v` source file (`module azul`) that exposes the
//! C-ABI of `libazul` to V programs. This is the **archetype-A**
//! (C-ABI-direct) model, cloned from the Odin backend: V has no C-header
//! importer we rely on here, so — like Odin / Pascal / Nim — we translate
//! the whole FFI surface explicitly. Every struct becomes a plain V
//! `struct`, every enum a V `enum` with an explicit backing integer,
//! every tagged union a V `union` of per-variant structs (mirroring the
//! tested `lang_c` layout), every callback typedef a V `fn`-type alias,
//! and every exported symbol a top-level `fn C.AzFoo(...)` extern
//! declaration.
//!
//! # Why plain V structs (not `struct C.Foo`)
//!
//! V only *emits* a struct definition for structs it owns (`struct Foo`);
//! a `struct C.Foo` is assumed to come from a `#include`d header, which we
//! deliberately do not ship (this is the explicit-translation model). So
//! the records are declared as ordinary `pub struct AzFoo`, which V lays
//! out with C ABI (no field reordering, natural alignment). The extern
//! `fn C.AzFoo_bar(x AzFoo)` declarations reference those same records, so
//! passing them by value is ABI-identical to the C bindings — the linker
//! only matches the bare symbol name (`AzApp_create`, …).
//!
//! # Callbacks are C-direct (the key simplification)
//!
//! A top-level V `fn` compiles to a plain C function, so a V function
//! whose signature matches a callback typedef **is** a real C function
//! pointer. `on_click` / `layout` in the counter example are passed
//! straight to `C.AzButton_setOnClick` / `C.AzWindowCreateOptions_create`
//! — there is NO host-invoker table and NO handle registration, exactly
//! like the C / Zig / Odin / Nim bindings. For each callback typedef we
//! emit a V fn-type alias (`pub type AzButtonOnClickCallbackType =
//! fn (AzRefAny, AzCallbackInfo) AzUpdate`), and functions that take a
//! callback-wrapper arg bind the RAW C symbol taking that bare fn-pointer
//! typedef (matching the raw `_setOnClick` variant the DLL exports).
//!
//! # Order-independence
//!
//! V resolves module-scope declarations regardless of source order, so —
//! unlike C / Pascal — no forward declarations or topological sort are
//! needed. Types skipped for codegen (recursive / generic template) are
//! emitted as opaque pointer aliases (`pub type AzXmlNode = voidptr`) so
//! any by-pointer reference still resolves, mirroring the Odin backend.
//!
//! # Build / link requirements
//!
//! `#flag -L.` + `#flag -lazul` (plus the macOS `#flag darwin -framework
//! …` set) make V's C backend link `libazul`. The user drops
//! `libazul.{so,dylib}` / `azul.dll` on the library path and builds with
//! `v run .`. The generated `azul.v` is meant to live in an `azul/`
//! subdirectory imported via `import azul`.

pub mod functions;
pub mod types;

use std::collections::BTreeSet;

use anyhow::Result;

use super::config::CodegenConfig;
use super::generator::CodeBuilder;
