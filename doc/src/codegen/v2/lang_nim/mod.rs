/// The `Az`-prefixed FFI type name for an IR type (e.g. `Dom` -> `AzDom`).
pub fn ffi_type_name(name: &str) -> String {
    format!("Az{}", name)
}

/// Map an IR / Rust type name to the corresponding Nim type token.
///
/// Pointers / references collapse to `ptr AzT` (typed) — `cstring` for
/// `c_char` strings, `ptr uint8` for byte buffers, and `pointer`
/// (Nim's `void*`) for opaque / unknown targets. Primitives map to
/// explicit-width Nim types so the binding is correct on 32- and 64-bit
/// hosts.
pub fn map_type_to_nim(rust_type: &str, ir: &CodegenIR) -> String {
    let trimmed = rust_type.trim();

    if let Some(rest) = trimmed.strip_prefix("*const ") {
        return ptr_type_for(rest, ir);
    }
    if let Some(rest) = trimmed.strip_prefix("*mut ") {
        return ptr_type_for(rest, ir);
    }
    if let Some(rest) = trimmed.strip_prefix("&mut ") {
        return ptr_type_for(rest, ir);
    }
    if let Some(rest) = trimmed.strip_prefix('&') {
        return ptr_type_for(rest, ir);
    }

    // Fixed-size arrays: `[T; N]` -> `array[N, <elem>]`.
    if trimmed.starts_with('[') && trimmed.ends_with(']') {
        let inner = &trimmed[1..trimmed.len() - 1];
        if let Some(semi) = inner.rfind(';') {
            let elem = inner[..semi].trim();
            if let Ok(count) = inner[semi + 1..].trim().parse::<usize>() {
                let nim_elem = map_type_to_nim(elem, ir);
                return format!("array[{}, {}]", count, nim_elem);
            }
        }
    }

    match trimmed {
        // Void / unit — only reachable in value position (e.g. an opaque
        // `*mut c_void` field already handled above); model as `pointer`.
        "void" | "c_void" | "()" => "pointer".to_string(),

        // Booleans — C `bool` (stdbool) and Nim `bool` are both 1 byte.
        "bool" | "GLboolean" => "bool".to_string(),

        // Signed / unsigned integers via explicit-width Nim types.
        "i8" | "c_char" | "char" => "int8".to_string(),
        "u8" | "c_uchar" => "uint8".to_string(),
        "i16" => "int16".to_string(),
        "u16" => "uint16".to_string(),
        "i32" | "GLint" | "GLsizei" => "int32".to_string(),
        "c_int" => "cint".to_string(),
        "u32" | "GLuint" | "GLenum" | "GLbitfield" => "uint32".to_string(),
        "c_uint" => "cuint".to_string(),
        "i64" | "GLint64" => "int64".to_string(),
        "u64" | "GLuint64" => "uint64".to_string(),
        "f32" | "GLfloat" | "GLclampf" => "float32".to_string(),
        "f64" | "GLdouble" | "GLclampd" => "float64".to_string(),
        // `csize_t` is Nim's unsigned pointer-width type (== C size_t).
        "usize" | "size_t" | "uintptr_t" => "csize_t".to_string(),
        // Nim `int` is signed pointer-width (== isize / intptr_t).
        "isize" | "ssize_t" | "intptr_t" | "GLsizeiptr" | "GLintptr" => "int".to_string(),

        _ => {
            if ir.find_struct(trimmed).is_some()
                || ir.find_enum(trimmed).is_some()
                || ir.find_type_alias(trimmed).is_some()
                || ir.callback_typedefs.iter().any(|c| c.name == trimmed)
            {
                ffi_type_name(trimmed)
            } else {
                // Unknown — opaque void-pointer.
                "pointer".to_string()
            }
        }
    }
}

/// Pointer mapping helper: `<inner>` (after `*const`/`*mut`/`&`/`&mut`)
/// -> Nim type token.
pub fn ptr_type_for(inner: &str, ir: &CodegenIR) -> String {
    let inner = inner.trim();
    match inner {
        "c_char" | "char" => "cstring".to_string(),
        "i8" | "u8" | "c_uchar" => "ptr uint8".to_string(),
        "c_void" | "void" | "()" => "pointer".to_string(),
        _ => {
            if ir.find_struct(inner).is_some()
                || ir.find_enum(inner).is_some()
                || ir.find_type_alias(inner).is_some()
                || ir.callback_typedefs.iter().any(|c| c.name == inner)
            {
                format!("ptr {}", ffi_type_name(inner))
            } else {
                "pointer".to_string()
            }
        }
    }
}

/// Nim reserved keywords (the ones that can collide with api.json field /
/// argument / method names). Nim is *style-insensitive* (case- and
/// underscore-insensitive after the first character) and its keywords are
/// lower-case, so we compare against the collapsed lower-case form.
pub fn is_nim_keyword(name: &str) -> bool {
    let lower = name.to_ascii_lowercase();
    matches!(
        lower.as_str(),
        "addr"
            | "and"
            | "as"
            | "asm"
            | "bind"
            | "block"
            | "break"
            | "case"
            | "cast"
            | "concept"
            | "const"
            | "continue"
            | "converter"
            | "defer"
            | "discard"
            | "distinct"
            | "div"
            | "do"
            | "elif"
            | "else"
            | "end"
            | "enum"
            | "except"
            | "export"
            | "finally"
            | "for"
            | "from"
            | "func"
            | "if"
            | "import"
            | "in"
            | "include"
            | "interface"
            | "is"
            | "isnot"
            | "iterator"
            | "let"
            | "macro"
            | "method"
            | "mixin"
            | "mod"
            | "nil"
            | "not"
            | "notin"
            | "object"
            | "of"
            | "or"
            | "out"
            | "proc"
            | "ptr"
            | "raise"
            | "ref"
            | "return"
            | "shl"
            | "shr"
            | "static"
            | "template"
            | "try"
            | "tuple"
            | "type"
            | "using"
            | "var"
            | "when"
            | "while"
            | "xor"
            | "yield"
            // Not keywords, but magic / built-in identifiers that are unsafe
            // as user-declared names.
            | "result"
            | "true"
            | "false"
    )
}

/// Sanitize an identifier for use as a Nim field / argument / proc name.
/// Reserved words are wrapped in backticks (Nim "stropping"), which keeps
/// the original spelling usable as an ordinary identifier.
pub fn sanitize_identifier(name: &str) -> String {
    // Nim identifiers must start with a letter — a leading underscore (e.g. the
    // C filler field `_reserved`) is a hard lexer error ("invalid token: _"),
    // and a bare `_` is illegal too. Strip leading underscores; these are Nim
    // `{.bycopy.} = object` fields whose layout is positional, so the rename is
    // ABI-safe. Fall back to `field` if nothing is left.
    // Nim rejects leading ("invalid token: _") AND trailing ("trailing
    // underscore") underscores, plus a bare `_`. Trim both ends (e.g. the C arg
    // `ref_` -> `ref`, which then keyword-escapes to `` `ref` `` below).
    let stripped = name.trim_matches('_');
    let base = if stripped.is_empty() { "field" } else { stripped };
    // Positional/tuple fields arrive as bare indices ("0", "1", ...); Nim needs
    // an identifier, not a number ("identifier expected, but got '0'"). Prefix a
    // leading digit (accessor is cosmetic; `{.bycopy.}` layout is positional).
    let base: String = if base.chars().next().map(|c| c.is_ascii_digit()).unwrap_or(false) {
        format!("f{}", base)
    } else {
        base.to_string()
    };
    if is_nim_keyword(&base) {
        format!("`{}`", base)
    } else {
        base
    }
}

/// Convert a snake_case / camelCase name to lowerCamelCase (idiomatic for
/// Nim proc names).
pub fn to_lower_camel(s: &str) -> String {
    let pascal = to_pascal_case(s);
    let mut chars = pascal.chars();
    match chars.next() {
        Some(c) => c.to_lowercase().collect::<String>() + chars.as_str(),
        None => String::new(),
    }
}

/// Convert a snake_case / camelCase name to PascalCase.
pub fn to_pascal_case(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    let mut upper_next = true;
    for c in s.chars() {
        if c == '_' {
            upper_next = true;
        } else if upper_next {
            out.extend(c.to_uppercase());
            upper_next = false;
        } else {
            out.push(c);
        }
    }
    out
}

/// Collapse an identifier to the form Nim compares for equality: the first
/// character verbatim, the rest lower-cased with underscores removed. Two
/// spellings that collapse to the same string are the *same* identifier to
/// Nim (e.g. `getRawImage` and `getRawimage`, or `AzShape_Ellipse` and
/// `AzShapeEllipse`).
pub fn nim_normalize_ident(name: &str) -> String {
    let mut chars = name.chars();
    match chars.next() {
        None => String::new(),
        Some(first) => {
            let rest: String = chars
                .filter(|c| *c != '_')
                .flat_map(|c| c.to_lowercase())
                .collect();
            format!("{}{}", first, rest)
        }
    }
}

/// De-duplicates emitted proc names under Nim's style-insensitivity.
///
/// A `proc` may share a *name* with another proc as long as their parameter
/// types differ — that is an ordinary Nim overload (the wrapper layer leans
/// on this to reuse method names like `dom` across `self` types). Only a
/// genuine *redefinition* — same normalized name AND same parameter-type
/// list — is illegal. This tracks `(normalized_name, param_types)` pairs and
/// hands back a numerically-suffixed name whenever a true redefinition would
/// occur, so both real C symbols (e.g. `AzImageRef_getRawImage` and
/// `AzImageRef_getRawimage`, which differ only by case) stay callable. The
/// raw layer keeps the exact C symbol in its `importc` string regardless, so
/// only the Nim-visible alias of the *second* colliding proc changes.
pub struct ProcDedup {
    seen: HashSet<String>,
    /// Normalized names of emitted *types*. A type occupies its identifier
    /// for every signature, so any proc that normalizes to one of these must
    /// be renamed (Nim forbids a routine sharing a name with a type).
    type_names: HashSet<String>,
    /// Maps a raw external's true C symbol to the (possibly renamed)
    /// Nim-visible proc name, so the wrapper layer forwards to the exact
    /// proc it means rather than silently binding to the first same-named
    /// overload.
    externals: HashMap<String, String>,
}

impl ProcDedup {
    pub fn new() -> Self {
        ProcDedup {
            seen: HashSet::new(),
            type_names: HashSet::new(),
            externals: HashMap::new(),
        }
    }

    /// Reserve an emitted type identifier so no proc may share its name.
    pub fn reserve_type(&mut self, name: &str) {
        self.type_names.insert(nim_normalize_ident(name));
    }

    /// Return a Nim-unique spelling for `name` given its `param_types`
    /// signature (a stable, order-preserving join of the parameter type
    /// tokens). Usually returns `name` unchanged; on a true redefinition it
    /// appends `_2`, `_3`, … until the pair is free.
    pub fn unique(&mut self, name: &str, param_types: &str) -> String {
        let mut candidate = name.to_string();
        let mut n = 1;
        loop {
            let norm = nim_normalize_ident(&candidate);
            // A candidate is free only if no *type* claims its normalized
            // name and no already-emitted proc shares the (name, signature).
            if !self.type_names.contains(&norm)
                && self.seen.insert(format!("{}|{}", norm, param_types))
            {
                return candidate;
            }
            n += 1;
            candidate = format!("{}_{}", name, n);
        }
    }

    /// Like [`unique`], but for a raw external: also records the mapping from
    /// the true C symbol to the emitted proc name so wrappers can look it up.
    pub fn unique_external(&mut self, c_name: &str, param_types: &str) -> String {
        let name = self.unique(c_name, param_types);
        self.externals.insert(c_name.to_string(), name.clone());
        name
    }

    /// The Nim-visible name a raw external's C symbol was emitted under.
    /// Falls back to the symbol itself (externals are always emitted before
    /// wrappers, so a hit is expected for any wrapped function).
    pub fn external_name(&self, c_name: &str) -> String {
        self.externals
            .get(c_name)
            .cloned()
            .unwrap_or_else(|| c_name.to_string())
    }
}

/// Sanitize a doc comment so it sits safely inside a Nim `#` line
/// comment. Newlines collapse to spaces.
pub fn sanitize_comment(s: &str) -> String {
    s.replace('\n', " ").replace('\r', " ")
}
