/// Base library name. Nim's `dynlib` const resolves the platform file
/// name (`azul.dll` / `libazul.so` / `libazul.dylib`) at run time.
pub const LIB_NAME: &str = "azul";

/// Public entry point. Produces the full `azul.nim` module source.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    let mut builder = CodeBuilder::new("  "); // Nim idiom: 2-space indent.

    emit_header(&mut builder);
    emit_dynlib_const(&mut builder);

    // Single `type` section — order-independent inside the block.
    builder.line("type");
    builder.indent();
    types::generate_types(&mut builder, ir, config)?;
    builder.dedent();
    builder.blank();

    // Shared across the raw layer and the wrappers: both live in the one
    // module scope, so a proc name emitted by either must be unique under
    // Nim's style-insensitive identifier rules — and must not collide with
    // any type identifier emitted above.
    let mut procs = ProcDedup::new();
    for name in types::collect_type_names(ir, config) {
        procs.reserve_type(&name);
    }

    // Raw C-ABI `importc` proc declarations.
    functions::generate_externals(&mut builder, ir, config, &mut procs)?;
    builder.blank();

    // Idiomatic wrappers (drop the `Az` prefix).
    wrappers::generate_wrappers(&mut builder, ir, config, &mut procs)?;

    Ok(builder.finish())
}

fn emit_header(b: &mut CodeBuilder) {
    b.line("# ============================================================================");
    b.line("# azul.nim — Nim bindings for the Azul GUI framework");
    b.line("# Auto-generated by azul-doc codegen v2 (lang_nim). DO NOT EDIT MANUALLY.");
    b.line("# ============================================================================");
    b.line("#");
    b.line("# Nim compiles to C and speaks the C ABI natively:");
    b.line("#   * C structs  -> `{.bycopy.} object`  (plain objects have no hidden");
    b.line("#     RTTI field, so the layout matches Rust's #[repr(C)] one-for-one)");
    b.line("#   * C unions   -> `{.union.} object`");
    b.line("#   * C enums    -> size-pinned `{.pure.}` Nim enums (`{.pure, size: 4.}`);");
    b.line("#     members are scoped, so reference them as `AzUpdate.RefreshDom`");
    b.line("#     (NOT `AzUpdate_RefreshDom`). `{.pure.}` avoids Nim's style-");
    b.line("#     insensitive clash between `AzShape_Ellipse` and struct `AzShapeEllipse`.");
    b.line("#   * functions  -> `{.importc, cdecl, dynlib: azulLib.}` procs");
    b.line("#");
    b.line("# A top-level `proc (...) {.cdecl.}` is a real C function pointer, so");
    b.line("# callbacks pass straight to e.g. `AzButton_setOnClick` — no trampoline,");
    b.line("# no host-invoker, no handle table.");
    b.line("#");
    b.line("# The `Az`-prefixed procs below are the raw C-ABI layer. Idiomatic");
    b.line("# non-prefixed wrappers (dropping `Az`) follow at the bottom of the file.");
    b.line("# ============================================================================");
    b.blank();
}

/// Per-OS shared-library name for the `dynlib` pragma. Nim dlopens this
/// at run time, so no `--passL` link flag is strictly required (the
/// library only has to be discoverable, e.g. via `LD_LIBRARY_PATH=.`).
fn emit_dynlib_const(b: &mut CodeBuilder) {
    b.line("when defined(windows):");
    b.line("  const azulLib* = \"azul.dll\"");
    b.line("elif defined(macosx):");
    b.line("  const azulLib* = \"libazul.dylib\"");
    b.line("else:");
    b.line("  const azulLib* = \"libazul.so\"");
    b.blank();
}

