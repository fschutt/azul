//! Nim binding generator.
//!
//! Emits a single `azul.nim` module that exposes the prebuilt
//! `azul.dll` / `libazul.so` / `libazul.dylib` to Nim programs.
//!
//! # Why Nim is C-ABI-direct (archetype A, like Zig / Odin / C)
//!
//! Nim compiles to C and speaks the C ABI natively:
//!
//! - C structs map to `{.bycopy.} object` records whose fields match the
//!   Rust `#[repr(C)]` layout one-for-one. A plain (non-`ref`) `object`
//!   carries no hidden RTTI field, so its layout *is* the C struct
//!   layout; `{.bycopy.}` forces it to be passed by value across the FFI
//!   boundary, exactly like a C struct argument.
//! - C unions map to `{.union.} object`.
//! - C `enum`s map to size-pinned Nim enums (`{.size: 4.}` — a C `enum`
//!   is `int`-wide; tagged-union *tags* are a separate `uint8` field,
//!   modelled faithfully below).
//! - Every `AzFoo_bar` C function is imported with
//!   `{.importc: "AzFoo_bar", cdecl, dynlib: azulLib.}` — the `importc`
//!   string carries the real symbol so Nim's identifier
//!   style-insensitivity never mangles it, and `dynlib` dlopens the
//!   shared library at run time (no link flags required).
//! - CRUCIALLY: a top-level `proc (data: AzRefAny; info: AzCallbackInfo):
//!   AzUpdate {.cdecl.}` is a *real* C function pointer. It is passed
//!   straight to `AzButton_setOnClick` with no trampoline, host-invoker,
//!   or handle table. Nim callbacks pass through directly.
//!
//! # Output layout
//!
//! 1. A banner + the per-OS `azulLib` dynlib constant.
//! 2. One big `type` section (Nim resolves forward references within a
//!    single `type` block, so declaration order is irrelevant): enums,
//!    `{.cdecl.}` proc-type callback typedefs, POD `object` records, and
//!    tagged unions modelled as per-variant `{tag: uint8, payload…}`
//!    structs grouped under a `{.union.} object`.
//! 3. `proc AzFoo_bar*(...) {.importc, cdecl, dynlib.}` declarations for
//!    every C-ABI function (the raw layer — this is the path the example
//!    and the e2e test exercise, mirroring Zig's `azul.C.*`).
//! 4. Idiomatic non-`Az`-prefixed wrapper procs (`domCreateBody()`,
//!    `button.setOnClick(...)`) that forward to the raw layer.
//!
//! # Wiring
//!
//! Like the other Wave-2 generators (FreeBASIC / Pascal / Ada / Zig …)
//! this module is threaded in centrally by `generator.rs`; the entry
//! point is [`generate`].

use anyhow::Result;

use super::config::CodegenConfig;
use super::generator::CodeBuilder;
use super::ir::CodegenIR;

pub mod functions;
pub mod types;
pub mod wrappers;

/// Base library name. Nim's `dynlib` const resolves the platform file
/// name (`azul.dll` / `libazul.so` / `libazul.dylib`) at run time.
pub const LIB_NAME: &str = "azul";

/// Public entry point. Produces the full `azul.nim` module source.
pub fn generate(ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
    let mut builder = CodeBuilder::new("  "); // Nim idiom: 2-space indent.

    emit_header(&mut builder);
    emit_dynlib_const(&mut builder);

    // Single `type` section — order-independent inside the block.
    builder.line("type");
    builder.indent();
    types::generate_types(&mut builder, ir, config)?;
    builder.dedent();
    builder.blank();

    // Raw C-ABI `importc` proc declarations.
    functions::generate_externals(&mut builder, ir, config)?;
    builder.blank();

    // Idiomatic wrappers (drop the `Az` prefix).
    wrappers::generate_wrappers(&mut builder, ir, config)?;

    Ok(builder.finish())
}

fn emit_header(b: &mut CodeBuilder) {
    b.line("# ============================================================================");
    b.line("# azul.nim — Nim bindings for the Azul GUI framework");
    b.line("# Auto-generated by azul-doc codegen v2 (lang_nim). DO NOT EDIT MANUALLY.");
    b.line("# ============================================================================");
    b.line("#");
    b.line("# Nim compiles to C and speaks the C ABI natively:");
    b.line("#   * C structs  -> `{.bycopy.} object`  (plain objects have no hidden");
    b.line("#     RTTI field, so the layout matches Rust's #[repr(C)] one-for-one)");
    b.line("#   * C unions   -> `{.union.} object`");
    b.line("#   * C enums    -> size-pinned Nim enums (`{.size: 4.}`)");
    b.line("#   * functions  -> `{.importc, cdecl, dynlib: azulLib.}` procs");
    b.line("#");
    b.line("# A top-level `proc (...) {.cdecl.}` is a real C function pointer, so");
    b.line("# callbacks pass straight to e.g. `AzButton_setOnClick` — no trampoline,");
    b.line("# no host-invoker, no handle table.");
    b.line("#");
    b.line("# The `Az`-prefixed procs below are the raw C-ABI layer. Idiomatic");
    b.line("# non-prefixed wrappers (dropping `Az`) follow at the bottom of the file.");
    b.line("# ============================================================================");
    b.blank();
}

/// Per-OS shared-library name for the `dynlib` pragma. Nim dlopens this
/// at run time, so no `--passL` link flag is strictly required (the
/// library only has to be discoverable, e.g. via `LD_LIBRARY_PATH=.`).
fn emit_dynlib_const(b: &mut CodeBuilder) {
    b.line("when defined(windows):");
    b.line("  const azulLib* = \"azul.dll\"");
    b.line("elif defined(macosx):");
    b.line("  const azulLib* = \"libazul.dylib\"");
    b.line("else:");
    b.line("  const azulLib* = \"libazul.so\"");
    b.blank();
}

// ============================================================================
// Shared type-name / type-mapping helpers (used by submodules)
// ============================================================================

/// The `Az`-prefixed FFI type name for an IR type (e.g. `Dom` -> `AzDom`).
pub fn ffi_type_name(name: &str) -> String {
    format!("Az{}", name)
}

/// Map an IR / Rust type name to the corresponding Nim type token.
///
/// Pointers / references collapse to `ptr AzT` (typed) — `cstring` for
/// `c_char` strings, `ptr uint8` for byte buffers, and `pointer`
/// (Nim's `void*`) for opaque / unknown targets. Primitives map to
/// explicit-width Nim types so the binding is correct on 32- and 64-bit
/// hosts.
pub fn map_type_to_nim(rust_type: &str, ir: &CodegenIR) -> String {
    let trimmed = rust_type.trim();

    if let Some(rest) = trimmed.strip_prefix("*const ") {
        return ptr_type_for(rest, ir);
    }
    if let Some(rest) = trimmed.strip_prefix("*mut ") {
        return ptr_type_for(rest, ir);
    }
    if let Some(rest) = trimmed.strip_prefix("&mut ") {
        return ptr_type_for(rest, ir);
    }
    if let Some(rest) = trimmed.strip_prefix('&') {
        return ptr_type_for(rest, ir);
    }

    // Fixed-size arrays: `[T; N]` -> `array[N, <elem>]`.
    if trimmed.starts_with('[') && trimmed.ends_with(']') {
        let inner = &trimmed[1..trimmed.len() - 1];
        if let Some(semi) = inner.rfind(';') {
            let elem = inner[..semi].trim();
            if let Ok(count) = inner[semi + 1..].trim().parse::<usize>() {
                let nim_elem = map_type_to_nim(elem, ir);
                return format!("array[{}, {}]", count, nim_elem);
            }
        }
    }

    match trimmed {
        // Void / unit — only reachable in value position (e.g. an opaque
        // `*mut c_void` field already handled above); model as `pointer`.
        "void" | "c_void" | "()" => "pointer".to_string(),

        // Booleans — C `bool` (stdbool) and Nim `bool` are both 1 byte.
        "bool" | "GLboolean" => "bool".to_string(),

        // Signed / unsigned integers via explicit-width Nim types.
        "i8" | "c_char" | "char" => "int8".to_string(),
        "u8" | "c_uchar" => "uint8".to_string(),
        "i16" => "int16".to_string(),
        "u16" => "uint16".to_string(),
        "i32" | "GLint" | "GLsizei" => "int32".to_string(),
        "c_int" => "cint".to_string(),
        "u32" | "GLuint" | "GLenum" | "GLbitfield" => "uint32".to_string(),
        "c_uint" => "cuint".to_string(),
        "i64" | "GLint64" => "int64".to_string(),
        "u64" | "GLuint64" => "uint64".to_string(),
        "f32" | "GLfloat" | "GLclampf" => "float32".to_string(),
        "f64" | "GLdouble" | "GLclampd" => "float64".to_string(),
        // `csize_t` is Nim's unsigned pointer-width type (== C size_t).
        "usize" | "size_t" | "uintptr_t" => "csize_t".to_string(),
        // Nim `int` is signed pointer-width (== isize / intptr_t).
        "isize" | "ssize_t" | "intptr_t" | "GLsizeiptr" | "GLintptr" => "int".to_string(),

        _ => {
            if ir.find_struct(trimmed).is_some()
                || ir.find_enum(trimmed).is_some()
                || ir.find_type_alias(trimmed).is_some()
                || ir.callback_typedefs.iter().any(|c| c.name == trimmed)
            {
                ffi_type_name(trimmed)
            } else {
                // Unknown — opaque void-pointer.
                "pointer".to_string()
            }
        }
    }
}

/// Pointer mapping helper: `<inner>` (after `*const`/`*mut`/`&`/`&mut`)
/// -> Nim type token.
pub fn ptr_type_for(inner: &str, ir: &CodegenIR) -> String {
    let inner = inner.trim();
    match inner {
        "c_char" | "char" => "cstring".to_string(),
        "i8" | "u8" | "c_uchar" => "ptr uint8".to_string(),
        "c_void" | "void" | "()" => "pointer".to_string(),
        _ => {
            if ir.find_struct(inner).is_some()
                || ir.find_enum(inner).is_some()
                || ir.find_type_alias(inner).is_some()
                || ir.callback_typedefs.iter().any(|c| c.name == inner)
            {
                format!("ptr {}", ffi_type_name(inner))
            } else {
                "pointer".to_string()
            }
        }
    }
}

/// Nim reserved keywords (the ones that can collide with api.json field /
/// argument / method names). Nim is *style-insensitive* (case- and
/// underscore-insensitive after the first character) and its keywords are
/// lower-case, so we compare against the collapsed lower-case form.
pub fn is_nim_keyword(name: &str) -> bool {
    let lower = name.to_ascii_lowercase();
    matches!(
        lower.as_str(),
        "addr"
            | "and"
            | "as"
            | "asm"
            | "bind"
            | "block"
            | "break"
            | "case"
            | "cast"
            | "concept"
            | "const"
            | "continue"
            | "converter"
            | "defer"
            | "discard"
            | "distinct"
            | "div"
            | "do"
            | "elif"
            | "else"
            | "end"
            | "enum"
            | "except"
            | "export"
            | "finally"
            | "for"
            | "from"
            | "func"
            | "if"
            | "import"
            | "in"
            | "include"
            | "interface"
            | "is"
            | "isnot"
            | "iterator"
            | "let"
            | "macro"
            | "method"
            | "mixin"
            | "mod"
            | "nil"
            | "not"
            | "notin"
            | "object"
            | "of"
            | "or"
            | "out"
            | "proc"
            | "ptr"
            | "raise"
            | "ref"
            | "return"
            | "shl"
            | "shr"
            | "static"
            | "template"
            | "try"
            | "tuple"
            | "type"
            | "using"
            | "var"
            | "when"
            | "while"
            | "xor"
            | "yield"
            // Not keywords, but magic / built-in identifiers that are unsafe
            // as user-declared names.
            | "result"
            | "true"
            | "false"
    )
}

/// Sanitize an identifier for use as a Nim field / argument / proc name.
/// Reserved words are wrapped in backticks (Nim "stropping"), which keeps
/// the original spelling usable as an ordinary identifier.
pub fn sanitize_identifier(name: &str) -> String {
    if is_nim_keyword(name) {
        format!("`{}`", name)
    } else {
        name.to_string()
    }
}

/// Convert a snake_case / camelCase name to lowerCamelCase (idiomatic for
/// Nim proc names).
pub fn to_lower_camel(s: &str) -> String {
    let pascal = to_pascal_case(s);
    let mut chars = pascal.chars();
    match chars.next() {
        Some(c) => c.to_lowercase().collect::<String>() + chars.as_str(),
        None => String::new(),
    }
}

/// Convert a snake_case / camelCase name to PascalCase.
pub fn to_pascal_case(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    let mut upper_next = true;
    for c in s.chars() {
        if c == '_' {
            upper_next = true;
        } else if upper_next {
            out.extend(c.to_uppercase());
            upper_next = false;
        } else {
            out.push(c);
        }
    }
    out
}

/// Sanitize a doc comment so it sits safely inside a Nim `#` line
/// comment. Newlines collapse to spaces.
pub fn sanitize_comment(s: &str) -> String {
    s.replace('\n', " ").replace('\r', " ")
}
