//! Emits the Dune build manifest for the generated OCaml bindings.
//!
//! Two files are produced:
//!
//! - `dune-project` — declares the Dune lang version. Every Dune
//!   workspace needs exactly one of these at its root.
//! - `dune` — the per-directory build description. We declare a
//!   library `azul` that compiles the generated `azul.ml` /
//!   `azul.mli` against `ctypes` and `ctypes-foreign` and links
//!   against the prebuilt native library at runtime via `Dl.dlopen`.
//!
//! The user is expected to drop the platform-appropriate native
//! binary (`libazul.so` / `libazul.dylib` / `azul.dll`) somewhere
//! discoverable by `dlopen` (typically the working directory or
//! `LD_LIBRARY_PATH` / `DYLD_LIBRARY_PATH`). We don't link the
//! native at OCaml link-time because Ctypes-Foreign is a libffi
//! consumer — the binding is loaded dynamically.

/// Single deterministic `dune-project` string.
pub fn generate_dune_project() -> String {
    // Dune 3.x requires `(lang dune ...)` to be the very first
    // non-blank line — comments before it are rejected with
    //   "Invalid first line, expected: (lang <lang> <version>)".
    // Header comments live below the lang stanza.
    let s = r#"(lang dune 3.0)
(name azul)
; ============================================================================
; Auto-generated Dune project file for the Azul GUI framework OCaml bindings.
; Generated by azul-doc codegen v2 (lang_ocaml/dune). DO NOT EDIT MANUALLY.
; ============================================================================
"#;
    s.to_string()
}

/// Single deterministic per-library `dune` file.
///
/// Deliberately NO `(public_name azul)`: a public name requires an
/// `azul.opam` file in the workspace, and we don't ship one — with it,
/// `dune build` hard-errors ("You cannot declare items to be installed
/// without adding a <package>.opam file") before compiling anything.
pub fn generate_dune() -> String {
    let s = r#"; ============================================================================
; Auto-generated Dune build manifest for the Azul OCaml bindings library.
; Generated by azul-doc codegen v2 (lang_ocaml/dune). DO NOT EDIT MANUALLY.
;
; Build with:
;     dune build
;
; The native libazul.so / libazul.dylib / azul.dll must be discoverable by
; dlopen at runtime (e.g. in LD_LIBRARY_PATH, DYLD_LIBRARY_PATH, or the
; current working directory).
; ============================================================================

; The generated azul bindings (azul.ml + azul.mli) defined as a local library.
; The codegen emits many declarations that are unused by any given consumer;
; that's fine for an FFI-binding library. Suppress warning 32 (unused
; value declaration) so the build doesn't fail on a stable, complete API.
(library
 (name azul)
 (flags (:standard -w -32-26-27-33-34-35-37-39))
 (libraries ctypes ctypes-foreign)
 (modules azul))

; Uncomment to build the hello-world example from the guide
; (save the snippet as hello_world.ml next to this file):
;
; (executable
;  (name hello_world)
;  (modules hello_world)
;  (libraries ctypes ctypes-foreign azul))
"#;
    s.to_string()
}
