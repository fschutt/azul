impl RustGenerator {
    fn generate_capi_trait_impls(
        &self,
        builder: &mut CodeBuilder,
        struct_def: &StructDef,
        config: &CodegenConfig,
    ) {
        // Skip generic types — only concrete instantiations get C-API impls
        if !struct_def.generic_params.is_empty() {
            return;
        }

        let name = config.apply_prefix(&struct_def.name);

        // Clone impl calling C-ABI function
        if struct_def.traits.is_clone && !struct_def.traits.is_copy {
            let deep_copy_fn = format!("{}_clone", name);
            builder.line(&format!("impl Clone for {} {{", name));
            builder.indent();
            builder.line("fn clone(&self) -> Self {");
            builder.indent();
            builder.line(&format!("unsafe {{ {}(self) }}", deep_copy_fn));
            builder.dedent();
            builder.line("}");
            builder.dedent();
            builder.line("}");
            builder.blank();
        }

        // Drop impl calling C-ABI function
        if struct_def.traits.needs_delete() {
            let delete_fn = format!("{}_delete", name);
            builder.line(&format!("impl Drop for {} {{", name));
            builder.indent();
            builder.line("fn drop(&mut self) {");
            builder.indent();
            builder.line(&format!("unsafe {{ {}(self) }}", delete_fn));
            builder.dedent();
            builder.line("}");
            builder.dedent();
            builder.line("}");
            builder.blank();
        }
    }

    fn generate_capi_trait_impls_enum(
        &self,
        builder: &mut CodeBuilder,
        enum_def: &EnumDef,
        config: &CodegenConfig,
    ) {
        // Skip generic types — only concrete instantiations get C-API impls
        if !enum_def.generic_params.is_empty() {
            return;
        }

        let name = config.apply_prefix(&enum_def.name);

        // Clone impl calling C-ABI function
        if enum_def.traits.is_clone && !enum_def.traits.is_copy {
            let deep_copy_fn = format!("{}_clone", name);
            builder.line(&format!("impl Clone for {} {{", name));
            builder.indent();
            builder.line("fn clone(&self) -> Self {");
            builder.indent();
            builder.line(&format!("unsafe {{ {}(self) }}", deep_copy_fn));
            builder.dedent();
            builder.line("}");
            builder.dedent();
            builder.line("}");
            builder.blank();
        }

        // Drop impl calling C-ABI function
        if enum_def.traits.needs_delete() {
            let delete_fn = format!("{}_delete", name);
            builder.line(&format!("impl Drop for {} {{", name));
            builder.indent();
            builder.line("fn drop(&mut self) {");
            builder.indent();
            builder.line(&format!("unsafe {{ {}(self) }}", delete_fn));
            builder.dedent();
            builder.line("}");
            builder.dedent();
            builder.line("}");
            builder.blank();
        }
    }

    /// Generate inline Clone/Drop for generic enums like `BoxOrStatic<T>`.
    ///
    /// These can't use extern C-API functions (no single fn for all T) and
    /// Rust forbids specializing Drop on type aliases.  Instead, we generate
    /// the implementations inline based on the enum's structure.
    fn generate_generic_inline_impls_enum(
        &self,
        builder: &mut CodeBuilder,
        enum_def: &EnumDef,
        config: &CodegenConfig,
    ) {
        // Only needed for non-Copy generic types with custom Clone/Drop
        if enum_def.traits.is_copy {
            return;
        }

        let name = config.apply_prefix(&enum_def.name);
        let generics = format!("<{}>", enum_def.generic_params.join(", "));
        let full_name = format!("{}{}", name, generics);

        // Check if this is a BoxOrStatic-style enum (Boxed(*mut T), Static(*const T))
        let is_box_or_static = enum_def.variants.iter().any(|v| v.name == "Boxed")
            && enum_def.variants.iter().any(|v| v.name == "Static");

        if is_box_or_static {
            // Clone: Boxed → heap-copy, Static → copy pointer
            if enum_def.traits.is_clone {
                builder.line(&format!("impl<T: Clone> Clone for {} {{", full_name));
                builder.indent();
                builder.line("fn clone(&self) -> Self {");
                builder.indent();
                builder.line("match self {");
                builder.indent();
                builder.line(&format!("{}::Boxed(ptr) => unsafe {{", name));
                builder.indent();
                builder.line("let cloned = Box::new((**ptr).clone());");
                builder.line(&format!("{}::Boxed(Box::into_raw(cloned))", name));
                builder.dedent();
                builder.line("}");
                builder.line(&format!("{}::Static(ptr) => {}::Static(*ptr),", name, name));
                builder.dedent();
                builder.line("}");
                builder.dedent();
                builder.line("}");
                builder.dedent();
                builder.line("}");
                builder.blank();
            }

            // Drop: Boxed → free, Static → no-op.
            //
            // MUST mirror azul_css::css::BoxOrStatic::drop exactly: skip null and
            // null-out after freeing. Container mirrors (e.g. AzNodeData) have a
            // manual Drop that deletes through the core types (which frees and
            // NULLS the pointer in place); Rust's drop glue then recursively
            // drops the mirror fields, re-entering this Drop on the same memory.
            // Without the null check that second pass is a double-free abort.
            if enum_def.traits.has_custom_drop || !enum_def.traits.is_copy {
                builder.line(&format!("impl{} Drop for {} {{", generics, full_name));
                builder.indent();
                builder.line("fn drop(&mut self) {");
                builder.indent();
                builder.line("match self {");
                builder.indent();
                builder.line(&format!("{}::Boxed(ptr) => if !ptr.is_null() {{ unsafe {{ let _ = Box::from_raw(*ptr); }} *ptr = core::ptr::null_mut(); }},", name));
                builder.line(&format!("{}::Static(_) => {{}},", name));
                builder.dedent();
                builder.line("}");
                builder.dedent();
                builder.line("}");
                builder.dedent();
                builder.line("}");
                builder.blank();
            }
        }
        // Other generic types that are Copy (CssPropertyValue, PhysicalSize, etc.)
        // don't need Clone/Drop impls — they derive them.
    }
}

