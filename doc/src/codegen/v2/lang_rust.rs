impl RustGenerator {
    fn generate_function_definition(
        &self,
        builder: &mut CodeBuilder,
        func: &FunctionDef,
        ir: &CodegenIR,
        config: &CodegenConfig,
        export_feature: &str,
        is_export_only: bool,
    ) {
        // Trait functions only exist when cabi_export is enabled
        if is_export_only {
            builder.line(&format!("#[cfg(feature = \"{export_feature}\")]"));
        }
        // Attributes
        builder.line("#[allow(unused_variables)]");
        // Gate #[no_mangle] behind the export feature flag
        builder.line(&format!(
            "#[cfg_attr(feature = \"{export_feature}\", no_mangle)]"
        ));

        // Apply callback wrapper substitution for API functions (Constructor, Method, etc.)
        // NOT for trait functions (Delete, DeepCopy, etc.) which operate on the callback wrapper itself
        // NOT for EnumVariantConstructor - enum variants like OptionCallback::Some need exact types
        // for easier C code generation
        let should_substitute_callbacks = matches!(
            func.kind,
            FunctionKind::Constructor
                | FunctionKind::StaticMethod
                | FunctionKind::Method
                | FunctionKind::MethodMut
        );

        // M2.5 pair-pattern detection: every function with a
        // callback-wrapper arg (per `HOST_INVOKER_KINDS`) emits two
        // C-ABI variants — raw `_setOnX(fn_ptr)` and
        // `_setOnXWithCtx(fn_ptr, refany)`. See
        // `scripts/WEB_BACKEND_PLAN_2026_05_18.md` M2.5 for the design
        // and `managed_host_invoker::is_callback_wrapper`.
        let emit_pair = should_substitute_callbacks && Self::has_callback_wrapper_arg(func);

        let return_str = func
            .return_type
            .as_ref()
            .map(|r| format!(" -> {}", config.apply_prefix(r)))
            .unwrap_or_default();

        if !emit_pair {
            let args = if should_substitute_callbacks {
                self.format_function_args_for_cabi(func, ir, config)
            } else {
                self.format_function_args(func, config)
            };
            let body = self.generate_function_body(func, ir, config);
            builder.line(&format!(
                "pub unsafe extern \"C\" fn {}({}){} {}",
                func.c_name, args, return_str, body
            ));
            return;
        }

        // Pair-pattern emit. Both variants share the same body; the
        // prologue inserted via `inject_prologue_into_body` shadows
        // the fn-ptr arg with a reconstituted callback-wrapper struct
        // so the user's `fn_body` (which expects the wrapper struct)
        // works unchanged.
        let body = self.generate_function_body(func, ir, config);

        // Raw variant: original c_name, fn-ptr args, ctx: None prologue.
        let args_raw = self.format_function_args_for_cabi_pair_raw(func, config);
        let prologue_raw = self.callback_pair_prologue(func, ir, config, false);
        let body_raw = Self::inject_prologue_into_body(&body, &prologue_raw);
        builder.line("// Pair-pattern raw variant: takes the callback fn-ptr only.");
        builder.line("// Use the `WithCtx` sibling below if you need to attach a");
        builder.line("// host-handle refany ctx (e.g. for managed-FFI dispatch).");
        if is_export_only {
            builder.line(&format!("#[cfg(feature = \"{export_feature}\")]"));
        }
        builder.line("#[allow(unused_variables)]");
        builder.line(&format!(
            "#[cfg_attr(feature = \"{export_feature}\", no_mangle)]"
        ));
        builder.line(&format!(
            "pub unsafe extern \"C\" fn {}({}){} {}",
            func.c_name, args_raw, return_str, body_raw
        ));

        // WithCtx variant: c_name + "WithCtx", fn-ptr + ctx args,
        // ctx: Some(arg_ctx) prologue.
        let args_ctx = self.format_function_args_for_cabi_pair_with_ctx(func, config);
        let prologue_ctx = self.callback_pair_prologue(func, ir, config, true);
        let body_ctx = Self::inject_prologue_into_body(&body, &prologue_ctx);
        builder.blank();
        builder.line("// Pair-pattern WithCtx variant: takes the callback fn-ptr +");
        builder.line("// a refany ctx. Managed-FFI hosts (Python/Lua/Ruby/...) pass");
        builder.line("// the host-handle refany from `Az<Wrapper>_createFromHostHandle`");
        builder.line("// so the callback dispatcher can resolve the GC'd target.");
        if is_export_only {
            builder.line(&format!("#[cfg(feature = \"{export_feature}\")]"));
        }
        builder.line("#[allow(unused_variables)]");
        builder.line(&format!(
            "#[cfg_attr(feature = \"{export_feature}\", no_mangle)]"
        ));
        builder.line(&format!(
            "pub unsafe extern \"C\" fn {}WithCtx({}){} {}",
            func.c_name, args_ctx, return_str, body_ctx
        ));
    }

    /// Splice `prologue` (a sequence of `let` statements) just inside
    /// the opening brace of `body`. The body string emitted by
    /// `generate_function_body` is always wrapped in `{ … }`, so the
    /// transform is a textual `{` → `{ <prologue> ` rewrite at the
    /// first character. Empty prologue returns the body unchanged.
    fn inject_prologue_into_body(body: &str, prologue: &str) -> String {
        if prologue.is_empty() {
            return body.to_string();
        }
        let trimmed = body.trim_start();
        if let Some(rest) = trimmed.strip_prefix('{') {
            format!("{{ {}{}", prologue, rest)
        } else {
            // Body wasn't braced (shouldn't happen for the function
            // kinds we emit, but be defensive). Wrap it.
            format!("{{ {}{} }}", prologue, body)
        }
    }

    fn generate_function_body(
        &self,
        func: &FunctionDef,
        ir: &CodegenIR,
        config: &CodegenConfig,
    ) -> String {
        let prefixed_name = config.apply_prefix(&func.class_name);

        // Get external path for this type, transforming if needed
        let external_path = ir
            .type_to_external
            .get(&func.class_name)
            .map(|p| config.transform_external_path(p))
            .unwrap_or_else(|| format!("crate::{}", func.class_name));

        // Generate body based on function kind
        match func.kind {
            FunctionKind::Delete => {
                // For delete, we call drop_in_place to run the destructor.
                // The type is passed by pointer, so we need to dereference and drop it.
                // This works for both stack-allocated (repr(C)) and heap-allocated types.
                let arg_name = func
                    .args
                    .first()
                    .map(|a| a.name.as_str())
                    .unwrap_or("instance");
                format!(
                    "{{ core::ptr::drop_in_place({} as *mut {} as *mut {}); }}",
                    arg_name, prefixed_name, external_path
                )
            }
            FunctionKind::DeepCopy => {
                let arg_name = func
                    .args
                    .first()
                    .map(|a| a.name.as_str())
                    .unwrap_or("object");
                format!(
                    "{{ core::mem::transmute::<{}, {}>((*({}  as *const {} as *const {})).clone()) }}",
                    external_path, prefixed_name, arg_name, prefixed_name, external_path
                )
            }
            FunctionKind::PartialEq => {
                format!(
                    "{{ (*(a as *const {} as *const {})) == (*(b as *const {} as *const {})) }}",
                    prefixed_name, external_path, prefixed_name, external_path
                )
            }
            FunctionKind::PartialCmp => {
                format!(
                    "{{ match (*(a as *const {} as *const {})).partial_cmp(&*(b as *const {} as *const {})) {{
        Some(core::cmp::Ordering::Less) => 0,
        Some(core::cmp::Ordering::Equal) => 1,
        Some(core::cmp::Ordering::Greater) => 2,
        None => 255,
    }} }}",
                    prefixed_name, external_path, prefixed_name, external_path
                )
            }
            FunctionKind::Cmp => {
                format!(
                    "{{ match (*(a as *const {} as *const {})).cmp(&*(b as *const {} as *const {})) {{
        core::cmp::Ordering::Less => 0,
        core::cmp::Ordering::Equal => 1,
        core::cmp::Ordering::Greater => 2,
    }} }}",
                    prefixed_name, external_path, prefixed_name, external_path
                )
            }
            FunctionKind::Hash => {
                let arg_name = func
                    .args
                    .first()
                    .map(|a| a.name.as_str())
                    .unwrap_or("object");
                format!(
                    "{{ {{
        use core::hash::{{Hash, Hasher}};
        let mut hasher = std::collections::hash_map::DefaultHasher::new();
        (*({} as *const {} as *const {})).hash(&mut hasher);
        hasher.finish()
    }} }}",
                    arg_name, prefixed_name, external_path
                )
            }
            FunctionKind::Default => {
                format!(
                    "{{ core::mem::transmute::<{}, {}>({}::default()) }}",
                    external_path, prefixed_name, external_path
                )
            }
            FunctionKind::DebugToString => {
                let arg_name = func
                    .args
                    .first()
                    .map(|a| a.name.as_str())
                    .unwrap_or("instance");
                format!(
                    "{{ let s = format!(\"{{:#?}}\", *({} as *const {} as *const {})); {}String::from(s.as_str()) }}",
                    arg_name, prefixed_name, external_path, config.type_prefix
                )
            }
            FunctionKind::Constructor
            | FunctionKind::StaticMethod
            | FunctionKind::Method
            | FunctionKind::MethodMut
            | FunctionKind::EnumVariantConstructor => {
                // For regular API functions, use the fn_body from api.json
                // and transform it using generate_transmuted_fn_body
                if let Some(ref body) = func.fn_body {
                    // Build the type_to_external map for this function
                    let type_to_external: std::collections::BTreeMap<String, String> = ir
                        .type_to_external
                        .iter()
                        .map(|(k, v)| (config.apply_prefix(k), v.clone()))
                        .collect();

                    // Format fn_args - for EnumVariantConstructor, use the regular version
                    // (no callback wrapper substitution) because enum variants need exact types.
                    // For other functions, use the CABI version which replaces callback wrappers
                    // with their raw fn pointer types.
                    let fn_args = if matches!(func.kind, FunctionKind::EnumVariantConstructor) {
                        self.format_function_args(func, config)
                    } else {
                        self.format_function_args_for_cabi(func, ir, config)
                    };

                    // Determine return type with prefix
                    let return_type = func
                        .return_type
                        .as_ref()
                        .map(|r| config.apply_prefix(r))
                        .unwrap_or_default();

                    // Use the existing transmuted fn_body generator
                    // EnumVariantConstructor counts as a constructor for fn_body transformation
                    let is_constructor = matches!(
                        func.kind,
                        FunctionKind::Constructor
                            | FunctionKind::StaticMethod
                            | FunctionKind::EnumVariantConstructor
                    );
                    generate_transmuted_fn_body(
                        body,
                        &func.class_name,
                        is_constructor,
                        &return_type,
                        &config.type_prefix,
                        &type_to_external,
                        &fn_args,
                        true,             // is_for_dll
                        false,            // keep_self_name
                        false,            // force_clone_self
                        &BTreeSet::new(), // skip_args - empty, we want to transmute all args
                    )
                } else {
                    // No fn_body - generate error or stub
                    format!(
                        "{{ /* ERROR: No fn_body for {} */ unimplemented!() }}",
                        func.c_name
                    )
                }
            }
        }
    }

    fn generate_method_body(
        &self,
        func: &FunctionDef,
        ir: &CodegenIR,
        config: &CodegenConfig,
        external_path: &str,
        prefixed_name: &str,
    ) -> String {
        // Generate transmute calls for each argument
        let mut lines = Vec::new();
        lines.push("{".to_string());

        // Get the self argument name if this is a method
        let self_arg_name = match func.kind {
            FunctionKind::Method | FunctionKind::MethodMut => {
                func.args.first().map(|a| a.name.clone())
            }
            _ => None,
        };

        // Transmute each argument
        for arg in &func.args {
            let ext_type = ir
                .type_to_external
                .get(&arg.type_name)
                .cloned()
                .unwrap_or_else(|| {
                    // For primitive types, just use the type directly
                    if is_primitive_type(&arg.type_name) {
                        arg.type_name.clone()
                    } else {
                        format!("crate::{}", arg.type_name)
                    }
                });

            let transmute_expr = match arg.ref_kind {
                ArgRefKind::Ref => format!(
                    "    let {}: &{} = core::mem::transmute({});",
                    arg.name, ext_type, arg.name
                ),
                ArgRefKind::RefMut => format!(
                    "    let {}: &mut {} = core::mem::transmute({});",
                    arg.name, ext_type, arg.name
                ),
                ArgRefKind::Owned => {
                    if is_primitive_type(&arg.type_name) {
                        format!("    let {}: {} = {};", arg.name, ext_type, arg.name)
                    } else {
                        format!(
                            "    let {}: {} = core::mem::transmute({});",
                            arg.name, ext_type, arg.name
                        )
                    }
                }
                ArgRefKind::Ptr | ArgRefKind::PtrMut => format!(
                    "    let {}: {} = core::mem::transmute({});",
                    arg.name, ext_type, arg.name
                ),
            };
            lines.push(transmute_expr);
        }

        // Generate the call
        let args_for_call: Vec<String> = func
            .args
            .iter()
            .skip(if self_arg_name.is_some() { 1 } else { 0 })
            .map(|a| a.name.clone())
            .collect();

        let call = if let Some(ref self_name) = self_arg_name {
            // Instance method call
            format!(
                "    let __result = {}.{}({});",
                self_name,
                func.method_name,
                args_for_call.join(", ")
            )
        } else {
            // Static method or constructor
            format!(
                "    let __result: {} = {}::{}({});",
                external_path,
                external_path,
                func.method_name,
                args_for_call.join(", ")
            )
        };
        lines.push(call);

        // Transmute result back if needed
        if let Some(ref ret_type) = func.return_type {
            let ret_prefixed = config.apply_prefix(ret_type);
            let ret_external = ir
                .type_to_external
                .get(ret_type)
                .cloned()
                .unwrap_or_else(|| {
                    if is_primitive_type(ret_type) {
                        ret_type.clone()
                    } else {
                        format!("crate::{}", ret_type)
                    }
                });
            if is_primitive_type(ret_type) {
                lines.push("    __result".to_string());
            } else {
                lines.push(format!(
                    "    core::mem::transmute::<{}, {}>(__result)",
                    ret_external, ret_prefixed
                ));
            }
        } else {
            // No return, discard result
            lines.push("    let _ = __result;".to_string());
        }

        lines.push("}".to_string());
        lines.join("\n")
    }

    fn generate_function_declaration(
        &self,
        builder: &mut CodeBuilder,
        func: &FunctionDef,
        ir: &CodegenIR,
        config: &CodegenConfig,
    ) {
        // Use format_function_args_for_cabi for API functions (substitutes callback wrappers with fn pointers)
        // Use format_function_args for EnumVariantConstructor (keeps Callback structs for easier C code)
        let should_substitute_callbacks = matches!(
            func.kind,
            FunctionKind::Constructor
                | FunctionKind::StaticMethod
                | FunctionKind::Method
                | FunctionKind::MethodMut
        );
        let return_str = func
            .return_type
            .as_ref()
            .map(|r| format!(" -> {}", config.apply_prefix(r)))
            .unwrap_or_default();

        // Pair-pattern: a callback-wrapper arg makes the internal bindings emit
        // a raw + `WithCtx` C-ABI pair. The external (link-dynamic) bindings
        // must DECLARE both, otherwise references to `Az*_*WithCtx` fail to
        // resolve (E0425) under `--features link-dynamic`.
        if should_substitute_callbacks && Self::has_callback_wrapper_arg(func) {
            let args_raw = self.format_function_args_for_cabi_pair_raw(func, config);
            builder.line(&format!("pub fn {}({}){};", func.c_name, args_raw, return_str));
            let args_ctx = self.format_function_args_for_cabi_pair_with_ctx(func, config);
            builder.line(&format!(
                "pub fn {}WithCtx({}){};",
                func.c_name, args_ctx, return_str
            ));
            return;
        }

        let args = if should_substitute_callbacks {
            self.format_function_args_for_cabi(func, ir, config)
        } else {
            self.format_function_args(func, config)
        };
        builder.line(&format!("pub fn {}({}){};", func.c_name, args, return_str));
    }

    fn format_function_args(&self, func: &FunctionDef, config: &CodegenConfig) -> String {
        func.args
            .iter()
            .map(|arg| {
                let type_name = config.apply_prefix(&arg.type_name);
                let formatted = match arg.ref_kind {
                    ArgRefKind::Owned => type_name,
                    ArgRefKind::Ref => format!("&{}", type_name),
                    ArgRefKind::RefMut => format!("&mut {}", type_name),
                    ArgRefKind::Ptr => format!("*const {}", type_name),
                    ArgRefKind::PtrMut => format!("*mut {}", type_name),
                };
                format!("{}: {}", arg.name, formatted)
            })
            .collect::<Vec<_>>()
            .join(", ")
    }

    /// Format function arguments for C-ABI functions.
    ///
    /// Respects api.json's declared types verbatim. Historically this
    /// helper rewrote callback wrapper types (e.g. `Callback`) to their
    /// raw function-pointer typedefs (e.g. `CallbackType`) so C consumers
    /// could pass a function pointer and rely on `Into<Callback>` to
    /// wrap. That behaviour is gone — managed-FFI bindings need the
    /// wrapper shape to thread the host-handle ctx through, and api.json
    /// is now expected to declare the C-ABI shape explicitly. C consumers
    /// who want to pass a bare function pointer wrap with
    /// `AzCallback_create(fn)` at the call site, the same way they
    /// already wrap any other constructed value.
    fn format_function_args_for_cabi(
        &self,
        func: &FunctionDef,
        _ir: &CodegenIR,
        config: &CodegenConfig,
    ) -> String {
        let _ = func;
        func.args
            .iter()
            .map(|arg| {
                let type_name = config.apply_prefix(&arg.type_name);
                let formatted = match arg.ref_kind {
                    ArgRefKind::Owned => type_name,
                    ArgRefKind::Ref => format!("&{}", type_name),
                    ArgRefKind::RefMut => format!("&mut {}", type_name),
                    ArgRefKind::Ptr => format!("*const {}", type_name),
                    ArgRefKind::PtrMut => format!("*mut {}", type_name),
                };
                format!("{}: {}", arg.name, formatted)
            })
            .collect::<Vec<_>>()
            .join(", ")
    }

    /// Args list for the pair-pattern raw variant. Every callback-wrapper
    /// arg (`callback: AzCallback`) is replaced with the typedef fn-ptr
    /// form under the same name (`callback: AzCallbackType`). This keeps
    /// the user's `fn_body` working unchanged because the prologue
    /// (emitted in `generate_function_body_with_cb_pair_prologue`) binds
    /// `let callback = AzCallback { cb: callback, ctx: None };` so any
    /// `callback.cb` / `callback.ctx` reference still resolves.
    fn format_function_args_for_cabi_pair_raw(
        &self,
        func: &FunctionDef,
        config: &CodegenConfig,
    ) -> String {
        let self_snake = to_snake_case(&func.class_name);
        func.args
            .iter()
            .map(|arg| {
                let is_self = arg.name == "self" || arg.name == self_snake;
                let type_name = if !is_self
                    && super::managed_host_invoker::is_callback_wrapper(&arg.type_name)
                {
                    config.apply_prefix(&super::managed_host_invoker::callback_typedef_for(
                        arg.type_name.trim(),
                    ))
                } else {
                    config.apply_prefix(&arg.type_name)
                };
                let formatted = match arg.ref_kind {
                    ArgRefKind::Owned => type_name,
                    ArgRefKind::Ref => format!("&{}", type_name),
                    ArgRefKind::RefMut => format!("&mut {}", type_name),
                    ArgRefKind::Ptr => format!("*const {}", type_name),
                    ArgRefKind::PtrMut => format!("*mut {}", type_name),
                };
                format!("{}: {}", arg.name, formatted)
            })
            .collect::<Vec<_>>()
            .join(", ")
    }

    /// Args list for the pair-pattern `_WithCtx` variant. Same as the
    /// raw variant but with an extra `<arg>_ctx: AzOptionRefAny`
    /// appended after each callback-wrapper arg.
    ///
    /// The ctx arg's type is `OptionRefAny` (not bare `RefAny`) on
    /// purpose: it matches the layout of the wrapper struct's ctx
    /// field exactly, so the Rust wrapper method can `core::ptr::read`
    /// it straight from a `ManuallyDrop<AzCallback>` and pass it
    /// through without further destructuring. Managed-FFI bindings
    /// build `AzOptionRefAny::Some(refany)` at their call site; C
    /// users avoid the WithCtx form entirely and use the raw variant.
    fn format_function_args_for_cabi_pair_with_ctx(
        &self,
        func: &FunctionDef,
        config: &CodegenConfig,
    ) -> String {
        let opt_refany_type = config.apply_prefix("OptionRefAny");
        let self_snake = to_snake_case(&func.class_name);
        let mut out = Vec::with_capacity(func.args.len());
        for arg in &func.args {
            let is_self = arg.name == "self" || arg.name == self_snake;
            let is_cb = !is_self
                && super::managed_host_invoker::is_callback_wrapper(&arg.type_name);
            let type_name = if is_cb {
                config.apply_prefix(&super::managed_host_invoker::callback_typedef_for(
                    arg.type_name.trim(),
                ))
            } else {
                config.apply_prefix(&arg.type_name)
            };
            let formatted = match arg.ref_kind {
                ArgRefKind::Owned => type_name,
                ArgRefKind::Ref => format!("&{}", type_name),
                ArgRefKind::RefMut => format!("&mut {}", type_name),
                ArgRefKind::Ptr => format!("*const {}", type_name),
                ArgRefKind::PtrMut => format!("*mut {}", type_name),
            };
            out.push(format!("{}: {}", arg.name, formatted));
            if is_cb {
                out.push(format!("{}_ctx: {}", arg.name, opt_refany_type));
            }
        }
        out.join(", ")
    }

    /// Build the prologue that re-creates the callback wrapper struct
    /// from the pair-pattern args, so the user's `fn_body` (which
    /// references the wrapper by its original name) works unchanged.
    ///
    /// `with_ctx == true` produces `<ctx_field>: OptionRefAny::Some(<arg>_ctx)`,
    /// otherwise `<ctx_field>: OptionRefAny::None`. The actual ctx
    /// field name (`ctx` for core/layout callbacks, `callable` for
    /// widget callbacks) comes from
    /// [`managed_host_invoker::callback_ctx_field`].
    fn callback_pair_prologue(
        &self,
        func: &FunctionDef,
        ir: &CodegenIR,
        config: &CodegenConfig,
        with_ctx: bool,
    ) -> String {
        let mut prologue = String::new();
        let self_snake = to_snake_case(&func.class_name);
        for arg in &func.args {
            let wrapper_name = arg.type_name.trim();
            let is_self = arg.name == "self" || arg.name == self_snake;
            if is_self || !super::managed_host_invoker::is_callback_wrapper(wrapper_name) {
                continue;
            }
            let wrapper_ty = config.apply_prefix(wrapper_name);
            let opt_refany = config.apply_prefix("OptionRefAny");
            let ctx_expr = if with_ctx {
                // The WithCtx variant takes the ctx as a pre-built
                // `OptionRefAny`; pass it through verbatim. (See
                // `format_function_args_for_cabi_pair_with_ctx` for
                // why the arg type is the option rather than a bare
                // refany.)
                format!("{}_ctx", arg.name)
            } else {
                format!("{}::None", opt_refany)
            };
            let ctx_field = super::managed_host_invoker::callback_ctx_field(wrapper_name, ir)
                .unwrap_or_else(|| "ctx".to_string());
            // Shadow the fn-ptr binding with the constructed wrapper so
            // the user's fn_body (which expects the wrapper) sees the
            // expected name. The ctx field is named per the IR struct
            // definition — `ctx` for core/layout, `callable` for widget
            // callbacks — see `callback_ctx_field`.
            prologue.push_str(&format!(
                "let {arg} = {ty} {{ cb: {arg}, {field}: {ctx} }}; ",
                arg = arg.name,
                ty = wrapper_ty,
                field = ctx_field,
                ctx = ctx_expr,
            ));
        }
        prologue
    }

    /// Does this function have at least one callback-wrapper arg eligible
    /// for the pair-pattern emit (see [`format_function_args_for_cabi_pair_raw`])?
    ///
    /// Skips:
    ///   - args named `self` (the receiver — never the callback being
    ///     registered, even on methods of `Callback` itself).
    ///   - args matching the function's own class name in snake_case
    ///     (legacy convention; the IR sometimes surfaces the self-arg
    ///     under the class's snake name rather than `self`).
    fn has_callback_wrapper_arg(func: &FunctionDef) -> bool {
        let self_snake = to_snake_case(&func.class_name);
        func.args.iter().any(|a| {
            if a.name == "self" || a.name == self_snake {
                return false;
            }
            super::managed_host_invoker::is_callback_wrapper(&a.type_name)
        })
    }

    /// Generate test module with size/alignment verification tests
    fn generate_tests(&self, ir: &CodegenIR, config: &CodegenConfig) -> Result<String> {
        let mut builder = CodeBuilder::new(&config.indent);

        builder.line("#[cfg(test)]");
        builder.line("mod generated_tests {");
        builder.indent();
        builder.line("use super::*;");
        builder.line("use super::__dll_api_inner::dll;");
        builder.line("use core::mem;");
        builder.blank();

        // Generate size/alignment tests for each struct
        for struct_def in &ir.structs {
            if !config.should_include_type(&struct_def.name) {
                continue;
            }
            // Skip generic types - can't test size at compile time
            if !struct_def.generic_params.is_empty() {
                continue;
            }
            // Need external_path to compare sizes
            let Some(external_path) = &struct_def.external_path else {
                continue;
            };

            // Skip if external path contains generic parameters (like <T>)
            if external_path.contains('<') {
                continue;
            }

            let external_path = config.transform_external_path(external_path);
            let generated_type = format!("dll::Az{}", struct_def.name);
            let test_name = format!("test_size_align_{}", struct_def.name.to_lowercase());

            builder.line("#[test]");
            builder.line(&format!("fn {}() {{", test_name));
            builder.indent();
            builder.line(&format!(
                "let gen_size = mem::size_of::<{}>();",
                generated_type
            ));
            builder.line(&format!(
                "let ext_size = mem::size_of::<{}>();",
                external_path
            ));
            builder.line(&format!(
                "let gen_align = mem::align_of::<{}>();",
                generated_type
            ));
            builder.line(&format!(
                "let ext_align = mem::align_of::<{}>();",
                external_path
            ));
            builder.line(&format!(
                "assert_eq!(gen_size, ext_size, \"Size mismatch for {}\");",
                struct_def.name
            ));
            builder.line(&format!(
                "assert_eq!(gen_align, ext_align, \"Align mismatch for {}\");",
                struct_def.name
            ));
            builder.dedent();
            builder.line("}");
            builder.blank();
        }

        // Generate size/alignment tests for each enum
        for enum_def in &ir.enums {
            if !config.should_include_type(&enum_def.name) {
                continue;
            }
            // Skip generic types - can't test size at compile time
            if !enum_def.generic_params.is_empty() {
                continue;
            }
            // Need external_path to compare sizes
            let Some(external_path) = &enum_def.external_path else {
                continue;
            };

            // Skip if external path contains generic parameters (like <T>)
            if external_path.contains('<') {
                continue;
            }

            let external_path = config.transform_external_path(external_path);
            let generated_type = format!("dll::Az{}", enum_def.name);
            let test_name = format!("test_size_align_{}", enum_def.name.to_lowercase());

            builder.line("#[test]");
            builder.line(&format!("fn {}() {{", test_name));
            builder.indent();
            builder.line(&format!(
                "let gen_size = mem::size_of::<{}>();",
                generated_type
            ));
            builder.line(&format!(
                "let ext_size = mem::size_of::<{}>();",
                external_path
            ));
            builder.line(&format!(
                "let gen_align = mem::align_of::<{}>();",
                generated_type
            ));
            builder.line(&format!(
                "let ext_align = mem::align_of::<{}>();",
                external_path
            ));
            builder.line(&format!(
                "assert_eq!(gen_size, ext_size, \"Size mismatch for {}\");",
                enum_def.name
            ));
            builder.line(&format!(
                "assert_eq!(gen_align, ext_align, \"Align mismatch for {}\");",
                enum_def.name
            ));
            builder.dedent();
            builder.line("}");
            builder.blank();
        }

        builder.dedent();
        builder.line("} // mod generated_tests");

        Ok(builder.finish())
    }
}

