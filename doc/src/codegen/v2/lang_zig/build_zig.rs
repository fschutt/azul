//! Emits a `build.zig` manifest for an azul example project.
//!
//! The Zig binding is "header-only" from the user's perspective: the
//! prebuilt native shared library (`libazul.so` / `libazul.dylib` /
//! `azul.dll`) plus `azul.h` plus our generated `azul.zig` is everything
//! they need. The `build.zig` here:
//!
//! 1. declares an executable from `hello-world.zig`,
//! 2. adds the current directory to the C include path so `@cImport`
//!    can find `azul.h`,
//! 3. adds the current directory to the library path and links
//!    `azul` as a system library,
//! 4. links `libc` (required by `@cImport`).
//!
//! The user is expected to drop `azul.h`, `azul.zig`, and the prebuilt
//! native library into the same directory as `build.zig` (or adjust the
//! paths below).

/// Generate the contents of a `build.zig` file as a String.
///
/// The output is intentionally minimal and self-contained — Zig's
/// build system gets churned often, but this shape (declaring an
/// executable, adding include/library paths, linking system libraries)
/// has been stable since 0.11. Callers using a much older or much
/// newer Zig may need to tweak the API names.
pub fn generate_build_zig() -> String {
    String::from(
        r#"// build.zig — auto-generated by azul-doc codegen v2 (lang_zig).
//
// This is a minimal Zig build manifest for an azul example. Drop
// `azul.h`, `azul.zig`, and the prebuilt native library
// (`libazul.so` / `libazul.dylib` / `azul.dll`) into the same directory
// as this file, then run `zig build run`.
//
// Tested against Zig 0.16. For older Zig (0.11 / 0.12) the
// `addExecutable` call took `root_source_file` / `target` /
// `optimize` directly; 0.13 introduced the `Module` indirection and
// 0.16 dropped the legacy fields entirely. We target the current
// stable API.

const std = @import("std");

pub fn build(b: *std.Build) void {
    const target = b.standardTargetOptions(.{});
    const optimize = b.standardOptimizeOption(.{});

    const exe_mod = b.createModule(.{
        .root_source_file = b.path("hello-world.zig"),
        .target = target,
        .optimize = optimize,
        .link_libc = true,
    });
    // `@cImport(@cInclude("azul.h"))` needs the header on the C path.
    exe_mod.addIncludePath(b.path("."));

    // Find and link the prebuilt native library. Adjust the path if the
    // shared library lives elsewhere.
    exe_mod.addLibraryPath(b.path("."));
    exe_mod.linkSystemLibrary("azul", .{});

    const exe = b.addExecutable(.{
        .name = "hello-world",
        .root_module = exe_mod,
    });

    b.installArtifact(exe);

    const run_cmd = b.addRunArtifact(exe);
    run_cmd.step.dependOn(b.getInstallStep());
    if (b.args) |args| {
        run_cmd.addArgs(args);
    }

    const run_step = b.step("run", "Run the example");
    run_step.dependOn(&run_cmd.step);
}
"#,
    )
}
