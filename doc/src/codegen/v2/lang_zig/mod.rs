/// The `Az`-prefixed FFI type name for an IR type
/// (e.g. `Dom` → `AzDom`).
pub fn ffi_type_name(name: &str) -> String {
    format!("Az{}", name)
}

/// Sanitize a name for use as a Zig identifier. Zig keywords get an
/// `@"…"` quoted-identifier wrapper.
pub fn sanitize_identifier(name: &str) -> String {
    if is_zig_keyword(name) {
        format!("@\"{}\"", name)
    } else {
        name.to_string()
    }
}

fn is_zig_keyword(s: &str) -> bool {
    matches!(
        s,
        "addrspace"
            | "align"
            | "allowzero"
            | "and"
            | "anyframe"
            | "anytype"
            | "asm"
            | "async"
            | "await"
            | "break"
            | "callconv"
            | "catch"
            | "comptime"
            | "const"
            | "continue"
            | "defer"
            | "else"
            | "enum"
            | "errdefer"
            | "error"
            | "export"
            | "extern"
            | "fn"
            | "for"
            | "if"
            | "inline"
            | "linksection"
            | "noalias"
            | "nosuspend"
            | "noinline"
            | "or"
            | "orelse"
            | "packed"
            | "pub"
            | "resume"
            | "return"
            | "linksection_"
            | "struct"
            | "suspend"
            | "switch"
            | "test"
            | "threadlocal"
            | "try"
            | "type"
            | "union"
            | "unreachable"
            | "usingnamespace"
            | "var"
            | "volatile"
            | "while"
            // Primitive type names — Zig 0.16 treats them as
            // reserved identifiers ("name shadows primitive 'X'") when
            // they appear in declaration position (`pub fn null()` /
            // `pub fn u8()` / etc.). The C ABI legitimately exposes
            // methods named after primitives (e.g. `RawImageData.u8`,
            // `Json.null`); they need the `@"..."` escape just like
            // language keywords.
            | "anyerror"
            | "anyopaque"
            | "bool"
            | "c_int"
            | "c_long"
            | "c_longdouble"
            | "c_longlong"
            | "c_short"
            | "c_uint"
            | "c_ulong"
            | "c_ulonglong"
            | "c_ushort"
            | "comptime_float"
            | "comptime_int"
            | "f128"
            | "f16"
            | "f32"
            | "f64"
            | "f80"
            | "false"
            | "i128"
            | "i16"
            | "i32"
            | "i64"
            | "i8"
            | "isize"
            | "noreturn"
            | "null"
            | "true"
            | "u128"
            | "u16"
            | "u32"
            | "u64"
            | "u8"
            | "undefined"
            | "usize"
            | "void"
    )
}
