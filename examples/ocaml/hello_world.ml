(* OCaml port of examples/c/hello-world.c using the autogenerated
   bindings from `doc/src/codegen/v2/lang_ocaml/`.

   Same shape as examples/lua/hello-world.lua, examples/ruby/hello-world.rb,
   and examples/python/hello-world.py — `Azul.RefAny.wrap` stashes an
   OCaml value, callbacks are plain OCaml functions, and the framework
   keeps everything alive via the host-handle RefAny refcount. The
   codegen routes callbacks through libazul's host-invoker
   (`AzCallback_createFromHostHandle`, `AzApp_setCallbackInvoker`) so
   the OCaml callable is held alive by the framework's RefAny refcount.

   Build (Dune):
       opam install ctypes ctypes-foreign
       dune build
       LD_LIBRARY_PATH=path/to/libazul ./_build/default/hello_world.exe

   ── Codegen status ────────────────────────────────────────────────
   As of this commit the host-invoker prelude is wired for Lua and
   Ruby. The OCaml adapter (`lang_ocaml/`) needs an analogous
   `managed.ml` emitting:
     * `Foreign.foreign "AzApp_setHostHandleReleaser"
         (Ctypes.funptr Ctypes.uint64_t @-> Ctypes.returning Ctypes.void)`
     * `Foreign.foreign "AzCallback_createFromHostHandle"
         (Ctypes.uint64_t @-> Ctypes.returning AzCallback.t)`
       (and the matching LayoutCallback / VirtualViewCallback variants)
     * an id-keyed `Hashtbl` storing OCaml callables, with `Root.create`
       to keep the GC from collecting them while a RefAny clone is alive,
     * `Azul.register_callback` that returns the Az<Wrapper>
       constructed via `_createFromHostHandle`.
   Once that lands this example runs unchanged. *)

open Azul

(* ── Data model ──────────────────────────────────────────────────── *)
(* `Azul.RefAny.wrap value` stashes an OCaml value in a package-level
   table and returns an AzRefAny whose payload is just the integer
   handle. The destructor that fires on the last clone calls back
   through the registered host releaser to drop the entry. *)

type model = { mutable counter : int }
let model : model = { counter = 5 }
let data = RefAny.wrap model

(* ── Callback: button click ─────────────────────────────────────── *)

let on_click data_ptr _info_ptr =
  match RefAny.unwrap data_ptr with
  | Some m ->
      m.counter <- m.counter + 1;
      Update.RefreshDom
  | None -> Update.DoNothing

(* ── Callback: layout (rebuilds DOM each frame) ─────────────────── *)

let layout data_ptr _info_ptr =
  match RefAny.unwrap data_ptr with
  | None -> Dom.create_body ()
  | Some m ->
      (* Counter label (wrapped in a div so the font-size sticks). *)
      let label_text = String.copy_from_string (string_of_int m.counter) in
      let label = Dom.create_text label_text in
      let label_wrapper = Dom.create_div () in
      Dom.add_css_property label_wrapper
        (CssPropertyWithConditions.simple
           (CssProperty.font_size (StyleFontSize.px 32.0)));
      Dom.add_child label_wrapper label;

      (* Increment button. The wrapper auto-routes `on_click` through
         `Azul.register_callback "Callback" on_click` — the codegen
         passes the OCaml fn through unchanged. *)
      let button = Button.create (String.copy_from_string "Increase counter") in
      Button.set_button_type button ButtonType.Primary;
      Button.set_on_click button (RefAny.clone data_ptr) on_click;
      let button_dom = Button.dom button in

      (* Body *)
      let body = Dom.create_body () in
      Dom.add_child body label_wrapper;
      Dom.add_child body button_dom;
      body

(* ── Main ───────────────────────────────────────────────────────── *)

let () =
  let window = WindowCreateOptions.create layout in
  WindowState.set_title window.window_state
    (String.copy_from_string "Hello World");
  window.window_state.size.dimensions.width  <- 400.0;
  window.window_state.size.dimensions.height <- 300.0;

  (* NoTitleAutoInject: OS draws close/min/max buttons, framework
     auto-injects a Titlebar with drag support. *)
  window.window_state.flags.decorations <-
    WindowDecorations.NoTitleAutoInject;
  window.window_state.flags.background_material <-
    WindowBackgroundMaterial.Sidebar;

  let app = App.create data (AppConfig.create ()) in
  App.run app window
  (* `app` falls out of scope; the wrapper's Gc.finalise calls
     `AzApp_delete` automatically. *)
