//! Android backend using NativeActivity + the `android-activity` crate.
//!
//! Mirrors the structure of `headless/mod.rs` — a `CpuBackend` does layout-driven
//! CPU rendering into an `AzulPixmap`, then we blit those pixels into an
//! `ANativeWindow` lock buffer (`ANativeWindow_lock` → memcpy → `unlockAndPost`).
//!
//! There is **no Java/Kotlin code on the build path** for Phase 1-4. The whole
//! event loop, input handling, and rendering happens inside Rust via
//! `android-activity` (which provides the `android_main` entry point and the
//! `MainEvent`/`InputEvent` enums) plus `ndk` (for `NativeWindow_lock`).
//!
//! Soft-keyboard / IME support (Phase 5) needs a ~50-line Java JNI bridge that
//! is compiled with plain `javac` + `d8` — still no Gradle.

