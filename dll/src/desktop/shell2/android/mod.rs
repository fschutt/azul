/// `android_main` is the entry point that the `android-activity` crate's
/// `ANativeActivity_onCreate` shim invokes on the Android UI thread.
///
/// The user's `azul_run(app_data, ...)` call stashes `INITIAL_OPTIONS` into a
/// global static *before* the dylib hands control to `ANativeActivity_onCreate`,
/// so by the time we get here the options are ready.
#[cfg(all(target_os = "android", feature = "android-activity"))]
#[no_mangle]
pub fn android_main(app: AndroidApp) {
    log_info!(LogCategory::EventLoop, "[Android] android_main entered");

    // Retrieve initial options stashed by `run()`
    let (app_data, config, fc_cache, font_registry, root_window) = unsafe {
        match crate::desktop::shell2::run::ANDROID_INITIAL_OPTIONS.take() {
            Some(opts) => opts,
            None => {
                log_error!(
                    LogCategory::EventLoop,
                    "[Android] android_main called without INITIAL_OPTIONS set — \
                     did azul_run() run first?",
                );
                return;
            }
        }
    };

    let mut window =
        match AndroidWindow::new(root_window, fc_cache, config, app_data, font_registry) {
            Ok(w) => w,
            Err(e) => {
                log_error!(
                    LogCategory::EventLoop,
                    "[Android] AndroidWindow::new failed: {:?}",
                    e
                );
                return;
            }
        };

    // Publish the window address so AzulActivity.onWindowFocusChanged can
    // hand it to NativeGestureBridge. `&mut window` is stable for the
    // duration of this function (window stays on the stack frame). Once
    // AzulActivity has constructed the bridge, the Java side never
    // dereferences the pointer except from JNI callbacks that this same
    // thread eventually processes via MainEvent::InputAvailable.
    ANDROID_WINDOW_PTR
        .store(&mut window as *mut AndroidWindow as i64, core::sync::atomic::Ordering::SeqCst);

    // Outer driver loop — exits when MainEvent::Destroy clears window.is_open.
    while window.is_open() {
        // Block forever when we have no native surface (background); poll
        // at frame rate when we do (foreground rendering).
        let timeout = {
            #[cfg(feature = "ndk")]
            { if window.native_window.is_some() { Some(Duration::from_millis(16)) } else { None } }
            #[cfg(not(feature = "ndk"))]
            { Some(Duration::from_millis(16)) }
        };

        app.poll_events(timeout, |event| {
            handle_poll_event(&app, &mut window, event);
        });

        // Regenerate layout when something invalidated the frame (init,
        // resize, input). Populates cpu_backend.last_frame.
        if window.common.frame_needs_regeneration {
            if let Err(e) = window.regenerate_layout() {
                log_error!(
                    LogCategory::Layout,
                    "[Android] regenerate_layout failed: {}",
                    e
                );
            }
        }

        // After draining events + relayout, present a frame.
        let _ = render_frame(&mut window);
    }

    log_info!(LogCategory::EventLoop, "[Android] android_main exiting cleanly");
}

#[cfg(all(target_os = "android", feature = "android-activity"))]
fn handle_poll_event(app: &AndroidApp, window: &mut AndroidWindow, event: PollEvent<'_>) {
    match event {
        PollEvent::Main(main_event) => match main_event {
            MainEvent::InitWindow { .. } => {
                // Sync the framework's window state to the native
                // surface: physical dimensions + DPI. `regenerate_layout`
                // multiplies by `ws.size.dpi / 96.0` to derive its
                // `dpi_factor`, so a stale 96 here would yield a
                // 5x-too-small layout on a 480-dpi screen.
                let dpi = app.config().density().filter(|&d| d > 0).unwrap_or(160);
                window.common.current_window_state.size.dpi = dpi;
                #[cfg(feature = "ndk")]
                {
                    if let Some(nw) = app.native_window() {
                        let scale = dpi as f32 / 160.0;
                        let logical_w = (nw.width() as f32) / scale;
                        let logical_h = (nw.height() as f32) / scale;
                        log_debug!(
                            LogCategory::Window,
                            "[Android] InitWindow physical={}x{} dpi={} logical={}x{}",
                            nw.width(), nw.height(), dpi, logical_w, logical_h,
                        );
                        window.common.current_window_state.size.dimensions.width = logical_w;
                        window.common.current_window_state.size.dimensions.height = logical_h;
                        window.native_window = Some(nw);
                        window.common.frame_needs_regeneration = true;
                    }
                }
            }
            MainEvent::TerminateWindow { .. } => {
                log_debug!(LogCategory::Window, "[Android] TerminateWindow");
                #[cfg(feature = "ndk")]
                { window.native_window = None; }
            }
            MainEvent::WindowResized { .. } => {
                #[cfg(feature = "ndk")]
                {
                    if let Some(nw) = window.native_window.as_ref() {
                        let dpi = window.common.current_window_state.size.dpi.max(1);
                        let scale = dpi as f32 / 160.0;
                        window.common.current_window_state.size.dimensions.width =
                            (nw.width() as f32) / scale;
                        window.common.current_window_state.size.dimensions.height =
                            (nw.height() as f32) / scale;
                    }
                }
                window.common.frame_needs_regeneration = true;
            }
            MainEvent::InputAvailable => {
                drain_input(app, window);
            }
            MainEvent::Destroy => {
                window.close();
            }
            MainEvent::Pause | MainEvent::Resume { .. } => {
                // No-op for now; future: pause timers, stop animations.
            }
            _ => {}
        },
        _ => {}
    }
}

/// Drain the input queue once. Maps the first pointer of every motion event
/// to the mouse left button — enough for hover/click in the existing event
/// system; multi-touch (`TouchStart`/`TouchMove`/`TouchEnd`) is deferred.
#[cfg(all(target_os = "android", feature = "android-activity"))]
fn drain_input(app: &AndroidApp, window: &mut AndroidWindow) {
    use azul_core::geom::LogicalPosition;
    use azul_core::window::CursorPosition;

    let mut iter = match app.input_events_iter() {
        Ok(it) => it,
        Err(e) => {
            log_debug!(LogCategory::Input, "[Android] input_events_iter: {:?}", e);
            return;
        }
    };

    // dpi factor: device pixels per logical px. Android's baseline
    // density is 160 dpi (mdpi); a 480-dpi xxhdpi screen returns 480,
    // so logical = physical * (160/480) = physical * 0.333. Fall back
    // to 1.0 on the rare device that returns no density.
    let dpi = match app.config().density() {
        Some(d) if d > 0 => (d as f32) / 160.0_f32,
        _ => 1.0_f32,
    };

    // Collect state-update tuples first; do the actual state diff +
    // process_window_events outside the iterator's closure because the
    // closure borrows `app` for the input queue and we need &mut window.
    let mut motion_updates: Vec<(MotionAction, LogicalPosition)> = Vec::new();
    // (KeyAction, Option<VirtualKeyCode>) — None for unmapped keycodes.
    let mut key_updates: Vec<(KeyAction, Option<azul_core::window::VirtualKeyCode>)> =
        Vec::new();

    while iter.next(|event| {
        match event {
            InputEvent::MotionEvent(m) => {
                if let Some(p) = m.pointers().next() {
                    let pos = LogicalPosition::new(p.raw_x() / dpi, p.raw_y() / dpi);
                    motion_updates.push((m.action(), pos));
                }
            }
            InputEvent::KeyEvent(k) => {
                key_updates.push((k.action(), map_keycode(k.key_code())));
            }
            _ => {}
        }
        // unicode_char mapping (KeyCharacterMap) still TODO — gets us
        // text input from a hardware keyboard. State-diff already fires
        // VirtualKeyDown / VirtualKeyUp HoverEventFilters from the
        // current_virtual_keycode change below, which is enough for the
        // most common test cases (Enter to submit, Tab to navigate).
        InputStatus::Unhandled
    }) {}

    for (action, pos) in motion_updates {
        // Snapshot previous state — required by the state-diffing event system.
        window.common.previous_window_state =
            Some(window.common.current_window_state.clone());

        {
            let ms = &mut window.common.current_window_state.mouse_state;
            match action {
                MotionAction::Down => {
                    ms.cursor_position = CursorPosition::InWindow(pos);
                    ms.left_down = true;
                }
                MotionAction::Move | MotionAction::HoverMove => {
                    ms.cursor_position = CursorPosition::InWindow(pos);
                }
                MotionAction::Up | MotionAction::Cancel => {
                    ms.left_down = false;
                }
                _ => {}
            }
        }

        // Update CPU hit-tester at the new cursor; dispatch callbacks.
        window.update_hit_test_at(pos);
        let r = window.process_window_events(0);
        if !matches!(r, azul_core::events::ProcessEventResult::DoNothing) {
            window.common.frame_needs_regeneration = true;
        }
    }

    // Apply collected key updates.
    for (action, vkc) in key_updates {
        window.common.previous_window_state =
            Some(window.common.current_window_state.clone());
        {
            let ks = &mut window.common.current_window_state.keyboard_state;
            match (action, vkc) {
                (KeyAction::Down, Some(code)) => {
                    ks.current_virtual_keycode = Some(code).into();
                    // Add to pressed set if not already present (no
                    // VirtualKeyCodeVec dedup API exposed; rely on the
                    // event-determination to dedupe).
                    let mut v = ks.pressed_virtual_keycodes.clone().into_library_owned_vec();
                    if !v.iter().any(|c| *c == code) {
                        v.push(code);
                    }
                    ks.pressed_virtual_keycodes = v.into();
                }
                (KeyAction::Up, Some(code)) => {
                    ks.current_virtual_keycode = None.into();
                    let mut v = ks.pressed_virtual_keycodes.clone().into_library_owned_vec();
                    v.retain(|c| *c != code);
                    ks.pressed_virtual_keycodes = v.into();
                }
                _ => {}
            }
        }
        let r = window.process_window_events(0);
        if !matches!(r, azul_core::events::ProcessEventResult::DoNothing) {
            window.common.frame_needs_regeneration = true;
        }
    }

    // After callbacks have observed any native-gesture override the
    // Android `GestureDetector` JNI bridge injected this frame, clear
    // the slot so a stale OS gesture doesn't keep firing. No-op until
    // #17 wires real injection.
    if let Some(lw) = window.common.layout_window.as_mut() {
        lw.gesture_drag_manager.clear_native_gesture();
    }
}

/// Map the Android `Keycode` enum to Azul's `VirtualKeyCode` for the
/// most common navigation / editing keys. Letters and digits arrive via
/// the soft keyboard's text-input path (Sprint H follow-up — wires
/// `KeyCharacterMap.get` for unicode), so this map only handles the
/// non-character keys that callbacks query through
/// `KeyboardState::is_key_down` / `current_virtual_keycode`.
#[cfg(all(target_os = "android", feature = "android-activity"))]
fn map_keycode(k: Keycode) -> Option<azul_core::window::VirtualKeyCode> {
    use azul_core::window::VirtualKeyCode as V;
    Some(match k {
        Keycode::Enter | Keycode::NumpadEnter => V::Return,
        Keycode::Space => V::Space,
        Keycode::Tab => V::Tab,
        Keycode::Escape => V::Escape,
        Keycode::Del => V::Back,
        Keycode::ForwardDel => V::Delete,
        Keycode::DpadLeft => V::Left,
        Keycode::DpadRight => V::Right,
        Keycode::DpadUp => V::Up,
        Keycode::DpadDown => V::Down,
        Keycode::ShiftLeft => V::LShift,
        Keycode::ShiftRight => V::RShift,
        Keycode::CtrlLeft => V::LControl,
        Keycode::CtrlRight => V::RControl,
        Keycode::AltLeft => V::LAlt,
        Keycode::AltRight => V::RAlt,
        _ => return None,
    })
}

#[cfg(all(target_os = "android", feature = "android-activity", feature = "ndk", feature = "cpurender"))]
fn render_frame(window: &mut AndroidWindow) -> Result<(), WindowError> {
    // Sprint G: pixmap → ANativeWindow_lock → memcpy → unlockAndPost.
    //
    // Caller must arrange for `window.cpu_backend.last_frame` to be populated
    // (typically by a regenerate_layout() invocation). Until that wiring is
    // in place we present whatever pixmap is present, or no-op.
    let nw = match window.native_window.as_ref() {
        Some(nw) => nw,
        None => return Ok(()), // background — surface released
    };

    let pixmap = match window.cpu_backend.last_frame.as_ref() {
        Some(p) => p,
        None => return Ok(()), // nothing to present yet
    };

    let pw = pixmap.width() as i32;
    let ph = pixmap.height() as i32;
    if pw <= 0 || ph <= 0 { return Ok(()); }

    // Tell the surface we want RGBA8 of this exact size. Returns an
    // io::Error if Android can't satisfy — surface is gone or surfaceflinger
    // rejected the format; we just skip the frame.
    let _ = nw.set_buffers_geometry(
        pw, ph,
        Some(ndk::hardware_buffer_format::HardwareBufferFormat::R8G8B8A8_UNORM),
    );

    let mut guard = match nw.lock(None) {
        Ok(g) => g,
        Err(e) => {
            log_debug!(LogCategory::Rendering, "[Android] ANativeWindow_lock failed: {}", e);
            return Ok(());
        }
    };

    // Per-line blit. `lines()` returns one mutable slice per scanline,
    // trimmed to the visible width — exactly what we need for an RGBA→RGBA
    // copy where stride may exceed width.
    let src = pixmap.data();
    let src_stride = (pw as usize) * 4;
    if let Some(lines) = guard.lines() {
        for (y, line) in lines.enumerate() {
            if y >= ph as usize { break; }
            let src_off = y * src_stride;
            let end = (src_off + src_stride).min(src.len());
            let src_row = &src[src_off..end];
            let n = src_row.len().min(line.len());
            // SAFETY: line is &mut [MaybeUninit<u8>]; writing initialised
            // bytes is sound. We never read line before writing it.
            unsafe {
                let dst_ptr = line.as_mut_ptr().cast::<u8>();
                core::ptr::copy_nonoverlapping(src_row.as_ptr(), dst_ptr, n);
            }
        }
    }
    // guard.drop() calls ANativeWindow_unlockAndPost
    Ok(())
}

