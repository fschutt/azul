//! Android backend using NativeActivity + the `android-activity` crate.
//!
//! Mirrors the structure of `headless/mod.rs` — a `CpuBackend` does layout-driven
//! CPU rendering into an `AzulPixmap`, then we blit those pixels into an
//! `ANativeWindow` lock buffer (`ANativeWindow_lock` → memcpy → `unlockAndPost`).
//!
//! There is **no Java/Kotlin code on the build path** for Phase 1-4. The whole
//! event loop, input handling, and rendering happens inside Rust via
//! `android-activity` (which provides the `android_main` entry point and the
//! `MainEvent`/`InputEvent` enums) plus `ndk` (for `NativeWindow_lock`).
//!
//! Soft-keyboard / IME support (Phase 5) needs a ~50-line Java JNI bridge that
//! is compiled with plain `javac` + `d8` — still no Gradle.

use std::{
    cell::RefCell,
    ffi::c_void,
    sync::Arc,
    time::Duration,
};

use azul_core::{
    callbacks::RelayoutReason,
    refany::RefAny,
    resources::{AppConfig, ImageCache, RendererResources},
    window::{AndroidHandle, RawWindowHandle},
};
use azul_layout::{
    window::LayoutWindow,
    window_state::{FullWindowState, WindowCreateOptions},
};
use rust_fontconfig::FcFontCache;

use crate::desktop::shell2::common::{
    event::{self, CommonWindowState, PlatformWindow},
    debug_server::LogCategory,
    WindowError,
};
use crate::desktop::shell2::headless::CpuBackend;
use crate::{impl_platform_window_getters, log_debug, log_error, log_info};

#[cfg(feature = "android-activity")]
use android_activity::{AndroidApp, MainEvent, PollEvent};
#[cfg(feature = "ndk")]
use ndk::native_window::NativeWindow;

/// Events generated by the Android backend (currently only the close signal).
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum AndroidEvent {
    Close,
}

/// Rendering backend selector. CPU only on day 1 (mirrors the headless backend).
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum RenderBackend {
    Cpu,
}

/// Android platform window backed by an `ANativeWindow`.
///
/// The window pointer is `None` while the activity is in the background
/// (`MainEvent::TerminateWindow` fires before `Pause`). All rendering and hit
/// testing must be guarded against that case.
pub struct AndroidWindow {
    /// Cross-platform window state (layout, resources, event diffing).
    pub common: CommonWindowState,
    /// CPU rendering backend (replaces WebRender).
    pub cpu_backend: CpuBackend,
    /// Currently-attached `ANativeWindow`. Released on `TerminateWindow`.
    #[cfg(feature = "ndk")]
    pub native_window: Option<NativeWindow>,
    /// Rendering backend selector (always Cpu for now).
    pub backend: RenderBackend,
    /// `false` once `MainEvent::Destroy` arrives; breaks the outer loop.
    pub is_open: bool,
}

impl AndroidWindow {
    /// Construct a window with no native surface attached yet.
    /// `InitWindow` later swaps in the real `ANativeWindow`.
    pub fn new(
        options: WindowCreateOptions,
        fc_cache: Arc<FcFontCache>,
        config: AppConfig,
        app_data: RefAny,
    ) -> Result<Self, WindowError> {
        // `WindowCreateOptions::window_state` is already a `FullWindowState`,
        // mirroring how `HeadlessWindow::new` consumes it. No constructor call.
        let full_window_state = options.window_state;

        let mut layout_window = LayoutWindow::new(fc_cache.as_ref().clone())
            .map_err(|e| WindowError::PlatformError(format!("Layout init failed: {:?}", e)))?;
        layout_window.current_window_state = full_window_state.clone();
        layout_window.routes = config.routes.clone();

        Ok(Self {
            common: CommonWindowState {
                layout_window: Some(layout_window),
                current_window_state: full_window_state,
                previous_window_state: None,
                image_cache: ImageCache::default(),
                renderer_resources: RendererResources::default(),
                fc_cache,
                gl_context_ptr: azul_core::gl::OptionGlContextPtr::None,
                system_style: Arc::new(azul_css::system::SystemStyle::default()),
                app_data: Arc::new(RefCell::new(app_data)),
                scrollbar_drag_state: None,
                hit_tester: None,
                cpu_hit_tester: Some(azul_layout::headless::CpuHitTester::new()),
                last_hovered_node: None,
                document_id: None,
                id_namespace: None,
                render_api: None,
                renderer: None,
                frame_needs_regeneration: true,
                next_relayout_reason: RelayoutReason::Initial,
                display_list_initialized: false,
                display_list_dirty: false,
                a11y_dirty: true,
            },
            cpu_backend: CpuBackend::new(),
            #[cfg(feature = "ndk")]
            native_window: None,
            backend: RenderBackend::Cpu,
            is_open: true,
        })
    }

    pub fn is_open(&self) -> bool { self.is_open }
    pub fn close(&mut self) { self.is_open = false; }
}

impl PlatformWindow for AndroidWindow {
    impl_platform_window_getters!(common);

    fn get_raw_window_handle(&self) -> RawWindowHandle {
        #[cfg(feature = "ndk")]
        let ptr = self.native_window.as_ref()
            .map(|nw| nw.ptr().as_ptr() as *mut c_void)
            .unwrap_or(std::ptr::null_mut());
        #[cfg(not(feature = "ndk"))]
        let ptr = std::ptr::null_mut();

        RawWindowHandle::Android(AndroidHandle { a_native_window: ptr })
    }

    fn sync_window_state(&mut self) {}
}

// ---------------------------------------------------------------------------
// android_main entry point (called by the NativeActivity glue thread)
// ---------------------------------------------------------------------------

/// `android_main` is the entry point that the `android-activity` crate's
/// `ANativeActivity_onCreate` shim invokes on the Android UI thread.
///
/// The user's `azul_run(app_data, ...)` call stashes `INITIAL_OPTIONS` into a
/// global static *before* the dylib hands control to `ANativeActivity_onCreate`,
/// so by the time we get here the options are ready.
#[cfg(all(target_os = "android", feature = "android-activity"))]
#[no_mangle]
pub fn android_main(app: AndroidApp) {
    log_info!(LogCategory::EventLoop, "[Android] android_main entered");

    // Retrieve initial options stashed by `run()`
    let (app_data, config, fc_cache, _font_registry, root_window) = unsafe {
        match crate::desktop::shell2::run::ANDROID_INITIAL_OPTIONS.take() {
            Some(opts) => opts,
            None => {
                log_error!(
                    LogCategory::EventLoop,
                    "[Android] android_main called without INITIAL_OPTIONS set — \
                     did azul_run() run first?",
                );
                return;
            }
        }
    };

    let mut window = match AndroidWindow::new(root_window, fc_cache, config, app_data) {
        Ok(w) => w,
        Err(e) => {
            log_error!(LogCategory::EventLoop, "[Android] AndroidWindow::new failed: {:?}", e);
            return;
        }
    };

    // Outer driver loop — exits when MainEvent::Destroy clears window.is_open.
    while window.is_open() {
        // Block forever when we have no native surface (background); poll
        // at frame rate when we do (foreground rendering).
        let timeout = {
            #[cfg(feature = "ndk")]
            { if window.native_window.is_some() { Some(Duration::from_millis(16)) } else { None } }
            #[cfg(not(feature = "ndk"))]
            { Some(Duration::from_millis(16)) }
        };

        app.poll_events(timeout, |event| {
            handle_poll_event(&app, &mut window, event);
        });

        // After draining events, run process_window_events and present a frame.
        let _ = render_frame(&mut window);
    }

    log_info!(LogCategory::EventLoop, "[Android] android_main exiting cleanly");
}

#[cfg(all(target_os = "android", feature = "android-activity"))]
fn handle_poll_event(app: &AndroidApp, window: &mut AndroidWindow, event: PollEvent<'_>) {
    match event {
        PollEvent::Main(main_event) => match main_event {
            MainEvent::InitWindow { .. } => {
                #[cfg(feature = "ndk")]
                {
                    if let Some(nw) = app.native_window() {
                        log_debug!(LogCategory::Window, "[Android] InitWindow {}x{}",
                            nw.width(), nw.height());
                        window.native_window = Some(nw);
                        window.common.frame_needs_regeneration = true;
                    }
                }
            }
            MainEvent::TerminateWindow { .. } => {
                log_debug!(LogCategory::Window, "[Android] TerminateWindow");
                #[cfg(feature = "ndk")]
                { window.native_window = None; }
            }
            MainEvent::WindowResized { .. } => {
                window.common.frame_needs_regeneration = true;
            }
            MainEvent::InputAvailable => {
                // Drained inside `render_frame`'s pre-pass — see input_events()
                // calls there. Keeping the marker fires a redraw soon.
                window.common.frame_needs_regeneration = true;
            }
            MainEvent::Destroy => {
                window.close();
            }
            MainEvent::Pause | MainEvent::Resume { .. } => {
                // No-op for now; future: pause timers, stop animations.
            }
            _ => {}
        },
        _ => {}
    }
}

#[cfg(all(target_os = "android", feature = "android-activity"))]
fn render_frame(_window: &mut AndroidWindow) -> Result<(), WindowError> {
    // Sprint G fills this in: cpu_backend.render_frame → ANativeWindow_lock →
    // memcpy → unlockAndPost. For now we just yield, so the event loop is
    // non-busy and the skeleton compiles cleanly.
    Ok(())
}

