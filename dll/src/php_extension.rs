//! PHP extension entry point (feature `php-extension`).
//!
//! Built as a Zend-engine native extension via the `ext-php-rs` crate.
//! Unlike the standard php-ffi path (which rejects closure-to-fnpointer
//! by design), the Zend engine supports calling back into PHP from a C
//! function pointer pinned at extension-load time. This unblocks the
//! host-invoker pattern that all the other managed-FFI hosts use.
//!
//! ## Building
//!
//! ```sh
//! cargo build --release --features php-extension
//! # The resulting libazul.dylib (macOS) / libazul.so (Linux) is the
//! # PHP extension binary. Load it via:
//! php -d extension=/path/to/libazul.dylib hello-world.php
//! ```
//!
//! ## Surface
//!
//! Today this is a minimal scaffolding extension that proves the
//! toolchain wiring works. It exposes one PHP function,
//! `azul_version()`, returning the crate version string.
//!
//! ## Build prerequisites
//!
//! 1. **`LIBCLANG_PATH` + `DYLD_FALLBACK_LIBRARY_PATH`** — ext-php-rs's
//!    build script links against `libclang.dylib`. On macOS set:
//!    `LIBCLANG_PATH=/Library/Developer/CommandLineTools/usr/lib`
//!    and `DYLD_FALLBACK_LIBRARY_PATH=$LIBCLANG_PATH` (the second is
//!    required because the build-script binary itself dlopens
//!    libclang at runtime via its @rpath).
//! 2. **Dynamic linker flags for Zend symbols** — PHP's runtime
//!    provides `zend_*` symbols at extension-load time, not link
//!    time. On macOS pass:
//!    `RUSTFLAGS="-C link-arg=-undefined -C link-arg=dynamic_lookup"`.
//!    On Linux pass
//!    `RUSTFLAGS="-C link-arg=-Wl,--unresolved-symbols=ignore-in-object-files"`.
//!
//! Full invocation:
//!
//! ```sh
//! LIBCLANG_PATH=/Library/Developer/CommandLineTools/usr/lib \
//! DYLD_FALLBACK_LIBRARY_PATH=$LIBCLANG_PATH \
//! RUSTFLAGS="-C link-arg=-undefined -C link-arg=dynamic_lookup" \
//!   cargo build --release -p azul-dll --features php-extension
//! ```
//!
//! The full host-invoker surface (`azul_refany_create`,
//! `azul_register_callback`, the Dom builders, App::run) lives in
//! `target/codegen/php_api.rs` generated by `azul-doc codegen all`
//! — same pattern Python uses through `python_api.rs`. The codegen
//! pass for PHP isn't written yet; until it lands, this file
//! documents the path forward and keeps the feature flag green.

use ext_php_rs::prelude::*;

/// PHP function: `azul_version() : string`.
///
/// Smoke test for the extension load path. Returning a literal here
/// instead of `env!("CARGO_PKG_VERSION")` keeps the string lifetime
/// trivial.
#[php_function]
pub fn azul_version() -> &'static str {
    "0.0.7"
}

#[php_module]
pub fn get_module(module: ModuleBuilder) -> ModuleBuilder {
    // The `#[php_function]` macro alone declares the function but
    // doesn't register it; the module builder needs an explicit
    // `wrap_function!` call per function. Without this PHP loads the
    // extension successfully but `get_extension_funcs('azul-dll')`
    // returns an empty array.
    module.function(wrap_function!(azul_version))
}
