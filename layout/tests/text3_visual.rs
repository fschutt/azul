fn range(start: u32, end: u32) -> UnicodeRange {
    UnicodeRange { start, end }
}

/// Absolute path to `layout/tests/fonts/<name>`.
fn font_path(name: &str) -> PathBuf {
    PathBuf::from(concat!(env!("CARGO_MANIFEST_DIR"), "/tests/fonts")).join(name)
}

/// Output directory `target/text3_visual/` (gitignored under target/).
fn out_dir() -> PathBuf {
    // CARGO_MANIFEST_DIR = .../azul/layout ; workspace target is .../azul/target
    let mut p = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    p.pop(); // -> azul/
    p.push("target");
    p.push("text3_visual");
    std::fs::create_dir_all(&p).expect("create target/text3_visual");
    p
}

/// Build a `FontManager` with the built-in mock fonts PLUS the on-disk stress
/// fonts registered by family name and true coverage.
fn font_manager_with_stress_fonts() -> FontManager<FontRef> {
    let mut fm = FontManager::<FontRef>::new(build_font_cache()).expect("font manager");

    let load = |name: &str| std::fs::read(font_path(name)).unwrap_or_else(|e| {
        panic!("read stress font {name}: {e}");
    });

    // ASCII-covered fonts.
    fm.register_named_font("Azul Mock Liga", &load("azul-mock-liga.ttf"), vec![range(0x20, 0x7E)]);
    fm.register_named_font("Azul Mock Kern", &load("azul-mock-kern.ttf"), vec![range(0x20, 0x7E)]);
    fm.register_named_font("Azul Mock Prop", &load("azul-mock-prop.ttf"), vec![range(0x20, 0x7E)]);
    // Arabic block + space.
    fm.register_named_font(
        "Azul Mock Arabic",
        &load("azul-mock-arabic.ttf"),
        vec![range(0x20, 0x20), range(0x0600, 0x06FF)],
    );
    // 16 CJK ideographs U+4E00..U+4E0F + space.
    fm.register_named_font(
        "Azul Mock Vertical",
        &load("azul-mock-vertical.ttf"),
        vec![range(0x20, 0x20), range(0x4E00, 0x4E0F)],
    );
    fm
}

/// Render `html` at a FIXED canvas `w`x`h` and write the PNG to
/// `target/text3_visual/<scenario>.png`. Returns the absolute path.
///
/// Asserts the PNG is non-empty and the rendered canvas matches `w`x`h`, so a
/// broken render fails loudly instead of writing a blank/garbage file.
fn render_to_png(scenario: &str, html: &str, fm: &FontManager<FontRef>, w: f32, h: f32) -> PathBuf {
    let styled_dom = Dom::from_xml_string(html);
    let opts = ComponentPreviewOptions {
        width: Some(w),
        height: Some(h),
        dpi_factor: 1.0,
        ..Default::default()
    };
    let result = render_component_preview(&styled_dom, fm, opts, None)
        .unwrap_or_else(|e| panic!("[{scenario}] render_component_preview failed: {e}"));

    assert!(
        !result.png_data.is_empty(),
        "[{scenario}] png_data is empty — nothing rendered"
    );
    assert!(
        (result.content_width - w).abs() < 0.5,
        "[{scenario}] content_width {} != requested {w}",
        result.content_width
    );
    assert!(
        (result.content_height - h).abs() < 0.5,
        "[{scenario}] content_height {} != requested {h}",
        result.content_height
    );

    let path = out_dir().join(format!("{scenario}.png"));
    std::fs::write(&path, &result.png_data)
        .unwrap_or_else(|e| panic!("[{scenario}] write PNG: {e}"));
    println!(
        "[{scenario}] wrote {} ({} bytes, {}x{})",
        path.display(),
        result.png_data.len(),
        result.content_width,
        result.content_height
    );
    path
}

