/// 1. Knuth-Plass / greedy wrapping: a multi-word paragraph in a fixed-width
///    box that forces several lines; alternating word colours.
#[test]
fn knuth_plass_wrap() {
    let fm = font_manager_with_stress_fonts();
    let html = r#"
<html><head><style>
  body { margin: 0; background: white; }
  #p { font-family: "Azul Mock Prop"; font-size: 32px; width: 360px; line-height: 40px; }
  .a { color: #cc2222; } .b { color: #2266cc; } .c { color: #118844; }
</style></head><body>
  <div id="p"><span class="a">the</span> <span class="b">quick</span> <span class="c">brown</span> <span class="a">fox</span> <span class="b">jumps</span> <span class="c">over</span> <span class="a">lazy</span> <span class="b">dogs</span> <span class="c">while</span> <span class="a">running</span> <span class="b">home</span></div>
</body></html>"#;
    render_to_png("knuth_plass_wrap", html, &fm, 400.0, 260.0);
}

