//! VISUAL text-layout regression harness for the `text3` engine.
//!
//! Each test renders one text-layout scenario through the FULL solver3 + text3
//! pipeline and CPU-rasterizes it to a coloured PNG under
//! `target/text3_visual/<scenario>.png`. Because every mock-font glyph outline
//! is a filled rectangle, the PNG shows exactly where each glyph box landed
//! after shaping + layout — a human or agent can open the image and confirm at
//! a glance that wrapping / justification / dropcap / RTL shaping / vertical
//! text / ligatures / kerning are working.
//!
//! Fonts:
//!  * built-in embedded "Azul Mock Mono" (0.5 em) / "Azul Mock Wide" (1.0 em);
//!  * on-disk stress fonts in `tests/fonts/` (liga, kern, arabic, vertical,
//!    prop) loaded at RUNTIME with `std::fs::read` (never `include_bytes!`, so
//!    they stay excludable from the published crate);
//!  * an in-memory rectangle "CJK" font generated by `rect_font::build_rect_font`.
//!
//! Run SAFELY (memory-capped — a prior unbounded parallel run rebooted the box):
//! ```text
//! systemd-run --user --scope -p MemoryMax=8G -p MemorySwapMax=0 -q -- \
//!   bash -c 'cd /home/fs/Development/azul && timeout 700 cargo test -p azul-layout \
//!     --test text3_visual -- --test-threads=2 --nocapture 2>&1' | tail -40
//! ```

#![cfg(all(feature = "cpurender", feature = "text_layout", feature = "font_loading"))]

