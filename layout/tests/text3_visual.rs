/// 8. In-memory CJK: generate a ~20k-glyph rectangle font (U+4E00..U+9FFF)
///    at runtime, register it, and lay out a handful of ideographs. Proves the
///    in-memory generator + big-cmap path.
#[test]
fn cjk_inmemory() {
    let mut fm = font_manager_with_stress_fonts();

    // Full CJK Unified Ideographs block: ~20992 glyphs.
    let codepoints: Vec<u32> = (0x4E00u32..=0x9FFFu32).collect();
    let bytes = rect_font::build_rect_font(
        "Azul Rect CJK",
        /* upem */ 1000,
        /* advance */ 1000,
        /* ascent */ 800,
        /* descent (positive magnitude) */ 200,
        &codepoints,
    );
    assert!(bytes.len() > 100_000, "generated CJK font suspiciously small: {} bytes", bytes.len());
    fm.register_named_font("Azul Rect CJK", &bytes, vec![range(0x4E00, 0x9FFF)]);

    // A few ideographs spread across the block, in two colours.
    let html = "\
<html><head><style>\
  body { margin: 0; background: white; }\
  #c { font-family: \"Azul Rect CJK\"; font-size: 48px; }\
  .a { color: #cc2222; } .b { color: #2266cc; }\
</style></head><body>\
  <div id=\"c\"><span class=\"a\">\u{4E00}\u{5B57}\u{6C34}</span><span class=\"b\">\u{9F8D}\u{9FFF}</span></div>\
</body></html>";
    render_to_png("cjk_inmemory", html, &fm, 340.0, 90.0);
}
